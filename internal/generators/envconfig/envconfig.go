// Package envconfig generates .env.example files.
package envconfig

import (
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

func init() {
	emitter.RegisterTool("env", New())
}

type EnvEmitter struct{}

func New() *EnvEmitter      { return &EnvEmitter{} }
func (*EnvEmitter) IsTool() {}

func (e *EnvEmitter) Summary(_ []string) []emitter.SummaryLine {
	return []emitter.SummaryLine{
		{Dir: "./", Files: ".env.example"},
	}
}

func (e *EnvEmitter) Emit(a ast.AST, outDir string, opts emitter.EmitOptions) error {
	if opts.DryRun {
		return nil
	}
	if err := os.MkdirAll(outDir, 0755); err != nil {
		return err
	}

	content := generateEnvExample(a, outDir)
	return os.WriteFile(filepath.Join(outDir, ".env.example"), []byte(content), 0644)
}

func generateEnvExample(a ast.AST, outDir string) string {
	var sb strings.Builder
	sb.WriteString("# AUTO-GENERATED BY VELD — Customize for your environment\n")
	sb.WriteString("# Copy this file to .env and fill in the values\n\n")

	// Server
	sb.WriteString("# ── Server ────────────────────────────────────────\n")
	sb.WriteString("PORT=8080\n")
	sb.WriteString("HOST=0.0.0.0\n")
	sb.WriteString("NODE_ENV=development\n\n")

	// API
	sb.WriteString("# ── API ───────────────────────────────────────────\n")
	sb.WriteString("VELD_API_URL=http://localhost:8080\n\n")

	// Database
	sb.WriteString("# ── Database ──────────────────────────────────────\n")
	sb.WriteString("DATABASE_URL=postgresql://user:password@localhost:5432/veld_db\n")
	sb.WriteString("DB_HOST=localhost\n")
	sb.WriteString("DB_PORT=5432\n")
	sb.WriteString("DB_NAME=veld_db\n")
	sb.WriteString("DB_USER=user\n")
	sb.WriteString("DB_PASSWORD=password\n\n")

	// Auth (detect if auth module exists)
	hasAuth := false
	for _, mod := range a.Modules {
		if strings.ToLower(mod.Name) == "auth" {
			hasAuth = true
			break
		}
	}
	if hasAuth {
		sb.WriteString("# ── Authentication ────────────────────────────────\n")
		sb.WriteString("JWT_SECRET=change-me-to-a-secure-random-string\n")
		sb.WriteString("JWT_EXPIRES_IN=24h\n")
		sb.WriteString("BCRYPT_ROUNDS=10\n\n")
	}

	// Logging
	sb.WriteString("# ── Logging ───────────────────────────────────────\n")
	sb.WriteString("LOG_LEVEL=info\n\n")

	// CORS
	sb.WriteString("# ── CORS ──────────────────────────────────────────\n")
	sb.WriteString("CORS_ORIGIN=http://localhost:3000\n\n")

	// Language-specific
	lang := detectLang(outDir)
	switch lang {
	case "go":
		sb.WriteString("# ── Go-specific ───────────────────────────────────\n")
		sb.WriteString("GO_ENV=development\n")
		sb.WriteString("GIN_MODE=debug\n\n")
	case "python":
		sb.WriteString("# ── Python-specific ───────────────────────────────\n")
		sb.WriteString("FLASK_APP=app.py\n")
		sb.WriteString("FLASK_ENV=development\n")
		sb.WriteString("FLASK_DEBUG=1\n\n")
	case "java":
		sb.WriteString("# ── Java-specific ─────────────────────────────────\n")
		sb.WriteString("SPRING_PROFILES_ACTIVE=dev\n")
		sb.WriteString("SERVER_PORT=8080\n\n")
	case "csharp":
		sb.WriteString("# ── .NET-specific ─────────────────────────────────\n")
		sb.WriteString("ASPNETCORE_ENVIRONMENT=Development\n")
		sb.WriteString("ASPNETCORE_URLS=http://+:8080\n\n")
	case "php":
		sb.WriteString("# ── Laravel-specific ──────────────────────────────\n")
		sb.WriteString("APP_KEY=base64:change-me\n")
		sb.WriteString("APP_DEBUG=true\n")
		sb.WriteString("APP_URL=http://localhost:8080\n\n")
	}

	// Redis (if middleware is detected)
	hasRateLimit := false
	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			for _, mw := range act.Middleware {
				if strings.Contains(strings.ToLower(mw), "rate") {
					hasRateLimit = true
				}
			}
		}
	}
	if hasRateLimit {
		sb.WriteString("# ── Redis (for rate limiting) ─────────────────────\n")
		sb.WriteString("REDIS_URL=redis://localhost:6379\n\n")
	}

	return sb.String()
}

func detectLang(outDir string) string {
	checks := []struct {
		file string
		lang string
	}{
		{"go.mod", "go"},
		{"Cargo.toml", "rust"},
		{"pom.xml", "java"},
		{"VeldGenerated.csproj", "csharp"},
		{"composer.json", "php"},
	}
	for _, c := range checks {
		if _, err := os.Stat(filepath.Join(outDir, c.file)); err == nil {
			return c.lang
		}
	}
	if _, err := os.Stat(filepath.Join(outDir, "schemas", "schemas.py")); err == nil {
		return "python"
	}
	return "node"
}
