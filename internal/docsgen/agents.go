package docsgen

import (
	"fmt"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/config"
)

// BuildAgentsMd generates AGENTS.md — a compact, AI-optimised summary of the
// entire API contract. Designed to be ingested in a single context read.
func BuildAgentsMd(a ast.AST, rc config.ResolvedConfig) string {
	var sb strings.Builder

	sb.WriteString("# API Contract — AGENTS.md\n")
	sb.WriteString("> Auto-generated by Veld. Do not edit. Regenerate: `veld export agents`\n\n")

	if rc.Description != "" {
		sb.WriteString(rc.Description + "\n\n")
	}

	// ── How to use ────────────────────────────────────────────────────────────
	sb.WriteString("## How to use this project\n")
	sb.WriteString("- Import from `@veld/generated`\n")
	sb.WriteString(fmt.Sprintf("- Backend: `%s` — implement interfaces in `generated/interfaces/`\n", rc.Backend))
	sb.WriteString(fmt.Sprintf("- Frontend: `%s` SDK — use `generated/client/api.ts`\n", rc.Frontend))
	sb.WriteString("- **Never edit files in `generated/`** — overwritten on `veld generate`\n\n")

	// ── Services map (only shown when per-service URLs are configured) ─────
	if len(rc.Services) > 0 {
		sb.WriteString("## Services\n")
		sb.WriteString("| Module | Base URL |\n")
		sb.WriteString("|--------|----------|\n")
		for _, mod := range a.Modules {
			url := rc.Services[mod.Name]
			if url == "" {
				url = rc.BaseUrl
			}
			if url == "" {
				url = "(env: VELD_API_URL)"
			}
			sb.WriteString(fmt.Sprintf("| %s | %s |\n", mod.Name, url))
		}
		sb.WriteString("\n")
	}

	// ── API Modules ───────────────────────────────────────────────────────────
	sb.WriteString("## API Modules\n\n")
	for _, mod := range a.Modules {
		sb.WriteString(fmt.Sprintf("### %s", mod.Name))

		// Resolve base URL for this module
		baseUrl := ""
		if mod.BaseUrl != "" {
			baseUrl = mod.BaseUrl
		} else if rc.Services != nil {
			baseUrl = rc.Services[mod.Name]
		}
		if baseUrl == "" {
			baseUrl = rc.BaseUrl
		}
		if baseUrl != "" {
			sb.WriteString(fmt.Sprintf(" — `%s`", baseUrl))
		}
		sb.WriteString("\n")

		if mod.Description != "" {
			sb.WriteString(mod.Description + "\n")
		}

		if len(mod.Actions) == 0 {
			sb.WriteString("_(no actions)_\n\n")
			continue
		}

		sb.WriteString("\n| Action | Method | Path | Input | Output | Stream |\n")
		sb.WriteString("|--------|--------|------|-------|--------|--------|\n")
		for _, act := range mod.Actions {
			path := act.Path
			if mod.Prefix != "" {
				path = mod.Prefix + act.Path
			}
			input := "—"
			if act.Input != "" {
				input = act.Input
			}
			output := "—"
			if act.Output != "" {
				output = act.Output
				if act.OutputArray {
					output += "[]"
				}
			}
			stream := wsStreamLabel(act)
			deprecated := ""
			if act.Deprecated != "" {
				deprecated = " ~~deprecated~~"
			}
			sb.WriteString(fmt.Sprintf("| %s%s | %s | `%s` | %s | %s | %s |\n",
				act.Name, deprecated, act.Method, path, input, output, stream))
		}
		sb.WriteString("\n")
	}

	// ── Models ────────────────────────────────────────────────────────────────
	if len(a.Models) > 0 {
		sb.WriteString("## Models\n\n")
		for _, m := range a.Models {
			sb.WriteString(fmt.Sprintf("### %s", m.Name))
			if m.Extends != "" {
				sb.WriteString(fmt.Sprintf(" _(extends %s)_", m.Extends))
			}
			sb.WriteString("\n")
			if m.Description != "" {
				sb.WriteString(m.Description + "\n")
			}
			sb.WriteString("\n| Field | Type | Required | Notes |\n")
			sb.WriteString("|-------|------|----------|-------|\n")
			for _, f := range m.Fields {
				typ := f.Type
				if f.IsArray {
					typ += "[]"
				}
				if f.IsMap {
					typ = "Map<string, " + f.MapValueType + ">"
				}
				required := "yes"
				if f.Optional {
					required = "no"
				}
				var notes []string
				if f.Deprecated != "" {
					notes = append(notes, "@deprecated: "+f.Deprecated)
				}
				if f.Default != "" {
					notes = append(notes, "@default: "+f.Default)
				}
				if f.Unique {
					notes = append(notes, "@unique")
				}
				sb.WriteString(fmt.Sprintf("| %s | %s | %s | %s |\n",
					f.Name, typ, required, strings.Join(notes, " · ")))
			}
			sb.WriteString("\n")
		}
	}

	// ── Enums ─────────────────────────────────────────────────────────────────
	if len(a.Enums) > 0 {
		sb.WriteString("## Enums\n\n")
		for _, e := range a.Enums {
			sb.WriteString(fmt.Sprintf("### %s\n", e.Name))
			quoted := make([]string, len(e.Values))
			for i, v := range e.Values {
				quoted[i] = "`" + v + "`"
			}
			sb.WriteString("Values: " + strings.Join(quoted, ", ") + "\n\n")
		}
	}

	// ── SDK usage examples ────────────────────────────────────────────────────
	sb.WriteString("## SDK Usage\n\n")

	sb.WriteString("### Frontend (TypeScript)\n")
	sb.WriteString("```ts\n")
	sb.WriteString("import { VeldApiClient } from '@veld/generated/client/api';\n\n")
	if len(rc.Services) > 0 {
		sb.WriteString("// Each module can point to a different service URL:\n")
		sb.WriteString("const api = new VeldApiClient({\n")
		for name, url := range rc.Services {
			sb.WriteString(fmt.Sprintf("  // %s: '%s',\n", name, url))
		}
		sb.WriteString("});\n")
	} else if rc.BaseUrl != "" {
		sb.WriteString(fmt.Sprintf("const api = new VeldApiClient({ baseUrl: '%s' });\n", rc.BaseUrl))
	} else {
		sb.WriteString("// baseUrl read from VELD_API_URL env var, or pass it explicitly:\n")
		sb.WriteString("const api = new VeldApiClient({ baseUrl: 'https://api.example.com' });\n")
	}
	sb.WriteString("```\n\n")

	// Show one example action if any exist
	if len(a.Modules) > 0 && len(a.Modules[0].Actions) > 0 {
		mod := a.Modules[0]
		act := mod.Actions[0]
		modLower := strings.ToLower(mod.Name)
		actCamel := toCamel(act.Name)
		sb.WriteString("```ts\n")
		if act.Input != "" {
			sb.WriteString(fmt.Sprintf("const result = await api.%s.%s(input);\n", modLower, actCamel))
		} else {
			sb.WriteString(fmt.Sprintf("const result = await api.%s.%s();\n", modLower, actCamel))
		}
		sb.WriteString("```\n\n")
	}

	sb.WriteString("### Backend (Node.js)\n")
	sb.WriteString("```ts\n")
	if len(a.Modules) > 0 {
		modName := a.Modules[0].Name
		sb.WriteString(fmt.Sprintf("import type { I%sService } from '@veld/generated/interfaces/I%sService';\n\n", modName, modName))
		sb.WriteString(fmt.Sprintf("export class %sServiceImpl implements I%sService {\n", modName, modName))
		sb.WriteString("  // Implement all methods — types are enforced by the interface\n")
		sb.WriteString("}\n")
	}
	sb.WriteString("```\n\n")

	// ── Machine-readable specs ────────────────────────────────────────────────
	sb.WriteString("## Machine-readable specs\n")
	sb.WriteString("- OpenAPI 3.0: `veld export openapi -o openapi.json`\n")
	sb.WriteString("- GraphQL SDL: `veld export graphql`\n")
	sb.WriteString("- Database schema: `veld export schema`\n")

	return sb.String()
}

// wsStreamLabel returns the Stream column value for the AGENTS.md action table.
func wsStreamLabel(act ast.Action) string {
	if act.Method != "WS" {
		return "—"
	}
	switch {
	case act.Stream != "" && act.Emit != "":
		return act.Stream + " ↔ " + act.Emit
	case act.Stream != "":
		return act.Stream
	case act.Emit != "":
		return "→ " + act.Emit
	default:
		return "—"
	}
}

// toCamel converts PascalCase to camelCase for the usage example.
func toCamel(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}
