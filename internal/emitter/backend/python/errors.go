package python

// errors.go — emits errors/_base.py, errors/{module_lower}_errors.py, errors/__init__.py
// with typed error classes, convenience subclasses, type guards, and error handler.
// Matches the comprehensive approach used by the TypeScript/Node emitter.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitAllErrors generates the full errors/ directory in one shot.
// Called once from Emit() — replaces the old per-module emitErrors call.
func (e *PythonEmitter) emitAllErrors(a ast.AST, outDir string) error {
	hasAny := false
	for _, mod := range a.Modules {
		if emitter.HasErrors(mod) {
			hasAny = true
			break
		}
	}
	if !hasAny {
		return nil
	}

	dir := filepath.Join(outDir, "errors")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	if err := emitPythonErrorsBase(dir); err != nil {
		return err
	}
	for _, mod := range a.Modules {
		if err := emitPythonModuleErrors(mod, dir); err != nil {
			return fmt.Errorf("errors %s: %w", mod.Name, err)
		}
	}
	return emitPythonErrorsBarrel(a, dir)
}

// emitPythonErrorsBase writes errors/_base.py with the core ApiError class,
// 7 convenience subclasses, type guard helpers, to_error_response(), and
// a framework-agnostic error handler function.
func emitPythonErrorsBase(dir string) error {
	var sb strings.Builder
	sb.WriteString("# AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString("from __future__ import annotations\n\n")
	sb.WriteString("from datetime import datetime, timezone\n")
	sb.WriteString("from typing import Any, Dict, Optional, Tuple\n\n\n")

	// ── Core ApiError class
	sb.WriteString("class ApiError(Exception):\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    Base typed API error. Every Veld-generated error extends this class.\n")
	sb.WriteString("    The ``code`` attribute carries the machine-readable error code string.\n")
	sb.WriteString("\n")
	sb.WriteString("    Example::\n")
	sb.WriteString("\n")
	sb.WriteString("        raise ApiError('GET_USER_NOT_FOUND', 404, 'user not found')\n")
	sb.WriteString("    \"\"\"\n\n")
	sb.WriteString("    def __init__(self, code: str, status: int, message: Optional[str] = None) -> None:\n")
	sb.WriteString("        super().__init__(message or code)\n")
	sb.WriteString("        self.code = code\n")
	sb.WriteString("        self.status = status\n")
	sb.WriteString("        self.timestamp = datetime.now(timezone.utc).isoformat()\n\n")
	sb.WriteString("    def to_dict(self) -> Dict[str, Any]:\n")
	sb.WriteString("        \"\"\"Serialise to a JSON-safe dictionary.\"\"\"\n")
	sb.WriteString("        return {\"error\": str(self), \"code\": self.code, \"status\": self.status}\n\n\n")

	// ── Type guards
	sb.WriteString("# ── Type Guards ──────────────────────────────────────────────────────\n\n")

	sb.WriteString("def is_api_error(err: BaseException) -> bool:\n")
	sb.WriteString("    \"\"\"Check if an exception is an ApiError instance.\"\"\"\n")
	sb.WriteString("    return isinstance(err, ApiError)\n\n\n")

	sb.WriteString("def is_error_code(err: BaseException, code: str) -> bool:\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    Check if an error matches a specific error code.\n")
	sb.WriteString("\n")
	sb.WriteString("    Example::\n")
	sb.WriteString("\n")
	sb.WriteString("        if is_error_code(err, 'GET_USER_NOT_FOUND'):\n")
	sb.WriteString("            return jsonify({'error': 'User not found'}), 404\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    return isinstance(err, ApiError) and err.code == code\n\n\n")

	sb.WriteString("def is_http_status(err: BaseException, status: int) -> bool:\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    Check if an error has a specific HTTP status code.\n")
	sb.WriteString("\n")
	sb.WriteString("    Example::\n")
	sb.WriteString("\n")
	sb.WriteString("        if is_http_status(err, 404):\n")
	sb.WriteString("            ...\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    return isinstance(err, ApiError) and err.status == status\n\n\n")

	// ── Convenience error subclasses
	sb.WriteString("# ── Convenience Errors ───────────────────────────────────────────────\n\n")

	type errDef struct {
		name      string
		status    int
		msg       string
		codeConst string
	}
	defs := []errDef{
		{"NotFoundError", 404, "Not Found", "NOT_FOUND"},
		{"BadRequestError", 400, "Bad Request", "BAD_REQUEST"},
		{"UnauthorizedError", 401, "Unauthorized", "UNAUTHORIZED"},
		{"ForbiddenError", 403, "Forbidden", "FORBIDDEN"},
		{"ConflictError", 409, "Conflict", "CONFLICT"},
		{"ValidationError", 422, "Validation Failed", "VALIDATION_FAILED"},
		{"InternalError", 500, "Internal Server Error", "INTERNAL_SERVER_ERROR"},
	}

	for _, d := range defs {
		sb.WriteString(fmt.Sprintf("class %s(ApiError):\n", d.name))
		sb.WriteString(fmt.Sprintf("    \"\"\"%d %s — raise from service implementations for quick status responses.\"\"\"\n\n", d.status, d.msg))
		sb.WriteString(fmt.Sprintf("    def __init__(self, message: Optional[str] = None, code: Optional[str] = None) -> None:\n"))
		sb.WriteString(fmt.Sprintf("        super().__init__(code or '%s', %d, message or '%s')\n\n\n", d.codeConst, d.status, d.msg))
	}

	// ── Error response helper
	sb.WriteString("# ── Error Response Helper ────────────────────────────────────────────\n\n")

	sb.WriteString("def to_error_response(err: BaseException) -> Dict[str, Any]:\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    Convert any caught exception into a consistent {error, code, status} dict.\n")
	sb.WriteString("\n")
	sb.WriteString("    Example::\n")
	sb.WriteString("\n")
	sb.WriteString("        except Exception as err:\n")
	sb.WriteString("            resp = to_error_response(err)\n")
	sb.WriteString("            return jsonify(resp), resp['status']\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    if isinstance(err, ApiError):\n")
	sb.WriteString("        return {\"error\": str(err), \"code\": err.code, \"status\": err.status}\n")
	sb.WriteString("    if isinstance(err, Exception):\n")
	sb.WriteString("        return {\"error\": str(err), \"code\": \"INTERNAL_ERROR\", \"status\": 500}\n")
	sb.WriteString("    return {\"error\": str(err), \"code\": \"UNKNOWN_ERROR\", \"status\": 500}\n\n\n")

	// ── Error handler
	sb.WriteString("# ── Error Handler ────────────────────────────────────────────────────\n\n")

	sb.WriteString("def error_handler(err: BaseException) -> Tuple[Dict[str, Any], int]:\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    Framework-agnostic error handler.\n")
	sb.WriteString("    Returns a (body_dict, status_code) tuple suitable for Flask/FastAPI/etc.\n")
	sb.WriteString("\n")
	sb.WriteString("    Flask usage::\n")
	sb.WriteString("\n")
	sb.WriteString("        @app.errorhandler(ApiError)\n")
	sb.WriteString("        def handle_api_error(err):\n")
	sb.WriteString("            body, status = error_handler(err)\n")
	sb.WriteString("            return jsonify(body), status\n")
	sb.WriteString("\n")
	sb.WriteString("    FastAPI usage::\n")
	sb.WriteString("\n")
	sb.WriteString("        @app.exception_handler(ApiError)\n")
	sb.WriteString("        async def handle_api_error(request, err):\n")
	sb.WriteString("            body, status = error_handler(err)\n")
	sb.WriteString("            return JSONResponse(body, status_code=status)\n")
	sb.WriteString("    \"\"\"\n")
	sb.WriteString("    resp = to_error_response(err)\n")
	sb.WriteString("    return {\"error\": resp[\"error\"], \"code\": resp[\"code\"]}, resp[\"status\"]\n")

	return os.WriteFile(filepath.Join(dir, "_base.py"), []byte(sb.String()), 0644)
}

// emitPythonModuleErrors writes errors/{module_lower}_errors.py with typed
// per-action error subclasses and factory functions.
func emitPythonModuleErrors(mod ast.Module, dir string) error {
	if !emitter.HasErrors(mod) {
		return nil
	}

	moduleLower := strings.ToLower(mod.Name)
	var sb strings.Builder

	sb.WriteString("# AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString("from __future__ import annotations\n\n")
	sb.WriteString("from typing import Literal, Optional\n\n")
	sb.WriteString("from ._base import ApiError\n\n\n")

	for _, act := range mod.Actions {
		if len(act.Errors) == 0 {
			continue
		}

		var codes []string
		for _, errName := range act.Errors {
			code := emitter.ErrorCode(act.Name, errName)
			codes = append(codes, fmt.Sprintf("\"%s\"", code))
		}

		sb.WriteString(fmt.Sprintf("# Error codes for %s\n", act.Name))
		sb.WriteString(fmt.Sprintf("%sErrorCode = Literal[%s]\n\n\n", act.Name, strings.Join(codes, ", ")))

		sb.WriteString(fmt.Sprintf("class %sError(ApiError):\n", act.Name))
		sb.WriteString(fmt.Sprintf("    \"\"\"Typed error for %s — only allows %s error codes.\"\"\"\n", act.Name, act.Name))
		sb.WriteString(fmt.Sprintf("    code: %sErrorCode  # type: ignore[assignment]\n\n\n", act.Name))

		snakeAction := emitter.ToSnakeCase(act.Name)
		for _, errName := range act.Errors {
			code := emitter.ErrorCode(act.Name, errName)
			status := emitter.ErrorHTTPStatus(errName)
			snakeErr := emitter.ToSnakeCase(errName)
			funcName := snakeAction + "_" + snakeErr
			sb.WriteString(fmt.Sprintf("def %s(message: Optional[str] = None) -> %sError:\n", funcName, act.Name))
			sb.WriteString(fmt.Sprintf("    \"\"\"Create a %s error for the %s action.\"\"\"\n", errName, act.Name))
			sb.WriteString(fmt.Sprintf("    return %sError(\"%s\", %d, message or \"%s\")\n\n\n", act.Name, code, status, errName))
		}
	}

	sb.WriteString(fmt.Sprintf("class %sErrors:\n", moduleLower))
	sb.WriteString(fmt.Sprintf("    \"\"\"Namespace for all %s module error factories.\"\"\"\n\n", mod.Name))
	for _, act := range mod.Actions {
		if len(act.Errors) == 0 {
			continue
		}
		snakeAction := emitter.ToSnakeCase(act.Name)
		for _, errName := range act.Errors {
			snakeErr := emitter.ToSnakeCase(errName)
			funcName := snakeAction + "_" + snakeErr
			sb.WriteString(fmt.Sprintf("    %s = staticmethod(%s)\n", funcName, funcName))
		}
	}
	sb.WriteString("\n")

	return os.WriteFile(filepath.Join(dir, moduleLower+"_errors.py"), []byte(sb.String()), 0644)
}

// emitPythonErrorsBarrel writes errors/__init__.py that re-exports everything.
func emitPythonErrorsBarrel(a ast.AST, dir string) error {
	var sb strings.Builder
	sb.WriteString("# AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")

	sb.WriteString("# Core error class and type guards\n")
	sb.WriteString("from ._base import (\n")
	sb.WriteString("    ApiError,\n")
	sb.WriteString("    is_api_error,\n")
	sb.WriteString("    is_error_code,\n")
	sb.WriteString("    is_http_status,\n")
	sb.WriteString("    to_error_response,\n")
	sb.WriteString("    error_handler,\n")
	sb.WriteString(")\n\n")

	sb.WriteString("# Convenience error classes\n")
	sb.WriteString("from ._base import (\n")
	sb.WriteString("    NotFoundError,\n")
	sb.WriteString("    BadRequestError,\n")
	sb.WriteString("    UnauthorizedError,\n")
	sb.WriteString("    ForbiddenError,\n")
	sb.WriteString("    ConflictError,\n")
	sb.WriteString("    ValidationError,\n")
	sb.WriteString("    InternalError,\n")
	sb.WriteString(")\n\n")

	for _, mod := range a.Modules {
		if !emitter.HasErrors(mod) {
			continue
		}
		moduleLower := strings.ToLower(mod.Name)
		sb.WriteString(fmt.Sprintf("# %s module errors\n", mod.Name))
		sb.WriteString(fmt.Sprintf("from .%s_errors import *  # noqa: F401,F403\n", moduleLower))
	}

	sb.WriteString("\n__all__ = [\n")
	sb.WriteString("    # Base\n")
	sb.WriteString("    'ApiError',\n")
	sb.WriteString("    'is_api_error',\n")
	sb.WriteString("    'is_error_code',\n")
	sb.WriteString("    'is_http_status',\n")
	sb.WriteString("    'to_error_response',\n")
	sb.WriteString("    'error_handler',\n")
	sb.WriteString("    # Convenience\n")
	sb.WriteString("    'NotFoundError',\n")
	sb.WriteString("    'BadRequestError',\n")
	sb.WriteString("    'UnauthorizedError',\n")
	sb.WriteString("    'ForbiddenError',\n")
	sb.WriteString("    'ConflictError',\n")
	sb.WriteString("    'ValidationError',\n")
	sb.WriteString("    'InternalError',\n")
	sb.WriteString("]\n")

	return os.WriteFile(filepath.Join(dir, "__init__.py"), []byte(sb.String()), 0644)
}

// emitErrors is kept for backward compat — now a no-op since emitAllErrors handles everything.
func (e *PythonEmitter) emitErrors(mod ast.Module, outDir string) error {
	return nil
}
