func TestPythonEmitterPathParams(t *testing.T) {
	e := python.New()
	outDir := t.TempDir()

	a := ast.AST{
		Models: []ast.Model{
			{Name: "Item", Fields: []ast.Field{{Name: "id", Type: "string"}}},
		},
		Modules: []ast.Module{
			{
				Name: "Items",
				Actions: []ast.Action{
					{Name: "GetById", Method: "GET", Path: "/items/:id", Output: "Item"},
					{Name: "Delete", Method: "DELETE", Path: "/items/:id"},
				},
			},
		},
	}

	if err := e.Emit(a, outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, _ := os.ReadFile(filepath.Join(outDir, "routes", "items_routes.py"))
	content := string(data)

	// Flask uses <id> syntax in URL rules.
	if !strings.Contains(content, "<id>") {
		t.Error("Flask routes should use <id> path syntax")
	}
	// The handler function should receive id as a parameter.
	if !strings.Contains(content, "def items_get_by_id(id)") {
		t.Errorf("handler function should have id param, got:\n%s", content)
	}
}

