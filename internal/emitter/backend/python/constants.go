package python

// constants.go — emits typed constant files into constants/ directory.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

func (e *PythonEmitter) emitConstants(a ast.AST, outDir string) error {
	if len(a.Constants) == 0 {
		return nil
	}
	dir := filepath.Join(outDir, "constants")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	var groups []string

	for _, cg := range a.Constants {
		fileName := emitter.ToSnakeCase(cg.Name)
		groups = append(groups, fileName)

		var sb strings.Builder
		sb.WriteString("# AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")

		needsDecimal := false
		for _, f := range cg.Fields {
			if f.Type == "decimal" {
				needsDecimal = true
				break
			}
		}
		if needsDecimal {
			sb.WriteString("from decimal import Decimal\n\n")
		}

		if cg.Description != "" {
			sb.WriteString(fmt.Sprintf("# %s\n\n", cg.Description))
		}

		for _, f := range cg.Fields {
			pyType := pyConstType(f.Type)
			pyVal := pyConstValue(f)
			sb.WriteString(fmt.Sprintf("%s: %s = %s\n", f.Name, pyType, pyVal))
		}

		if err := os.WriteFile(filepath.Join(dir, fileName+".py"), []byte(sb.String()), 0644); err != nil {
			return err
		}
	}

	// __init__.py barrel
	var barrel strings.Builder
	barrel.WriteString("# AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	for _, g := range groups {
		barrel.WriteString(fmt.Sprintf("from .%s import *  # noqa: F401,F403\n", g))
	}
	return os.WriteFile(filepath.Join(dir, "__init__.py"), []byte(barrel.String()), 0644)
}

func pyConstType(veldType string) string {
	switch veldType {
	case "string", "uuid", "date", "datetime":
		return "str"
	case "int":
		return "int"
	case "float":
		return "float"
	case "decimal":
		return "Decimal"
	case "bool":
		return "bool"
	default:
		return "str"
	}
}

func pyConstValue(f ast.ConstantField) string {
	switch f.Type {
	case "string", "uuid", "date", "datetime":
		return fmt.Sprintf("%q", f.Value)
	case "int", "float":
		return f.Value
	case "decimal":
		return fmt.Sprintf("Decimal(%q)", f.Value)
	case "bool":
		if f.Value == "true" {
			return "True"
		}
		return "False"
	default:
		return fmt.Sprintf("%q", f.Value)
	}
}
