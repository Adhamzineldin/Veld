package rustbackend

// sdk.go â€” service SDK generation for Rust backend.
// Generates typed HTTP clients using std::io + ureq-like patterns.
// Output: sdk/<service>/client.rs, types.rs, mod.rs

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/lang"
	"github.com/Adhamzineldin/Veld/internal/emitter/sdkhelpers"
)

func (e *RustEmitter) EmitServiceSdk(consumed []emitter.ConsumedServiceInfo, outDir string, opts emitter.EmitOptions) error {
	if opts.DryRun || len(consumed) == 0 {
		return nil
	}
	sdkDir := filepath.Join(outDir, "sdk")
	if err := os.MkdirAll(sdkDir, 0755); err != nil {
		return err
	}

	var modNames []string
	for _, c := range consumed {
		name := sdkhelpers.ServiceFileName(c.Name)
		modNames = append(modNames, name)
		dir := filepath.Join(sdkDir, name)
		if err := os.MkdirAll(dir, 0755); err != nil {
			return err
		}
		if err := emitRustSdkTypes(c, dir); err != nil {
			return err
		}
		if err := emitRustSdkClient(c, dir); err != nil {
			return err
		}
		if err := emitRustSdkConstants(c, dir); err != nil {
			return err
		}
		if err := emitRustSdkMod(c, dir); err != nil {
			return err
		}
	}
	return emitRustSdkBarrel(modNames, sdkDir)
}

func emitRustSdkTypes(consumed emitter.ConsumedServiceInfo, dir string) error {
	a := consumed.AST
	modelGroups, enumGroups, _, _ := emitter.AssignModelsToModules(a)
	keys := emitter.SortedGroupKeys(modelGroups, enumGroups)

	modelsDir := filepath.Join(dir, "models")
	if err := os.MkdirAll(modelsDir, 0755); err != nil {
		return err
	}

	for _, group := range keys {
		var sb strings.Builder
		sb.WriteString("// Code generated by Veld â€” DO NOT EDIT.\nuse serde::{Deserialize, Serialize};\n\n")

		for _, en := range enumGroups[group] {
			sb.WriteString("#[derive(Debug, Clone, Serialize, Deserialize, PartialEq)]\n")
			sb.WriteString(fmt.Sprintf("pub enum %s {\n", en.Name))
			for _, v := range en.Values {
				sb.WriteString(fmt.Sprintf("    #[serde(rename = %q)]\n    %s,\n", v, rustCapitalize(v)))
			}
			sb.WriteString("}\n\n")
		}

		for _, m := range modelGroups[group] {
			sb.WriteString("#[derive(Debug, Clone, Serialize, Deserialize)]\n")
			sb.WriteString(fmt.Sprintf("pub struct %s {\n", m.Name))
			for _, f := range m.Fields {
				rsType := rustFieldType(f)
				jsonName := f.Name
				fieldName := emitter.ToSnakeCase(f.Name)
				if f.Optional {
					rsType = fmt.Sprintf("Option<%s>", rsType)
					sb.WriteString(fmt.Sprintf("    #[serde(rename = %q, skip_serializing_if = \"Option::is_none\")]\n", jsonName))
				} else {
					sb.WriteString(fmt.Sprintf("    #[serde(rename = %q)]\n", jsonName))
				}
				sb.WriteString(fmt.Sprintf("    pub %s: %s,\n", fieldName, rsType))
			}
			sb.WriteString("}\n\n")

			// impl with new() + getters/setters
			sb.WriteString(fmt.Sprintf("impl %s {\n", m.Name))
			// Constructor
			var ctorParams []string
			var ctorFields []string
			for _, f := range m.Fields {
				rsType := rustFieldType(f)
				fieldName := emitter.ToSnakeCase(f.Name)
				if f.Optional {
					ctorParams = append(ctorParams, fmt.Sprintf("%s: Option<%s>", fieldName, rsType))
				} else {
					ctorParams = append(ctorParams, fmt.Sprintf("%s: %s", fieldName, rsType))
				}
				ctorFields = append(ctorFields, fmt.Sprintf("            %s,", fieldName))
			}
			sb.WriteString(fmt.Sprintf("    pub fn new(%s) -> Self {\n        Self {\n", strings.Join(ctorParams, ", ")))
			for _, cf := range ctorFields {
				sb.WriteString(cf + "\n")
			}
			sb.WriteString("        }\n    }\n")
			// Getters and setters
			for _, f := range m.Fields {
				rsType := rustFieldType(f)
				fieldName := emitter.ToSnakeCase(f.Name)
				if f.Optional {
					sb.WriteString(fmt.Sprintf("\n    pub fn %s(&self) -> &Option<%s> { &self.%s }\n", fieldName, rsType, fieldName))
					sb.WriteString(fmt.Sprintf("    pub fn set_%s(&mut self, v: Option<%s>) { self.%s = v; }\n", fieldName, rsType, fieldName))
				} else {
					sb.WriteString(fmt.Sprintf("\n    pub fn %s(&self) -> &%s { &self.%s }\n", fieldName, rsType, fieldName))
					sb.WriteString(fmt.Sprintf("    pub fn set_%s(&mut self, v: %s) { self.%s = v; }\n", fieldName, rsType, fieldName))
				}
			}
			sb.WriteString("}\n\n")
		}

		if err := os.WriteFile(filepath.Join(modelsDir, group+".rs"), []byte(sb.String()), 0644); err != nil {
			return err
		}
	}

	// models/mod.rs barrel
	var modFile strings.Builder
	modFile.WriteString("// Code generated by Veld â€” DO NOT EDIT.\n")
	for _, group := range keys {
		modFile.WriteString(fmt.Sprintf("pub mod %s;\n", group))
	}
	modFile.WriteString("\n")
	for _, group := range keys {
		modFile.WriteString(fmt.Sprintf("pub use %s::*;\n", group))
	}
	return os.WriteFile(filepath.Join(modelsDir, "mod.rs"), []byte(modFile.String()), 0644)
}

func emitRustSdkClient(consumed emitter.ConsumedServiceInfo, dir string) error {
	var sb strings.Builder
	a := consumed.AST
	envVar := sdkhelpers.EnvVarName(consumed.Name)

	sb.WriteString("// Code generated by Veld â€” DO NOT EDIT.\n")
	sb.WriteString("use std::collections::HashMap;\nuse serde::de::DeserializeOwned;\nuse super::models::*;\n\n")

	sb.WriteString("#[derive(Debug)]\npub struct VeldApiError {\n    pub status: u16,\n    pub body: String,\n}\n\n")
	sb.WriteString("impl std::fmt::Display for VeldApiError {\n    fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {\n        write!(f, \"HTTP {}: {}\", self.status, self.body)\n    }\n}\nimpl std::error::Error for VeldApiError {}\n\n")

	sb.WriteString("pub struct Client {\n    base: String,\n    client: reqwest::blocking::Client,\n    headers: HashMap<String, String>,\n}\n\n")

	sb.WriteString("impl Client {\n")
	sb.WriteString("    pub fn new(base_url: Option<&str>) -> Self {\n")
	sb.WriteString(fmt.Sprintf("        let base = base_url.map(String::from)\n            .or_else(|| std::env::var(%q).ok())\n", envVar))
	if consumed.BaseUrl != "" {
		sb.WriteString(fmt.Sprintf("            .unwrap_or_else(|| %q.to_string());\n", consumed.BaseUrl))
	} else {
		sb.WriteString(fmt.Sprintf("            .expect(\"No base URL for %s â€” pass base_url or set %s\");\n", consumed.Name, envVar))
	}
	sb.WriteString("        Client { base, client: reqwest::blocking::Client::new(), headers: HashMap::new() }\n    }\n\n")

	sb.WriteString("    pub fn with_headers(mut self, headers: HashMap<String, String>) -> Self {\n        self.headers = headers;\n        self\n    }\n\n")

	sb.WriteString("    fn request<T: DeserializeOwned>(&self, method: &str, path: &str, body: Option<&impl serde::Serialize>) -> Result<T, Box<dyn std::error::Error>> {\n")
	sb.WriteString("        let url = format!(\"{}{}\", self.base, path);\n")
	sb.WriteString("        let mut req = match method {\n")
	sb.WriteString("            \"GET\" => self.client.get(&url),\n            \"POST\" => self.client.post(&url),\n")
	sb.WriteString("            \"PUT\" => self.client.put(&url),\n            \"PATCH\" => self.client.patch(&url),\n")
	sb.WriteString("            \"DELETE\" => self.client.delete(&url),\n            _ => self.client.request(reqwest::Method::from_bytes(method.as_bytes())?, &url),\n        };\n")
	sb.WriteString("        req = req.header(\"Content-Type\", \"application/json\");\n")
	sb.WriteString("        for (k, v) in &self.headers { req = req.header(k.as_str(), v.as_str()); }\n")
	sb.WriteString("        if let Some(b) = body { req = req.json(b); }\n")
	sb.WriteString("        let res = req.send()?;\n")
	sb.WriteString("        if !res.status().is_success() {\n            return Err(Box::new(VeldApiError { status: res.status().as_u16(), body: res.text()? }));\n        }\n")
	sb.WriteString("        Ok(res.json()?)\n    }\n")

	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			if strings.ToUpper(act.Method) == "WS" {
				continue
			}
			sb.WriteString("\n")
			writeRustSdkMethod(&sb, mod, act)
		}
	}
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, "client.rs"), []byte(sb.String()), 0644)
}

func writeRustSdkMethod(sb *strings.Builder, mod ast.Module, act ast.Action) {
	routePath := mod.Prefix + act.Path
	method := strings.ToUpper(act.Method)
	fnName := emitter.ToSnakeCase(act.Name)
	pathParams := emitter.ExtractPathParams(routePath)

	var params []string
	params = append(params, "&self")
	for _, p := range pathParams {
		params = append(params, fmt.Sprintf("%s: &str", emitter.ToSnakeCase(p)))
	}
	if act.Input != "" {
		inputType, _, _ := rustSharedAdapter.MapType(act.Input)
		params = append(params, fmt.Sprintf("input: &%s", inputType))
	}

	hasOutput := act.Output != ""
	retType := "()"
	if hasOutput {
		mapped, _, _ := rustSharedAdapter.MapType(act.Output)
		if act.OutputArray {
			retType = fmt.Sprintf("Vec<%s>", mapped)
		} else {
			retType = mapped
		}
	}

	sb.WriteString(fmt.Sprintf("    pub fn %s(%s) -> Result<%s, Box<dyn std::error::Error>> {\n", fnName, strings.Join(params, ", "), retType))

	// Build path
	if len(pathParams) > 0 {
		fmtStr := routePath
		var args []string
		for _, p := range pathParams {
			fmtStr = strings.Replace(fmtStr, ":"+p, "{}", 1)
			args = append(args, emitter.ToSnakeCase(p))
		}
		sb.WriteString(fmt.Sprintf("        let path = format!(%q, %s);\n", fmtStr, strings.Join(args, ", ")))
	} else {
		sb.WriteString(fmt.Sprintf("        let path = %q;\n", routePath))
	}

	bodyArg := "None::<&()>"
	if act.Input != "" {
		bodyArg = "Some(input)"
	}

	sb.WriteString(fmt.Sprintf("        self.request(%q, &path, %s)\n    }\n", method, bodyArg))
}

// emitRustSdkConstants writes constants from the consumed service into the SDK.
func emitRustSdkConstants(consumed emitter.ConsumedServiceInfo, dir string) error {
	if len(consumed.AST.Constants) == 0 {
		return nil
	}
	var sb strings.Builder
	sb.WriteString("// Code generated by Veld — DO NOT EDIT.\n\n")
	for _, cg := range consumed.AST.Constants {
		if cg.Description != "" {
			sb.WriteString(fmt.Sprintf("/// %s\n", cg.Description))
		}
		for _, f := range cg.Fields {
			rType := rustConstType(f.Type)
			rVal := rustConstValue(f)
			sb.WriteString(fmt.Sprintf("pub const %s: %s = %s;\n", f.Name, rType, rVal))
		}
		sb.WriteString("\n")
	}
	return os.WriteFile(filepath.Join(dir, "constants.rs"), []byte(sb.String()), 0644)
}

func emitRustSdkMod(consumed emitter.ConsumedServiceInfo, dir string) error {
	var sb strings.Builder
	sb.WriteString("// Code generated by Veld — DO NOT EDIT.\npub mod models;\npub mod client;\n")
	if len(consumed.AST.Constants) > 0 {
		sb.WriteString("pub mod constants;\n")
	}
	sb.WriteString("\npub use client::Client;\npub use client::VeldApiError;\npub use models::*;\n")
	if len(consumed.AST.Constants) > 0 {
		sb.WriteString("pub use constants::*;\n")
	}
	return os.WriteFile(filepath.Join(dir, "mod.rs"), []byte(sb.String()), 0644)
}

func emitRustSdkBarrel(modNames []string, sdkDir string) error {
	var sb strings.Builder
	sb.WriteString("// Code generated by Veld â€” DO NOT EDIT.\n")
	for _, n := range modNames {
		sb.WriteString(fmt.Sprintf("pub mod %s;\n", n))
	}
	return os.WriteFile(filepath.Join(sdkDir, "mod.rs"), []byte(sb.String()), 0644)
}

// rustSharedAdapter is the single source of truth for Rust type mapping.
var rustSharedAdapter = &lang.RustAdapter{}

// rustFieldType maps a Veld AST field to its Rust type string.
// Shared between normal backend and SDK â€” no duplication.
func rustFieldType(f ast.Field) string {
	if f.IsMap {
		valType, _, _ := rustSharedAdapter.MapType(f.MapValueType)
		return fmt.Sprintf("std::collections::HashMap<String, %s>", valType)
	}
	base, _, _ := rustSharedAdapter.MapType(f.Type)
	if f.IsArray {
		return fmt.Sprintf("Vec<%s>", base)
	}
	return base
}

func rustCapitalize(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}
