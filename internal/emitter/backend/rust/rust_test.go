func TestRustEmitterModelsContent(t *testing.T) {
	e := rustbackend.New()
	outDir := t.TempDir()

	if err := e.Emit(minimalAST(), outDir, emitter.EmitOptions{BackendFramework: "axum"}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, _ := os.ReadFile(filepath.Join(outDir, "src", "models.rs"))
	content := string(data)

	checks := []struct{ desc, needle string }{
		{"header", "AUTO-GENERATED BY VELD"},
		{"serde import", "use serde::{Deserialize, Serialize};"},
		{"User struct", "pub struct User {"},
		{"derive macro", "#[derive(Debug, Clone, Serialize, Deserialize)]"},
		{"optional field", "Option<String>"},
		{"vec field", "Vec<String>"},
		{"Role enum", "pub enum Role {"},
		{"enum variant Admin", "Admin,"},
	}
	for _, c := range checks {
		if !strings.Contains(content, c.needle) {
			t.Errorf("models.rs missing %s: want %q", c.desc, c.needle)
		}
	}
}

