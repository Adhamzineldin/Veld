func TestRustEmitterSummary(t *testing.T) {
	e := rustbackend.New()
	lines := e.Summary([]string{"Auth", "Users"})
	if len(lines) == 0 {
		t.Fatal("Summary() returned no lines")
	}

	combined := ""
	for _, l := range lines {
		combined += l.Dir + l.Files
	}
	if !strings.Contains(combined, "src/") {
		t.Error("Summary() should mention src/ directory")
	}
	if !strings.Contains(combined, "Cargo.toml") {
		t.Error("Summary() should mention Cargo.toml")
	}
}

