// types.go - Type and model generation for Go backend
package gobackend

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter/codegen"
	"github.com/Adhamzineldin/Veld/internal/emitter/lang"
)

const header = "// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n"

// generateTypes writes internal/models/types.go with all enums and model structs.
func (e *GoEmitter) generateTypes(a ast.AST, outDir string) error {
	needsTime := e.astNeedsTimeImport(a)

	w := codegen.NewWriter("\t")
	w.Writeln(header)
	w.Writeln("package models")
	w.BlankLine()

	// Imports — only include what is actually used.
	im := codegen.NewImportManager()
	if needsTime {
		im.Add("time", codegen.GroupStdlib)
	}
	if imports := im.Format("go"); imports != "" {
		w.Write(imports)
		w.BlankLine()
	}

	for _, enum := range a.Enums {
		e.writeEnum(w, enum)
	}

	for _, model := range a.Models {
		if err := e.writeModel(w, model); err != nil {
			return err
		}
		w.BlankLine()
	}

	dir := filepath.Join(outDir, "internal", "models")
	return os.WriteFile(filepath.Join(dir, "types.go"), w.Bytes(), 0644)
}

// writeEnum generates a Go type alias + const block for a Veld enum.
// e.g.:  type Role string  \n  const (RoleAdmin Role = "admin" ...)
func (e *GoEmitter) writeEnum(w *codegen.Writer, enum ast.Enum) {
	w.Writeln(fmt.Sprintf("// %s is an enumeration of allowed values.", enum.Name))
	w.Writeln(fmt.Sprintf("type %s string", enum.Name))
	w.BlankLine()

	w.WriteBlock("const (")
	for _, val := range enum.Values {
		constName := e.adapter.NamingConvention(enum.Name+"_"+val, lang.NamingContextExported)
		w.Writeln(fmt.Sprintf("%s %s = %q", constName, enum.Name, val))
	}
	w.Dedent()
	w.Writeln(")")
	w.BlankLine()
}

// writeModel generates a Go struct for a Veld model.
func (e *GoEmitter) writeModel(w *codegen.Writer, model ast.Model) error {
	w.Writeln(fmt.Sprintf("// %s is a generated data model.", model.Name))

	if model.Extends != "" {
		// Go doesn't have true struct inheritance — embed the parent.
		w.WriteBlock(fmt.Sprintf("type %s struct {", model.Name))
		w.Writeln(model.Extends) // embedded struct
	} else {
		w.WriteBlock(fmt.Sprintf("type %s struct {", model.Name))
	}

	for _, field := range model.Fields {
		goType, err := e.mapFieldType(field)
		if err != nil {
			return fmt.Errorf("model %s field %s: %w", model.Name, field.Name, err)
		}

		fieldName := e.adapter.NamingConvention(field.Name, lang.NamingContextExported)
		jsonPart := field.Name
		if field.Optional {
			jsonPart += ",omitempty"
		}
		tag := fmt.Sprintf("`json:\"%s\"`", jsonPart)

		w.Writeln(fmt.Sprintf("%s %s %s", fieldName, goType, tag))
	}

	w.Dedent()
	w.Writeln("}")
	return nil
}

// mapFieldType converts an AST Field to the appropriate Go type string,
// accounting for arrays, maps, and optional (pointer) fields.
func (e *GoEmitter) mapFieldType(f ast.Field) (string, error) {
	if f.IsMap {
		valGoType, _, err := e.adapter.MapType(f.MapValueType)
		if err != nil {
			return "", err
		}
		goType := "map[string]" + valGoType
		if f.Optional {
			return "*" + goType, nil
		}
		return goType, nil
	}

	baseGoType, _, err := e.adapter.MapType(f.Type)
	if err != nil {
		return "", err
	}

	if f.IsArray {
		// Slice types are already reference types in Go; no pointer needed.
		return "[]" + baseGoType, nil
	}

	if f.Optional {
		return "*" + baseGoType, nil
	}

	return baseGoType, nil
}

// astNeedsTimeImport returns true if any model field uses time.Time (date/datetime).
func (e *GoEmitter) astNeedsTimeImport(a ast.AST) bool {
	timeTypes := map[string]bool{"date": true, "datetime": true, "time": true}
	for _, model := range a.Models {
		for _, field := range model.Fields {
			if timeTypes[strings.ToLower(field.Type)] {
				return true
			}
		}
	}
	return false
}
