func TestGoEmitterRoutesContent(t *testing.T) {
	e := gobackend.New()
	outDir := t.TempDir()

	a := minimalAST()
	if err := e.Emit(a, outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(outDir, "internal", "routes", "auth.go"))
	if err != nil {
		t.Fatalf("ReadFile routes/auth.go: %v", err)
	}
	content := string(data)

	checks := []struct {
		desc   string
		needle string
	}{
		{"package", "package routes"},
		{"setup function", "func setupAuthRoutes(r chi.Router"},
		{"POST Login route", `r.Post("/auth/login", loginHandler`},
		{"GET Me route", `r.Get("/auth/me", meHandler`},
		{"DELETE Logout route", `r.Delete("/auth/logout", logoutHandler`},
		{"handler signature", "func loginHandler(svc interfaces.AuthService) http.HandlerFunc"},
		{"JSON body decode", "readJSON(r, &req)"},
		{"POST status 201", "http.StatusCreated"},
		{"DELETE no-content", "http.StatusNoContent"},
		{"error handling", "err.Error()"},
	}
	for _, c := range checks {
		if !strings.Contains(content, c.needle) {
			t.Errorf("routes/auth.go missing %s: expected to find %q", c.desc, c.needle)
		}
	}

	// There should be NO duplicate ":=" for the service call.
	if strings.Count(content, "resp, err :=") > strings.Count(content, "func") {
		t.Error("routes/auth.go has suspicious duplicate service calls")
	}
}

func TestGoEmitterPathParams(t *testing.T) {
	e := gobackend.New()
	outDir := t.TempDir()

	a := ast.AST{
		Models: []ast.Model{
			{Name: "Item", Fields: []ast.Field{{Name: "id", Type: "string"}}},
		},
		Modules: []ast.Module{
			{
				Name: "Items",
				Actions: []ast.Action{
					{Name: "GetById", Method: "GET", Path: "/items/:id", Output: "Item"},
					{Name: "Delete", Method: "DELETE", Path: "/items/:id"},
				},
			},
		},
	}

	if err := e.Emit(a, outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, _ := os.ReadFile(filepath.Join(outDir, "internal", "routes", "items.go"))
	content := string(data)

	// Chi uses {id} not :id.
	if strings.Contains(content, `"/items/:id"`) {
		t.Error("routes should use Chi syntax {id}, not Express :id")
	}
	if !strings.Contains(content, `"/items/{id}"`) {
		t.Error("routes should contain Chi-style /items/{id}")
	}
	if !strings.Contains(content, `chi.URLParam(r, "id")`) {
		t.Error("handler should extract path param using chi.URLParam")
	}
}

