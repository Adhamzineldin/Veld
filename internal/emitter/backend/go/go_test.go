func TestGoEmitterSummary(t *testing.T) {
	e := gobackend.New()
	modules := []string{"Auth", "Users"}
	lines := e.Summary(modules)

	if len(lines) == 0 {
		t.Fatal("Summary() returned no lines")
	}

	// Check that all important directories are mentioned.
	dirs := make(map[string]bool)
	for _, l := range lines {
		dirs[l.Dir] = true
	}
	for _, want := range []string{"internal/models/", "internal/interfaces/", "internal/routes/", "internal/middleware/", "./"} {
		if !dirs[want] {
			t.Errorf("Summary() missing entry for directory %q", want)
		}
	}
}

