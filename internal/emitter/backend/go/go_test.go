func TestGoEmitterTypesContent(t *testing.T) {
	e := gobackend.New()
	outDir := t.TempDir()

	a := minimalAST()
	if err := e.Emit(a, outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(outDir, "internal", "models", "types.go"))
	if err != nil {
		t.Fatalf("ReadFile types.go: %v", err)
	}
	content := string(data)

	checks := []struct {
		desc   string
		needle string
	}{
		{"header", "AUTO-GENERATED BY VELD"},
		{"package", "package models"},
		{"User struct", "type User struct {"},
		{"optional pointer field", "*string"},
		{"slice field", "[]string"},
		{"Role enum type", "type Role string"},
		{"enum const block", "const ("},
		{"enum admin value", `RoleAdmin Role = "admin"`},
		{"uuid maps to string", "Id string"},
	}
	for _, c := range checks {
		if !strings.Contains(content, c.needle) {
			t.Errorf("types.go missing %s: expected to find %q", c.desc, c.needle)
		}
	}

	// time import only when needed — no time types in minimalAST, so should be absent.
	if strings.Contains(content, `"time"`) {
		t.Error("types.go should not import 'time' when no date/datetime fields exist")
	}
}

