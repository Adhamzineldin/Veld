// minimalAST returns a small but representative AST for testing.
func minimalAST() ast.AST {
	return ast.AST{
		Models: []ast.Model{
			{
				Name: "LoginInput",
				Fields: []ast.Field{
					{Name: "email", Type: "string"},
					{Name: "password", Type: "string"},
				},
			},
			{
				Name: "User",
				Fields: []ast.Field{
					{Name: "id", Type: "uuid"},
					{Name: "email", Type: "string"},
					{Name: "name", Type: "string"},
					{Name: "bio", Type: "string", Optional: true},
					{Name: "tags", Type: "string", IsArray: true},
				},
			},
		},
		Enums: []ast.Enum{
			{Name: "Role", Values: []string{"admin", "user", "guest"}},
		},
		Modules: []ast.Module{
			{
				Name:   "Auth",
				Prefix: "",
				Actions: []ast.Action{
					{
						Name:   "Login",
						Method: "POST",
						Path:   "/auth/login",
						Input:  "LoginInput",
						Output: "User",
					},
					{
						Name:   "Me",
						Method: "GET",
						Path:   "/auth/me",
						Output: "User",
					},
					{
						Name:   "Logout",
						Method: "DELETE",
						Path:   "/auth/logout",
					},
				},
			},
		},
	}
}

func TestGoEmitterEmit(t *testing.T) {
	e := gobackend.New()
	outDir := t.TempDir()

	a := minimalAST()
	opts := emitter.EmitOptions{}

	if err := e.Emit(a, outDir, opts); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	// Verify key files were created.
	expectedFiles := []string{
		filepath.Join(outDir, "go.mod"),
		filepath.Join(outDir, "server.go"),
		filepath.Join(outDir, "main.go"),
		filepath.Join(outDir, "internal", "models", "types.go"),
		filepath.Join(outDir, "internal", "interfaces", "auth.go"),
		filepath.Join(outDir, "internal", "routes", "routes.go"),
		filepath.Join(outDir, "internal", "routes", "auth.go"),
		filepath.Join(outDir, "internal", "middleware", "errors.go"),
	}
	for _, f := range expectedFiles {
		if _, err := os.Stat(f); os.IsNotExist(err) {
			t.Errorf("expected file %s to exist", f)
		}
	}
}

