package gobackend

// constants.go — emits typed constant files into constants/ directory.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

func (e *GoEmitter) emitConstants(a ast.AST, outDir string) error {
	if len(a.Constants) == 0 {
		return nil
	}
	dir := filepath.Join(outDir, "constants")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	for _, cg := range a.Constants {
		fileName := emitter.ToSnakeCase(cg.Name)
		var sb strings.Builder
		sb.WriteString("// Code generated by Veld — DO NOT EDIT.\npackage constants\n\n")

		if cg.Description != "" {
			sb.WriteString(fmt.Sprintf("// %s\n", cg.Description))
		}

		sb.WriteString("const (\n")
		for _, f := range cg.Fields {
			goType := goConstType(f.Type)
			goVal := goConstValue(f)
			sb.WriteString(fmt.Sprintf("\t%s %s = %s\n", f.Name, goType, goVal))
		}
		sb.WriteString(")\n")

		if err := os.WriteFile(filepath.Join(dir, fileName+".go"), []byte(sb.String()), 0644); err != nil {
			return err
		}
	}
	return nil
}

func goConstType(veldType string) string {
	switch veldType {
	case "string", "uuid", "date", "datetime", "decimal":
		return "string"
	case "int":
		return "int"
	case "float":
		return "float64"
	case "bool":
		return "bool"
	default:
		return "string"
	}
}

func goConstValue(f ast.ConstantField) string {
	switch f.Type {
	case "string", "uuid", "date", "datetime", "decimal":
		return fmt.Sprintf("%q", f.Value)
	case "int", "float":
		return f.Value
	case "bool":
		return f.Value
	default:
		return fmt.Sprintf("%q", f.Value)
	}
}
