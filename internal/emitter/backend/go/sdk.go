package gobackend

// sdk.go — service SDK generation for Go backend.
// Generates typed, net/http-based client packages for consumed services.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/sdkhelpers"
)

// EmitServiceSdk generates Go service SDK clients for each consumed service.
func (e *GoEmitter) EmitServiceSdk(consumed []emitter.ConsumedServiceInfo, outDir string, opts emitter.EmitOptions) error {
	if opts.DryRun || len(consumed) == 0 {
		return nil
	}
	for _, c := range consumed {
		if err := emitGoSdk(c, outDir); err != nil {
			return fmt.Errorf("service sdk %s: %w", c.Name, err)
		}
	}
	return nil
}

func emitGoSdk(consumed emitter.ConsumedServiceInfo, outDir string) error {
	name := sdkhelpers.ServiceFileName(consumed.Name)
	dir := filepath.Join(outDir, "sdk", name)
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}
	if err := emitGoSdkDoc(consumed, dir, name); err != nil {
		return err
	}
	if err := emitGoSdkTypes(consumed, dir, name); err != nil {
		return err
	}
	return emitGoSdkClient(consumed, dir, name)
}

func emitGoSdkDoc(consumed emitter.ConsumedServiceInfo, dir, pkgName string) error {
	content := fmt.Sprintf("// Code generated by Veld — DO NOT EDIT.\n// Service SDK: typed HTTP client for the %s service.\npackage %s\n", consumed.Name, pkgName)
	return os.WriteFile(filepath.Join(dir, "doc.go"), []byte(content), 0644)
}

func emitGoSdkTypes(consumed emitter.ConsumedServiceInfo, dir, pkgName string) error {
	var sb strings.Builder
	a := consumed.AST
	sb.WriteString("// Code generated by Veld — DO NOT EDIT.\n")
	sb.WriteString(fmt.Sprintf("package %s\n\n", pkgName))

	usedModels := make(map[string]bool)
	usedEnums := make(map[string]bool)
	for _, mod := range a.Modules {
		for k, v := range emitter.CollectTransitiveModels(a, mod) {
			if v {
				usedModels[k] = true
			}
		}
		for k, v := range emitter.CollectUsedEnums(a, mod) {
			if v {
				usedEnums[k] = true
			}
		}
	}

	needsTime := false
	for _, m := range a.Models {
		if !usedModels[m.Name] {
			continue
		}
		for _, f := range m.Fields {
			if f.Type == "date" || f.Type == "datetime" {
				needsTime = true
			}
		}
	}
	if needsTime {
		sb.WriteString("import \"time\"\n\n")
	}

	for _, en := range a.Enums {
		if !usedEnums[en.Name] {
			continue
		}
		sb.WriteString(fmt.Sprintf("type %s string\n\nconst (\n", en.Name))
		for _, v := range en.Values {
			sb.WriteString(fmt.Sprintf("\t%s%s %s = %q\n", en.Name, goCapitalize(v), en.Name, v))
		}
		sb.WriteString(")\n\n")
	}

	modelMap := make(map[string]ast.Model, len(a.Models))
	for _, m := range a.Models {
		modelMap[m.Name] = m
	}

	for _, m := range a.Models {
		if !usedModels[m.Name] {
			continue
		}
		sb.WriteString(fmt.Sprintf("type %s struct {\n", m.Name))
		allFields := goSdkFlattenFields(m, modelMap)
		for _, f := range allFields {
			goType := goSdkFieldType(f)
			if f.Optional {
				goType = "*" + goType
			}
			jsonTag := f.Name
			if f.Optional {
				jsonTag += ",omitempty"
			}
			sb.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", goCapitalize(f.Name), goType, jsonTag))
		}
		sb.WriteString("}\n\n")
	}

	return os.WriteFile(filepath.Join(dir, "types.go"), []byte(sb.String()), 0644)
}

func emitGoSdkClient(consumed emitter.ConsumedServiceInfo, dir, pkgName string) error {
	var sb strings.Builder
	a := consumed.AST
	envVar := sdkhelpers.EnvVarName(consumed.Name)

	sb.WriteString("// Code generated by Veld — DO NOT EDIT.\n")
	sb.WriteString(fmt.Sprintf("package %s\n\n", pkgName))
	sb.WriteString("import (\n\t\"bytes\"\n\t\"context\"\n\t\"encoding/json\"\n\t\"fmt\"\n\t\"io\"\n\t\"net/http\"\n\t\"net/url\"\n\t\"os\"\n)\n\n")

	sb.WriteString("type VeldApiError struct {\n\tStatus int\n\tBody   string\n}\n\n")
	sb.WriteString("func (e *VeldApiError) Error() string { return fmt.Sprintf(\"HTTP %d: %s\", e.Status, e.Body) }\n\n")

	sb.WriteString("type Option func(*Client)\n\n")
	sb.WriteString("func WithHTTPClient(c *http.Client) Option { return func(cl *Client) { cl.client = c } }\n")
	sb.WriteString("func WithHeaders(h map[string]string) Option { return func(cl *Client) { cl.hdrs = h } }\n\n")

	sb.WriteString("type Client struct {\n\tbase   string\n\tclient *http.Client\n\thdrs   map[string]string\n}\n\n")

	sb.WriteString("func NewClient(baseURL string, opts ...Option) *Client {\n")
	sb.WriteString(fmt.Sprintf("\tif baseURL == \"\" { baseURL = os.Getenv(%q) }\n", envVar))
	if consumed.BaseUrl != "" {
		sb.WriteString(fmt.Sprintf("\tif baseURL == \"\" { baseURL = %q }\n", consumed.BaseUrl))
	}
	sb.WriteString("\tc := &Client{base: baseURL, client: http.DefaultClient, hdrs: map[string]string{}}\n")
	sb.WriteString("\tfor _, o := range opts { o(c) }\n\treturn c\n}\n\n")

	sb.WriteString("func (c *Client) do(ctx context.Context, method, path string, body any) ([]byte, int, error) {\n")
	sb.WriteString("\tvar reader io.Reader\n\tif body != nil {\n\t\tb, err := json.Marshal(body)\n\t\tif err != nil { return nil, 0, err }\n\t\treader = bytes.NewReader(b)\n\t}\n")
	sb.WriteString("\treq, err := http.NewRequestWithContext(ctx, method, c.base+path, reader)\n\tif err != nil { return nil, 0, err }\n")
	sb.WriteString("\treq.Header.Set(\"Content-Type\", \"application/json\")\n\tfor k, v := range c.hdrs { req.Header.Set(k, v) }\n")
	sb.WriteString("\tresp, err := c.client.Do(req)\n\tif err != nil { return nil, 0, err }\n\tdefer resp.Body.Close()\n")
	sb.WriteString("\tdata, _ := io.ReadAll(resp.Body)\n\tif resp.StatusCode >= 400 { return nil, resp.StatusCode, &VeldApiError{Status: resp.StatusCode, Body: string(data)} }\n")
	sb.WriteString("\treturn data, resp.StatusCode, nil\n}\n")

	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			if strings.ToUpper(act.Method) == "WS" {
				continue
			}
			sb.WriteString("\n")
			writeGoSdkMethod(&sb, mod, act)
		}
	}

	sb.WriteString("\nvar _ = url.Values{}\n")
	return os.WriteFile(filepath.Join(dir, "client.go"), []byte(sb.String()), 0644)
}

func writeGoSdkMethod(sb *strings.Builder, mod ast.Module, act ast.Action) {
	routePath := mod.Prefix + act.Path
	method := strings.ToUpper(act.Method)
	funcName := goCapitalize(act.Name)
	pathParams := emitter.ExtractPathParams(routePath)

	var params []string
	params = append(params, "ctx context.Context")
	for _, p := range pathParams {
		params = append(params, fmt.Sprintf("%s string", p))
	}
	if act.Input != "" {
		params = append(params, fmt.Sprintf("input %s", act.Input))
	}

	hasOutput := act.Output != ""
	if hasOutput {
		retType := "*" + act.Output
		if act.OutputArray {
			retType = "[]" + act.Output
		}
		sb.WriteString(fmt.Sprintf("func (c *Client) %s(%s) (%s, error) {\n", funcName, strings.Join(params, ", "), retType))
	} else {
		sb.WriteString(fmt.Sprintf("func (c *Client) %s(%s) error {\n", funcName, strings.Join(params, ", ")))
	}

	urlExpr := goSdkBuildUrl(routePath, pathParams)
	bodyArg := "nil"
	if act.Input != "" {
		bodyArg = "input"
	}

	if hasOutput {
		sb.WriteString(fmt.Sprintf("\tdata, _, err := c.do(ctx, %q, %s, %s)\n\tif err != nil { return nil, err }\n", method, urlExpr, bodyArg))
		if act.OutputArray {
			sb.WriteString(fmt.Sprintf("\tvar out []%s\n", act.Output))
		} else {
			sb.WriteString(fmt.Sprintf("\tvar out %s\n", act.Output))
		}
		sb.WriteString("\tif err := json.Unmarshal(data, &out); err != nil { return nil, err }\n")
		if act.OutputArray {
			sb.WriteString("\treturn out, nil\n")
		} else {
			sb.WriteString("\treturn &out, nil\n")
		}
	} else {
		sb.WriteString(fmt.Sprintf("\t_, _, err := c.do(ctx, %q, %s, %s)\n\treturn err\n", method, urlExpr, bodyArg))
	}
	sb.WriteString("}\n")
}

// ── helpers ───────────────────────────────────────────────────────────────────

func goSdkFieldType(f ast.Field) string {
	if f.IsMap {
		return fmt.Sprintf("map[string]%s", goSdkScalar(f.MapValueType))
	}
	base := goSdkScalar(f.Type)
	if f.IsArray {
		return "[]" + base
	}
	return base
}

func goSdkScalar(t string) string {
	switch t {
	case "string", "uuid", "decimal":
		return "string"
	case "int":
		return "int64"
	case "float":
		return "float64"
	case "bool":
		return "bool"
	case "date", "datetime":
		return "time.Time"
	case "any", "json":
		return "interface{}"
	default:
		return t
	}
}

func goCapitalize(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}

func goSdkBuildUrl(routePath string, pathParams []string) string {
	if len(pathParams) == 0 {
		return fmt.Sprintf("%q", routePath)
	}
	fmtStr := routePath
	var args []string
	for _, p := range pathParams {
		fmtStr = strings.Replace(fmtStr, ":"+p, "%s", 1)
		args = append(args, p)
	}
	return fmt.Sprintf("fmt.Sprintf(%q, %s)", fmtStr, strings.Join(args, ", "))
}

func goSdkFlattenFields(m ast.Model, modelMap map[string]ast.Model) []ast.Field {
	var fields []ast.Field
	if m.Extends != "" {
		if parent, ok := modelMap[m.Extends]; ok {
			fields = append(fields, goSdkFlattenFields(parent, modelMap)...)
		}
	}
	return append(fields, m.Fields...)
}
