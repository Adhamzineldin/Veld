package csharp

// middleware.go — emits per-module middleware interfaces for ASP.NET Core.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitModuleMiddleware writes Services/I{Module}Middleware.cs with a typed interface.
func (e *CSharpEmitter) emitModuleMiddleware(mod ast.Module, outDir string) error {
	allMiddleware := emitter.CollectModuleMiddleware(mod)
	if len(allMiddleware) == 0 {
		return nil
	}

	dir := filepath.Join(outDir, "Middleware")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	interfaceName := "I" + mod.Name + "Middleware"
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString("using Microsoft.AspNetCore.Http;\n\n")
	sb.WriteString("namespace VeldGenerated.Middleware;\n\n")
	sb.WriteString(fmt.Sprintf("/// <summary>\n/// Middleware interface for the %s module.\n", mod.Name))
	sb.WriteString("/// Implement this interface and register it with dependency injection.\n/// </summary>\n")
	sb.WriteString(fmt.Sprintf("public interface %s\n{\n", interfaceName))

	for _, mw := range allMiddleware {
		// Capitalize first letter for C# naming convention
		methodName := strings.ToUpper(mw[:1]) + mw[1:]
		sb.WriteString(fmt.Sprintf("    /// <summary>%s middleware — return true to proceed, false to reject.</summary>\n", mw))
		sb.WriteString(fmt.Sprintf("    Task<bool> %sAsync(HttpContext context);\n\n", methodName))
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, interfaceName+".cs"), []byte(sb.String()), 0644)
}
