func TestCSharpEmitterModelContent(t *testing.T) {
	e := csharp.New()
	outDir := t.TempDir()
	if err := e.Emit(minimalAST(), outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(outDir, "Models", "User.cs"))
	if err != nil {
		t.Fatalf("ReadFile User.cs: %v", err)
	}
	content := string(data)

	checks := []struct{ desc, needle string }{
		{"header", "AUTO-GENERATED BY VELD"},
		{"namespace", "namespace VeldGenerated.Models;"},
		{"list using", "using System.Collections.Generic;"},
		{"record declaration", "public record User("},
		{"string field", "string Email"},
		{"list field", "List<string> Tags"},
		{"nullable optional", "string? Bio"},
	}
	for _, c := range checks {
		if !strings.Contains(content, c.needle) {
			t.Errorf("User.cs missing %s: want %q\ngot:\n%s", c.desc, c.needle, content)
		}
	}
}

