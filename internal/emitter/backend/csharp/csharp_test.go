func TestCSharpEmitterPathParams(t *testing.T) {
	e := csharp.New()
	outDir := t.TempDir()

	a := ast.AST{
		Models: []ast.Model{
			{Name: "Item", Fields: []ast.Field{{Name: "id", Type: "string"}}},
		},
		Modules: []ast.Module{
			{
				Name: "Items",
				Actions: []ast.Action{
					{Name: "GetById", Method: "GET", Path: "/items/:id", Output: "Item"},
				},
			},
		},
	}

	if err := e.Emit(a, outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, _ := os.ReadFile(filepath.Join(outDir, "Controllers", "ItemsController.cs"))
	content := string(data)

	// ASP.NET Core uses {id} syntax.
	if strings.Contains(content, `"/items/:id"`) {
		t.Error("controller should use {id} not :id")
	}
	if !strings.Contains(content, `"/items/{id}"`) {
		t.Errorf("controller should use {id} ASP.NET Core syntax:\n%s", content)
	}
	if !strings.Contains(content, "[FromRoute] string Id") {
		t.Errorf("controller should have [FromRoute] string Id param:\n%s", content)
	}
}

func TestCSharpEmitterSummary(t *testing.T) {
	e := csharp.New()
	lines := e.Summary([]string{"Auth", "Users"})

	if len(lines) == 0 {
		t.Fatal("Summary() returned no lines")
	}
	dirs := make(map[string]bool)
	for _, l := range lines {
		dirs[l.Dir] = true
	}
	for _, want := range []string{"Models/", "Services/", "Controllers/", "./"} {
		if !dirs[want] {
			t.Errorf("Summary() missing dir %q", want)
		}
	}
}

