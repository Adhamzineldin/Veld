package csharp

// routes.go — emits Controllers/{Module}Controller.cs with ASP.NET Core
// async action methods. Status codes: 201 for POST, 204 for DELETE with no
// output, 200 for everything else. Errors return 500 ProblemDetails.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

func (e *CSharpEmitter) emitController(a ast.AST, mod ast.Module, outDir string) error {
	dir := filepath.Join(outDir, "Controllers")

	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	modClass := capitalize(mod.Name)

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString("using Microsoft.AspNetCore.Mvc;\n")
	sb.WriteString("using System.Collections.Generic;\n")
	sb.WriteString(fmt.Sprintf("using %s.Models;\n", csNamespace))
	sb.WriteString(fmt.Sprintf("using %s.Services;\n", csNamespace))
	sb.WriteString(fmt.Sprintf("\nnamespace %s.Controllers;\n\n", csNamespace))

	if mod.Description != "" {
		sb.WriteString(fmt.Sprintf("/// <summary>%s</summary>\n", mod.Description))
	}
	sb.WriteString("[ApiController]\n")
	if mod.Prefix != "" {
		sb.WriteString(fmt.Sprintf("[Route(\"%s\")]\n", strings.TrimPrefix(mod.Prefix, "/")))
	}
	sb.WriteString(fmt.Sprintf("public class %sController : ControllerBase\n{\n", modClass))
	sb.WriteString(fmt.Sprintf("    private readonly I%sService _service;\n\n", modClass))
	sb.WriteString(fmt.Sprintf("    public %sController(I%sService service)\n    {\n", modClass, modClass))
	sb.WriteString("        _service = service;\n    }\n")

	for _, act := range mod.Actions {
		writeCSharpAction(&sb, mod, act, enumNames)
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, modClass+"Controller.cs"), []byte(sb.String()), 0644)
}

// writeCSharpAction appends a single async action method to sb.
func writeCSharpAction(sb *strings.Builder, mod ast.Module, act ast.Action, enumNames map[string]bool) {
	routePath := act.Path
	pathParams := emitter.ExtractPathParams(routePath)
	returnType := csReturnType(act, enumNames)

	httpAttr := csHttpAttribute(act.Method, routePath)

	sb.WriteString("\n")
	if act.Description != "" {
		sb.WriteString(fmt.Sprintf("    /// <summary>%s</summary>\n", act.Description))
	}
	sb.WriteString(fmt.Sprintf("    %s\n", httpAttr))

	// Method signature — async Task<IActionResult>.
	methodName := csPascalName(act.Name)
	var params []string
	for _, p := range pathParams {
		params = append(params, fmt.Sprintf("[FromRoute] string %s", csPascalName(p)))
	}
	if act.Input != "" {
		params = append(params, fmt.Sprintf("[FromBody] %s input", act.Input))
	}
	if act.Query != "" {
		params = append(params, "[FromQuery] Dictionary<string, string> query")
	}

	sb.WriteString(fmt.Sprintf("    public async Task<IActionResult> %s(%s)\n    {\n", methodName, strings.Join(params, ", ")))
	sb.WriteString("        try\n        {\n")

	// Build service call args.
	var callArgs []string
	for _, p := range pathParams {
		callArgs = append(callArgs, csPascalName(p))
	}
	if act.Input != "" {
		callArgs = append(callArgs, "input")
	}
	if act.Query != "" {
		callArgs = append(callArgs, "query")
	}

	serviceCall := fmt.Sprintf("_service.%s(%s)", methodName, strings.Join(callArgs, ", "))

	switch {
	case act.Method == "DELETE" && returnType == "":
		sb.WriteString(fmt.Sprintf("            await %s;\n", serviceCall))
		sb.WriteString("            return NoContent();\n")
	case act.Method == "POST":
		sb.WriteString(fmt.Sprintf("            var result = await %s;\n", serviceCall))
		sb.WriteString("            return StatusCode(201, result);\n")
	default:
		sb.WriteString(fmt.Sprintf("            var result = await %s;\n", serviceCall))
		sb.WriteString("            return Ok(result);\n")
	}

	sb.WriteString("        }\n        catch (Exception e)\n        {\n")
	sb.WriteString("            return StatusCode(500, new { error = e.Message });\n")
	sb.WriteString("        }\n    }\n")
}

// csHttpAttribute returns the ASP.NET Core HTTP method attribute for a route.
// ASP.NET Core uses {param} route templates (same as Chi), so we reuse ToChiPath.
func csHttpAttribute(method, path string) string {
	aspPath := emitter.ToChiPath(path) // :id → {id}
	route := fmt.Sprintf("(\"%s\")", aspPath)
	switch strings.ToUpper(method) {
	case "GET":
		return "[HttpGet" + route + "]"
	case "POST":
		return "[HttpPost" + route + "]"
	case "PUT":
		return "[HttpPut" + route + "]"
	case "DELETE":
		return "[HttpDelete" + route + "]"
	case "PATCH":
		return "[HttpPatch" + route + "]"
	default:
		return fmt.Sprintf("[HttpGet(\"%s\")] // TODO: unsupported method %s", aspPath, method)
	}
}
