package csharp

// routes.go — emits Controllers/{Module}Controller.cs with async action methods.
// Status codes: 201 for POST, 204 for DELETE with no output, 200 for everything else.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	csstrategy "github.com/Adhamzineldin/Veld/internal/emitter/backend/csharp/strategy"
)

func (e *CSharpEmitter) emitController(a ast.AST, mod ast.Module, outDir string, strat csstrategy.CSharpFrameworkStrategy) error {
	dir := filepath.Join(outDir, "Controllers")

	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	modClass := capitalize(mod.Name)

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")

	// Strategy-supplied using directives.
	for _, u := range strat.ControllerUsings() {
		sb.WriteString(u + "\n")
	}
	sb.WriteString(fmt.Sprintf("using %s.Models;\n", csNamespace))
	sb.WriteString(fmt.Sprintf("using %s.Errors;\n", csNamespace))
	sb.WriteString(fmt.Sprintf("using %s.Services;\n", csNamespace))
	sb.WriteString(fmt.Sprintf("\nnamespace %s.Controllers;\n\n", csNamespace))

	if mod.Description != "" {
		sb.WriteString(fmt.Sprintf("/// <summary>%s</summary>\n", mod.Description))
	}
	for _, ann := range strat.ControllerAnnotations() {
		sb.WriteString(ann + "\n")
	}
	if mod.Prefix != "" {
		sb.WriteString(fmt.Sprintf("[Route(\"%s\")]\n", strings.TrimPrefix(mod.Prefix, "/")))
	}

	baseClass := strat.ControllerBaseClass()
	if baseClass != "" {
		sb.WriteString(fmt.Sprintf("public class %sController : %s\n{\n", modClass, baseClass))
	} else {
		sb.WriteString(fmt.Sprintf("public class %sController\n{\n", modClass))
	}
	sb.WriteString(fmt.Sprintf("    private readonly I%sService _service;\n\n", modClass))
	sb.WriteString(fmt.Sprintf("    public %sController(I%sService service)\n    {\n", modClass, modClass))
	sb.WriteString("        _service = service;\n    }\n")

	for _, act := range mod.Actions {
		if act.Method == "WS" {
			routePath := act.Path
			if mod.Prefix != "" {
				routePath = mod.Prefix + act.Path
			}
			emitType := act.Emit
			if emitType != "" {
				emitType = veldScalarToCS(emitType, enumNames)
			}
			sb.WriteString(strat.WSActionMethod(csPascalName(act.Name), routePath, emitType, act.Stream))
		} else {
			writeCSharpAction(&sb, mod, act, enumNames, strat)
		}
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, modClass+"Controller.cs"), []byte(sb.String()), 0644)
}

// writeCSharpAction appends a single async action method to sb.
func writeCSharpAction(sb *strings.Builder, mod ast.Module, act ast.Action, enumNames map[string]bool, strat csstrategy.CSharpFrameworkStrategy) {
	routePath := act.Path
	pathParams := emitter.ExtractPathParams(routePath)
	returnType := csReturnType(act, enumNames)

	routeAttr := strat.RouteAnnotation(act.Method, routePath, mod.Prefix)
	responseWrapper := strat.ResponseWrapper()

	sb.WriteString("\n")
	if act.Description != "" {
		sb.WriteString(fmt.Sprintf("    /// <summary>%s</summary>\n", act.Description))
	}
	sb.WriteString(fmt.Sprintf("    %s\n", routeAttr))

	methodName := csPascalName(act.Name)
	var params []string
	for _, p := range pathParams {
		ann := strat.PathParamAnnotation(p)
		params = append(params, fmt.Sprintf("%sstring %s", ann, csPascalName(p)))
	}
	if act.Input != "" {
		ann := strat.InputParamAnnotation()
		params = append(params, fmt.Sprintf("%s%s input", ann, act.Input))
	}
	if act.Query != "" {
		params = append(params, "[FromQuery] Dictionary<string, string> query")
	}

	sb.WriteString(fmt.Sprintf("    public async Task<%s> %s(%s)\n    {\n", responseWrapper, methodName, strings.Join(params, ", ")))
	sb.WriteString("        try\n        {\n")

	var callArgs []string
	for _, p := range pathParams {
		callArgs = append(callArgs, csPascalName(p))
	}
	if act.Input != "" {
		callArgs = append(callArgs, "input")
	}
	if act.Query != "" {
		callArgs = append(callArgs, "query")
	}

	serviceCall := fmt.Sprintf("_service.%s(%s)", methodName, strings.Join(callArgs, ", "))

	switch {
	case act.Method == "DELETE" && returnType == "":
		sb.WriteString(fmt.Sprintf("            await %s;\n", serviceCall))
		sb.WriteString(fmt.Sprintf("            %s\n", strat.NoContentResponse()))
	case act.Method == "POST":
		sb.WriteString(fmt.Sprintf("            var result = await %s;\n", serviceCall))
		sb.WriteString(fmt.Sprintf("            %s\n", strat.CreatedResponse("result")))
	default:
		sb.WriteString(fmt.Sprintf("            var result = await %s;\n", serviceCall))
		sb.WriteString(fmt.Sprintf("            %s\n", strat.OkResponse("result")))
	}

	sb.WriteString("        }\n        catch (Exception e)\n        {\n")
	sb.WriteString(fmt.Sprintf("            %s\n", strat.ErrorResponse("e")))
	sb.WriteString("        }\n    }\n")
}
