package csharp

// interfaces.go — emits Services/I{Module}Service.cs with Task<T> async method signatures.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

func (e *CSharpEmitter) emitInterface(a ast.AST, mod ast.Module, outDir string) error {
	dir := filepath.Join(outDir, "Services")

	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString("using System.Collections.Generic;\n")
	sb.WriteString(fmt.Sprintf("using %s.Models;\n", csNamespace))
	sb.WriteString(fmt.Sprintf("using %s.Errors;\n", csNamespace))
	sb.WriteString(fmt.Sprintf("\nnamespace %s.Services;\n\n", csNamespace))

	modClass := capitalize(mod.Name)

	if mod.Description != "" {
		sb.WriteString(fmt.Sprintf("/// <summary>%s</summary>\n", mod.Description))
	}
	sb.WriteString(fmt.Sprintf("public interface I%sService\n{\n", modClass))

	for _, act := range mod.Actions {
		routePath := act.Path
		if mod.Prefix != "" {
			routePath = mod.Prefix + act.Path
		}
		pathParams := emitter.ExtractPathParams(routePath)

		if act.Method == "WS" {
			actionName := csPascalName(act.Name)

			// OnConnect
			var connectParams []string
			connectParams = append(connectParams, "System.Net.WebSockets.WebSocket socket")
			for _, p := range pathParams {
				connectParams = append(connectParams, "string "+csPascalName(p))
			}
			if act.Description != "" {
				sb.WriteString(fmt.Sprintf("    /// <summary>Called when a client opens the WS connection. %s</summary>\n", act.Description))
			} else {
				sb.WriteString(fmt.Sprintf("    /// <summary>Called when a client opens WS %s.</summary>\n", routePath))
			}
			sb.WriteString(fmt.Sprintf("    Task On%sConnect(%s);\n\n", actionName, strings.Join(connectParams, ", ")))

			// OnMessage — only when emit type is set
			if act.Emit != "" {
				csEmitType := veldScalarToCS(act.Emit, enumNames)
				sb.WriteString(fmt.Sprintf("    /// <summary>Called when a client sends a %s message.</summary>\n", act.Emit))
				sb.WriteString(fmt.Sprintf("    Task On%sMessage(System.Net.WebSockets.WebSocket socket, %s msg);\n\n", actionName, csEmitType))
			}

			// OnClose — always included
			sb.WriteString(fmt.Sprintf("    /// <summary>Called when the WS %s connection is closed.</summary>\n", routePath))
			sb.WriteString(fmt.Sprintf("    Task On%sClose(System.Net.WebSockets.WebSocket socket);\n\n", actionName))
			continue
		}

		returnType := csReturnType(act, enumNames)
		taskType := "Task"
		if returnType != "" {
			taskType = "Task<" + returnType + ">"
		}

		var params []string
		for _, p := range pathParams {
			params = append(params, "string "+csPascalName(p))
		}
		if act.Input != "" {
			params = append(params, act.Input+" input")
		}
		if act.Query != "" {
			params = append(params, "Dictionary<string, string> query")
		}

		if act.Description != "" {
			sb.WriteString(fmt.Sprintf("    /// <summary>%s</summary>\n", act.Description))
		}
		for _, errName := range act.Errors {
			code := emitter.ErrorCode(act.Name, errName)
			sb.WriteString(fmt.Sprintf("    /// <exception cref=\"%sException\">%s — %s</exception>\n", csPascalName(act.Name), code, errName))
		}
		sb.WriteString(fmt.Sprintf("    %s %s(%s);\n\n", taskType, csPascalName(act.Name), strings.Join(params, ", ")))
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, fmt.Sprintf("I%sService.cs", modClass)), []byte(sb.String()), 0644)
}

// csReturnType returns the C# inner return type for a service method (empty = void/Task).
func csReturnType(act ast.Action, enumNames map[string]bool) string {
	if act.Output == "" {
		return ""
	}
	base := veldScalarToCS(act.Output, enumNames)
	if act.OutputArray {
		return "List<" + base + ">"
	}
	return base
}
