package csharp

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

func (e *CSharpEmitter) emitConstants(a ast.AST, outDir string) error {
	if len(a.Constants) == 0 {
		return nil
	}
	dir := filepath.Join(outDir, "Constants")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}
	_ = emitter.ToSnakeCase // keep import

	for _, cg := range a.Constants {
		var sb strings.Builder
		sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")

		// Check if we need System namespace (Guid, DateTime)
		needsSystem := false
		for _, f := range cg.Fields {
			if f.Type == "uuid" || f.Type == "date" || f.Type == "datetime" {
				needsSystem = true
				break
			}
		}
		if needsSystem {
			sb.WriteString("using System;\n\n")
		}

		sb.WriteString("namespace Veld.Generated.Constants;\n\n")
		if cg.Description != "" {
			sb.WriteString(fmt.Sprintf("/// <summary>%s</summary>\n", cg.Description))
		}
		sb.WriteString(fmt.Sprintf("public static class %s\n{\n", cg.Name))
		for _, f := range cg.Fields {
			csType := csConstType(f.Type)
			csVal := csConstValue(f)
			// Guid, DateTime, decimal need 'static readonly' (not compile-time const)
			if f.Type == "decimal" || f.Type == "uuid" || f.Type == "date" || f.Type == "datetime" {
				sb.WriteString(fmt.Sprintf("    public static readonly %s %s = %s;\n", csType, f.Name, csVal))
			} else {
				sb.WriteString(fmt.Sprintf("    public const %s %s = %s;\n", csType, f.Name, csVal))
			}
		}
		sb.WriteString("}\n")
		if err := os.WriteFile(filepath.Join(dir, cg.Name+".cs"), []byte(sb.String()), 0644); err != nil {
			return err
		}
	}
	return nil
}

func csConstType(veldType string) string {
	switch veldType {
	case "string":
		return "string"
	case "uuid":
		return "Guid"
	case "date", "datetime":
		return "DateTime"
	case "int":
		return "long"
	case "float":
		return "double"
	case "decimal":
		return "decimal"
	case "bool":
		return "bool"
	default:
		return "string"
	}
}

func csConstValue(f ast.ConstantField) string {
	switch f.Type {
	case "string":
		return fmt.Sprintf("%q", f.Value)
	case "uuid":
		return fmt.Sprintf("Guid.Parse(%q)", f.Value)
	case "date", "datetime":
		return fmt.Sprintf("DateTime.Parse(%q)", f.Value)
	case "int":
		return f.Value + "L"
	case "float":
		return f.Value
	case "decimal":
		return f.Value + "m"
	case "bool":
		return f.Value
	default:
		return fmt.Sprintf("%q", f.Value)
	}
}
