package csharp

// errors.go — emits ErrorHandlingMiddleware, ApiErrorResponse, ApiException,
// 7 convenience exception subclasses, extension helper methods, and per-module
// typed error factories. Matches the comprehensive approach used by TS/Node.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitErrorHandling writes Models/ApiErrorResponse.cs, Models/ApiException.cs,
// Models/ApiExceptionExtensions.cs, 7 convenience subclass .cs files,
// and Middleware/ErrorHandlingMiddleware.cs.
func (e *CSharpEmitter) emitErrorHandling(outDir string) error {
	mwDir := filepath.Join(outDir, "Middleware")
	if err := os.MkdirAll(mwDir, 0755); err != nil {
		return err
	}

	modelsDir := filepath.Join(outDir, "Models")

	// ApiErrorResponse
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("namespace %s.Models;\n\n", csNamespace))
	sb.WriteString("/// <summary>Standard API error response.</summary>\n")
	sb.WriteString("public record ApiErrorResponse(\n")
	sb.WriteString("    string Error,\n")
	sb.WriteString("    string Code,\n")
	sb.WriteString("    int Status\n")
	sb.WriteString(");\n")
	if err := os.WriteFile(filepath.Join(modelsDir, "ApiErrorResponse.cs"), []byte(sb.String()), 0644); err != nil {
		return err
	}

	// ApiException
	var ae strings.Builder
	ae.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	ae.WriteString("using System;\n\n")
	ae.WriteString(fmt.Sprintf("namespace %s.Models;\n\n", csNamespace))
	ae.WriteString("/// <summary>\n")
	ae.WriteString("/// Typed API exception with a machine-readable code and HTTP status.\n")
	ae.WriteString("/// Every Veld-generated error factory returns ApiException.\n")
	ae.WriteString("/// </summary>\n")
	ae.WriteString("public class ApiException : Exception\n{\n")
	ae.WriteString("    public string Code { get; }\n")
	ae.WriteString("    public int Status { get; }\n")
	ae.WriteString("    public string Timestamp { get; }\n\n")
	ae.WriteString("    public ApiException(string code, int status, string message)\n")
	ae.WriteString("        : base(message)\n")
	ae.WriteString("    {\n")
	ae.WriteString("        Code = code;\n")
	ae.WriteString("        Status = status;\n")
	ae.WriteString("        Timestamp = DateTime.UtcNow.ToString(\"o\");\n")
	ae.WriteString("    }\n\n")

	// Helper methods
	ae.WriteString("    /// <summary>Check if this exception matches a specific error code.</summary>\n")
	ae.WriteString("    public bool IsErrorCode(string errorCode) => Code == errorCode;\n\n")
	ae.WriteString("    /// <summary>Check if this exception has a specific HTTP status.</summary>\n")
	ae.WriteString("    public bool IsHttpStatus(int httpStatus) => Status == httpStatus;\n\n")
	ae.WriteString("    /// <summary>Convert to a JSON-safe ApiErrorResponse.</summary>\n")
	ae.WriteString("    public ApiErrorResponse ToErrorResponse() => new(Message, Code, Status);\n")
	ae.WriteString("}\n")
	if err := os.WriteFile(filepath.Join(modelsDir, "ApiException.cs"), []byte(ae.String()), 0644); err != nil {
		return err
	}

	// ApiExceptionExtensions — static helper methods
	var ext strings.Builder
	ext.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	ext.WriteString("using System;\n\n")
	ext.WriteString(fmt.Sprintf("namespace %s.Models;\n\n", csNamespace))
	ext.WriteString("/// <summary>Extension methods for exception handling.</summary>\n")
	ext.WriteString("public static class ApiExceptionExtensions\n{\n")
	ext.WriteString("    /// <summary>Check if an exception is an ApiException.</summary>\n")
	ext.WriteString("    public static bool IsApiException(this Exception ex) => ex is ApiException;\n\n")
	ext.WriteString("    /// <summary>Check if an exception is an ApiException with the given code.</summary>\n")
	ext.WriteString("    public static bool IsErrorCode(this Exception ex, string code) =>\n")
	ext.WriteString("        ex is ApiException ae && ae.Code == code;\n\n")
	ext.WriteString("    /// <summary>Check if an exception is an ApiException with the given HTTP status.</summary>\n")
	ext.WriteString("    public static bool IsHttpStatus(this Exception ex, int status) =>\n")
	ext.WriteString("        ex is ApiException ae && ae.Status == status;\n\n")
	ext.WriteString("    /// <summary>\n")
	ext.WriteString("    /// Convert any exception into a consistent ApiErrorResponse.\n")
	ext.WriteString("    /// ApiException instances preserve their code and status;\n")
	ext.WriteString("    /// other exceptions become 500 Internal Server Error.\n")
	ext.WriteString("    /// </summary>\n")
	ext.WriteString("    public static ApiErrorResponse ToErrorResponse(this Exception ex) =>\n")
	ext.WriteString("        ex is ApiException ae\n")
	ext.WriteString("            ? ae.ToErrorResponse()\n")
	ext.WriteString("            : new ApiErrorResponse(ex.Message, \"INTERNAL_ERROR\", 500);\n")
	ext.WriteString("}\n")
	if err := os.WriteFile(filepath.Join(modelsDir, "ApiExceptionExtensions.cs"), []byte(ext.String()), 0644); err != nil {
		return err
	}

	// ── 7 convenience exception subclasses ───────────────────────────────
	type errDef struct {
		name      string
		status    int
		msg       string
		codeConst string
	}
	defs := []errDef{
		{"NotFoundException", 404, "Not Found", "NOT_FOUND"},
		{"BadRequestException", 400, "Bad Request", "BAD_REQUEST"},
		{"UnauthorizedException", 401, "Unauthorized", "UNAUTHORIZED"},
		{"ForbiddenException", 403, "Forbidden", "FORBIDDEN"},
		{"ConflictException", 409, "Conflict", "CONFLICT"},
		{"ValidationException", 422, "Validation Failed", "VALIDATION_FAILED"},
		{"InternalException", 500, "Internal Server Error", "INTERNAL_SERVER_ERROR"},
	}

	for _, d := range defs {
		var cs strings.Builder
		cs.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
		cs.WriteString(fmt.Sprintf("namespace %s.Models;\n\n", csNamespace))
		cs.WriteString(fmt.Sprintf("/// <summary>%d %s — throw from service implementations for quick status responses.</summary>\n", d.status, d.msg))
		cs.WriteString(fmt.Sprintf("public class %s : ApiException\n{\n", d.name))
		cs.WriteString(fmt.Sprintf("    public %s(string? message = null, string? code = null)\n", d.name))
		cs.WriteString(fmt.Sprintf("        : base(code ?? \"%s\", %d, message ?? \"%s\") { }\n", d.codeConst, d.status, d.msg))
		cs.WriteString("}\n")
		if err := os.WriteFile(filepath.Join(modelsDir, d.name+".cs"), []byte(cs.String()), 0644); err != nil {
			return err
		}
	}

	// ErrorHandlingMiddleware
	var mw strings.Builder
	mw.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	mw.WriteString("using System.Net;\n")
	mw.WriteString("using System.Text.Json;\n")
	mw.WriteString(fmt.Sprintf("using %s.Models;\n\n", csNamespace))
	mw.WriteString(fmt.Sprintf("namespace %s.Middleware;\n\n", csNamespace))
	mw.WriteString("public class ErrorHandlingMiddleware\n{\n")
	mw.WriteString("    private readonly RequestDelegate _next;\n")
	mw.WriteString("    private readonly ILogger<ErrorHandlingMiddleware> _logger;\n\n")
	mw.WriteString("    public ErrorHandlingMiddleware(RequestDelegate next, ILogger<ErrorHandlingMiddleware> logger)\n")
	mw.WriteString("    {\n        _next = next;\n        _logger = logger;\n    }\n\n")
	mw.WriteString("    public async Task InvokeAsync(HttpContext context)\n    {\n")
	mw.WriteString("        try\n        {\n            await _next(context);\n        }\n")
	mw.WriteString("        catch (ApiException ex)\n        {\n")
	mw.WriteString("            _logger.LogWarning(ex, \"API error: {Code}\", ex.Code);\n")
	mw.WriteString("            context.Response.StatusCode = ex.Status;\n")
	mw.WriteString("            context.Response.ContentType = \"application/json\";\n")
	mw.WriteString("            var error = ex.ToErrorResponse();\n")
	mw.WriteString("            await context.Response.WriteAsync(JsonSerializer.Serialize(error));\n")
	mw.WriteString("        }\n")
	mw.WriteString("        catch (Exception ex)\n        {\n")
	mw.WriteString("            _logger.LogError(ex, \"Unhandled exception\");\n")
	mw.WriteString("            context.Response.StatusCode = (int)HttpStatusCode.InternalServerError;\n")
	mw.WriteString("            context.Response.ContentType = \"application/json\";\n")
	mw.WriteString("            var error = ex.ToErrorResponse();\n")
	mw.WriteString("            await context.Response.WriteAsync(JsonSerializer.Serialize(error));\n")
	mw.WriteString("        }\n    }\n}\n")

	return os.WriteFile(filepath.Join(mwDir, "ErrorHandlingMiddleware.cs"), []byte(mw.String()), 0644)
}

// emitModuleErrors writes Errors/{Module}Errors.cs with typed error factories.
func (e *CSharpEmitter) emitModuleErrors(mod ast.Module, outDir string) error {
	if !emitter.HasErrors(mod) {
		return nil
	}

	dir := filepath.Join(outDir, "Errors")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	className := mod.Name + "Errors"
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("using %s.Models;\n\n", csNamespace))
	sb.WriteString(fmt.Sprintf("namespace %s.Errors;\n\n", csNamespace))
	sb.WriteString(fmt.Sprintf("public static class %s\n{\n", className))

	for _, act := range mod.Actions {
		if len(act.Errors) == 0 {
			continue
		}
		for _, errName := range act.Errors {
			code := emitter.ErrorCode(act.Name, errName)
			status := emitter.ErrorHTTPStatus(errName)
			methodName := act.Name + errName
			sb.WriteString(fmt.Sprintf("    public static ApiException %s(string? message = null) =>\n", methodName))
			sb.WriteString(fmt.Sprintf("        new(\"%s\", %d, message ?? \"%s\");\n\n", code, status, errName))
		}
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, className+".cs"), []byte(sb.String()), 0644)
}
