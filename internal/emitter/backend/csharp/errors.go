package csharp

// errors.go — emits error handling middleware for ASP.NET Core
// and per-module typed error factories.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitErrorHandling writes Models/ApiErrorResponse.cs, Models/ApiException.cs,
// and Middleware/ErrorHandlingMiddleware.cs.
func (e *CSharpEmitter) emitErrorHandling(outDir string) error {
	mwDir := filepath.Join(outDir, "Middleware")
	if err := os.MkdirAll(mwDir, 0755); err != nil {
		return err
	}

	modelsDir := filepath.Join(outDir, "Models")

	// ApiErrorResponse
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("namespace %s.Models;\n\n", csNamespace))
	sb.WriteString("/// <summary>Standard API error response.</summary>\n")
	sb.WriteString("public record ApiErrorResponse(\n")
	sb.WriteString("    string Error,\n")
	sb.WriteString("    string Code\n")
	sb.WriteString(");\n")
	if err := os.WriteFile(filepath.Join(modelsDir, "ApiErrorResponse.cs"), []byte(sb.String()), 0644); err != nil {
		return err
	}

	// ApiException
	var ae strings.Builder
	ae.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	ae.WriteString(fmt.Sprintf("namespace %s.Models;\n\n", csNamespace))
	ae.WriteString("/// <summary>Typed API exception with a machine-readable code and HTTP status.</summary>\n")
	ae.WriteString("public class ApiException : Exception\n{\n")
	ae.WriteString("    public string Code { get; }\n")
	ae.WriteString("    public int Status { get; }\n\n")
	ae.WriteString("    public ApiException(string code, int status, string message)\n")
	ae.WriteString("        : base(message)\n")
	ae.WriteString("    {\n")
	ae.WriteString("        Code = code;\n")
	ae.WriteString("        Status = status;\n")
	ae.WriteString("    }\n")
	ae.WriteString("}\n")
	if err := os.WriteFile(filepath.Join(modelsDir, "ApiException.cs"), []byte(ae.String()), 0644); err != nil {
		return err
	}

	// ErrorHandlingMiddleware
	var mw strings.Builder
	mw.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	mw.WriteString("using System.Net;\n")
	mw.WriteString("using System.Text.Json;\n")
	mw.WriteString(fmt.Sprintf("using %s.Models;\n\n", csNamespace))
	mw.WriteString(fmt.Sprintf("namespace %s.Middleware;\n\n", csNamespace))
	mw.WriteString("public class ErrorHandlingMiddleware\n{\n")
	mw.WriteString("    private readonly RequestDelegate _next;\n")
	mw.WriteString("    private readonly ILogger<ErrorHandlingMiddleware> _logger;\n\n")
	mw.WriteString("    public ErrorHandlingMiddleware(RequestDelegate next, ILogger<ErrorHandlingMiddleware> logger)\n")
	mw.WriteString("    {\n        _next = next;\n        _logger = logger;\n    }\n\n")
	mw.WriteString("    public async Task InvokeAsync(HttpContext context)\n    {\n")
	mw.WriteString("        try\n        {\n            await _next(context);\n        }\n")
	mw.WriteString("        catch (ApiException ex)\n        {\n")
	mw.WriteString("            _logger.LogWarning(ex, \"API error: {Code}\", ex.Code);\n")
	mw.WriteString("            context.Response.StatusCode = ex.Status;\n")
	mw.WriteString("            context.Response.ContentType = \"application/json\";\n")
	mw.WriteString("            var error = new ApiErrorResponse(ex.Message, ex.Code);\n")
	mw.WriteString("            await context.Response.WriteAsync(JsonSerializer.Serialize(error));\n")
	mw.WriteString("        }\n")
	mw.WriteString("        catch (Exception ex)\n        {\n")
	mw.WriteString("            _logger.LogError(ex, \"Unhandled exception\");\n")
	mw.WriteString("            context.Response.StatusCode = (int)HttpStatusCode.InternalServerError;\n")
	mw.WriteString("            context.Response.ContentType = \"application/json\";\n")
	mw.WriteString("            var error = new ApiErrorResponse(\"Internal server error\", \"\");\n")
	mw.WriteString("            await context.Response.WriteAsync(JsonSerializer.Serialize(error));\n")
	mw.WriteString("        }\n    }\n}\n")

	return os.WriteFile(filepath.Join(mwDir, "ErrorHandlingMiddleware.cs"), []byte(mw.String()), 0644)
}

// emitModuleErrors writes Errors/{Module}Errors.cs with typed error factories.
func (e *CSharpEmitter) emitModuleErrors(mod ast.Module, outDir string) error {
	if !emitter.HasErrors(mod) {
		return nil
	}

	dir := filepath.Join(outDir, "Errors")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	className := mod.Name + "Errors"
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("using %s.Models;\n\n", csNamespace))
	sb.WriteString(fmt.Sprintf("namespace %s.Errors;\n\n", csNamespace))
	sb.WriteString(fmt.Sprintf("public static class %s\n{\n", className))

	for _, act := range mod.Actions {
		if len(act.Errors) == 0 {
			continue
		}
		for _, errName := range act.Errors {
			code := emitter.ErrorCode(act.Name, errName)
			status := emitter.ErrorHTTPStatus(errName)
			methodName := act.Name + errName
			sb.WriteString(fmt.Sprintf("    public static ApiException %s(string? message = null) =>\n", methodName))
			sb.WriteString(fmt.Sprintf("        new(\"%s\", %d, message ?? \"%s\");\n\n", code, status, errName))
		}
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, className+".cs"), []byte(sb.String()), 0644)
}
