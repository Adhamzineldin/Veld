package csharp

// errors.go — emits:
//   Errors/ApiErrorResponse.cs, Errors/ApiException.cs — base types
//   Errors/{Action}Exception.cs — per-action typed exception
//   Errors/{convenience}.cs — 7 convenience subclasses
//   Errors/ApiExceptionExtensions.cs — extension helper methods
//   Errors/{Module}Errors.cs — per-module error factory class
//   Middleware/ErrorHandlingMiddleware.cs — error handler
//
// All error classes live in the Errors/ folder, NOT in Models/.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitAllErrors generates the full Errors/ directory in one shot.
func (e *CSharpEmitter) emitAllErrors(a ast.AST, outDir string) error {
	errDir := filepath.Join(outDir, "Errors")
	if err := os.MkdirAll(errDir, 0755); err != nil {
		return err
	}
	mwDir := filepath.Join(outDir, "Middleware")
	if err := os.MkdirAll(mwDir, 0755); err != nil {
		return err
	}

	// ── Clean up stale error files from Models/ (old code put them there) ──
	modelsDir := filepath.Join(outDir, "Models")
	for _, f := range []string{
		"ApiException.cs", "ApiErrorResponse.cs", "ApiExceptionExtensions.cs",
		"NotFoundException.cs", "BadRequestException.cs", "UnauthorizedException.cs",
		"ForbiddenException.cs", "ConflictException.cs", "ValidationException.cs",
		"InternalException.cs",
	} {
		_ = os.Remove(filepath.Join(modelsDir, f))
	}

	// ApiErrorResponse.cs
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("namespace %s.Errors;\n\n", csNamespace))
	sb.WriteString("/// <summary>Standard API error response.</summary>\n")
	sb.WriteString("public record ApiErrorResponse(string Error, string Code, int Status);\n")
	if err := os.WriteFile(filepath.Join(errDir, "ApiErrorResponse.cs"), []byte(sb.String()), 0644); err != nil {
		return err
	}

	// ApiException.cs
	var ae strings.Builder
	ae.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	ae.WriteString("using System;\n\n")
	ae.WriteString(fmt.Sprintf("namespace %s.Errors;\n\n", csNamespace))
	ae.WriteString("/// <summary>Typed API exception with a machine-readable code and HTTP status.</summary>\n")
	ae.WriteString("public class ApiException : Exception\n{\n")
	ae.WriteString("    public string Code { get; }\n")
	ae.WriteString("    public int Status { get; }\n")
	ae.WriteString("    public string Timestamp { get; }\n\n")
	ae.WriteString("    public ApiException(string code, int status, string message) : base(message)\n")
	ae.WriteString("    {\n        Code = code;\n        Status = status;\n")
	ae.WriteString("        Timestamp = DateTime.UtcNow.ToString(\"o\");\n    }\n\n")
	ae.WriteString("    public bool IsErrorCode(string errorCode) => Code == errorCode;\n")
	ae.WriteString("    public bool IsHttpStatus(int httpStatus) => Status == httpStatus;\n")
	ae.WriteString("    public ApiErrorResponse ToErrorResponse() => new(Message, Code, Status);\n\n")
	ae.WriteString("    public static bool IsApiException(Exception ex) => ex is ApiException;\n")
	ae.WriteString("    public static ApiErrorResponse ToErrorResponse(Exception ex) =>\n")
	ae.WriteString("        ex is ApiException ae ? ae.ToErrorResponse() : new(ex.Message, \"INTERNAL_ERROR\", 500);\n")
	ae.WriteString("}\n")
	if err := os.WriteFile(filepath.Join(errDir, "ApiException.cs"), []byte(ae.String()), 0644); err != nil {
		return err
	}

	// ApiExceptionExtensions.cs
	var ext strings.Builder
	ext.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	ext.WriteString("using System;\n\n")
	ext.WriteString(fmt.Sprintf("namespace %s.Errors;\n\n", csNamespace))
	ext.WriteString("public static class ApiExceptionExtensions\n{\n")
	ext.WriteString("    public static bool IsApiException(this Exception ex) => ex is ApiException;\n")
	ext.WriteString("    public static bool IsErrorCode(this Exception ex, string code) =>\n")
	ext.WriteString("        ex is ApiException ae && ae.Code == code;\n")
	ext.WriteString("    public static bool IsHttpStatus(this Exception ex, int status) =>\n")
	ext.WriteString("        ex is ApiException ae && ae.Status == status;\n")
	ext.WriteString("    public static ApiErrorResponse ToErrorResponse(this Exception ex) =>\n")
	ext.WriteString("        ex is ApiException ae ? ae.ToErrorResponse() : new(ex.Message, \"INTERNAL_ERROR\", 500);\n")
	ext.WriteString("}\n")
	if err := os.WriteFile(filepath.Join(errDir, "ApiExceptionExtensions.cs"), []byte(ext.String()), 0644); err != nil {
		return err
	}

	// 7 convenience subclasses
	type errDef struct {
		name, msg, codeConst string
		status               int
	}
	defs := []errDef{
		{"NotFoundException", "Not Found", "NOT_FOUND", 404},
		{"BadRequestException", "Bad Request", "BAD_REQUEST", 400},
		{"UnauthorizedException", "Unauthorized", "UNAUTHORIZED", 401},
		{"ForbiddenException", "Forbidden", "FORBIDDEN", 403},
		{"ConflictException", "Conflict", "CONFLICT", 409},
		{"ValidationException", "Validation Failed", "VALIDATION_FAILED", 422},
		{"InternalException", "Internal Server Error", "INTERNAL_SERVER_ERROR", 500},
	}
	for _, d := range defs {
		var cs strings.Builder
		cs.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
		cs.WriteString(fmt.Sprintf("namespace %s.Errors;\n\n", csNamespace))
		cs.WriteString(fmt.Sprintf("/// <summary>%d %s — throw from service implementations for quick status responses.</summary>\n", d.status, d.msg))
		cs.WriteString(fmt.Sprintf("public class %s : ApiException\n{\n", d.name))
		cs.WriteString(fmt.Sprintf("    public %s(string? message = null, string? code = null)\n", d.name))
		cs.WriteString(fmt.Sprintf("        : base(code ?? \"%s\", %d, message ?? \"%s\") { }\n", d.codeConst, d.status, d.msg))
		cs.WriteString("}\n")
		if err := os.WriteFile(filepath.Join(errDir, d.name+".cs"), []byte(cs.String()), 0644); err != nil {
			return err
		}
	}

	// Per-action typed exceptions (e.g., GetAccountException)
	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			if len(act.Errors) == 0 {
				continue
			}
			className := csPascalName(act.Name) + "Exception"
			var codes []string
			for _, errName := range act.Errors {
				codes = append(codes, emitter.ErrorCode(act.Name, errName))
			}

			var cs strings.Builder
			cs.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
			cs.WriteString(fmt.Sprintf("namespace %s.Errors;\n\n", csNamespace))
			cs.WriteString(fmt.Sprintf("/// <summary>Typed exception for the %s action. Codes: %s</summary>\n",
				act.Name, strings.Join(codes, ", ")))
			cs.WriteString(fmt.Sprintf("public class %s : ApiException\n{\n", className))
			cs.WriteString(fmt.Sprintf("    public %s(string code, int status, string message) : base(code, status, message) { }\n", className))

			for _, errName := range act.Errors {
				code := emitter.ErrorCode(act.Name, errName)
				status := emitter.ErrorHTTPStatus(errName)
				methodName := csPascalName(errName)
				cs.WriteString(fmt.Sprintf("\n    public static %s %s(string? message = null) =>\n", className, methodName))
				cs.WriteString(fmt.Sprintf("        new(\"%s\", %d, message ?? \"%s\");\n", code, status, errName))
			}

			cs.WriteString("}\n")
			if err := os.WriteFile(filepath.Join(errDir, className+".cs"), []byte(cs.String()), 0644); err != nil {
				return err
			}
		}
	}

	// Per-module error factory class
	for _, mod := range a.Modules {
		if !emitter.HasErrors(mod) {
			continue
		}
		className := mod.Name + "Errors"
		var ms strings.Builder
		ms.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
		ms.WriteString(fmt.Sprintf("namespace %s.Errors;\n\n", csNamespace))
		ms.WriteString(fmt.Sprintf("/// <summary>Error factories for %s — mirrors TypeScript %sErrors pattern.\n", mod.Name, strings.ToLower(mod.Name)))
		ms.WriteString(fmt.Sprintf("/// Usage: throw %s.%s.%s();</summary>\n",
			className,
			func() string {
				for _, act := range mod.Actions {
					if len(act.Errors) > 0 {
						return csPascalName(act.Name)
					}
				}
				return "Action"
			}(),
			func() string {
				for _, act := range mod.Actions {
					if len(act.Errors) > 0 {
						return csPascalName(act.Errors[0])
					}
				}
				return "Error"
			}()))
		ms.WriteString(fmt.Sprintf("public static class %s\n{\n", className))
		for _, act := range mod.Actions {
			if len(act.Errors) == 0 {
				continue
			}
			innerClass := csPascalName(act.Name) + "Errors"
			exceptionClass := csPascalName(act.Name) + "Exception"
			ms.WriteString(fmt.Sprintf("    public static %s %s { get; } = new();\n\n", innerClass, csPascalName(act.Name)))
			ms.WriteString(fmt.Sprintf("    /// <summary>Error factories and code constants for %s.</summary>\n", act.Name))
			ms.WriteString(fmt.Sprintf("    public sealed class %s\n    {\n", innerClass))
			for _, errName := range act.Errors {
				code := emitter.ErrorCode(act.Name, errName)
				status := emitter.ActionErrorStatus(act, errName)
				constName := emitter.ToScreamingSnake(errName) + "_CODE"
				constStatus := emitter.ToScreamingSnake(errName) + "_STATUS"
				methodName := csPascalName(errName)
				ms.WriteString(fmt.Sprintf("        public const string %s = \"%s\";\n", constName, code))
				ms.WriteString(fmt.Sprintf("        public const int %s = %d;\n\n", constStatus, status))
				ms.WriteString(fmt.Sprintf("        public %s %s(string? message = null) =>\n", exceptionClass, methodName))
				ms.WriteString(fmt.Sprintf("            new(\"%s\", %d, message ?? \"%s\");\n\n", code, status, errName))
			}
			ms.WriteString("    }\n\n")
		}
		ms.WriteString("}\n")
		if err := os.WriteFile(filepath.Join(errDir, className+".cs"), []byte(ms.String()), 0644); err != nil {
			return err
		}
	}

	// ErrorHandlingMiddleware.cs
	var mw strings.Builder
	mw.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	mw.WriteString("using System.Net;\nusing System.Text.Json;\n")
	mw.WriteString(fmt.Sprintf("using %s.Errors;\n\n", csNamespace))
	mw.WriteString(fmt.Sprintf("namespace %s.Middleware;\n\n", csNamespace))
	mw.WriteString("public class ErrorHandlingMiddleware\n{\n")
	mw.WriteString("    private readonly RequestDelegate _next;\n")
	mw.WriteString("    private readonly ILogger<ErrorHandlingMiddleware> _logger;\n\n")
	mw.WriteString("    public ErrorHandlingMiddleware(RequestDelegate next, ILogger<ErrorHandlingMiddleware> logger)\n")
	mw.WriteString("    { _next = next; _logger = logger; }\n\n")
	mw.WriteString("    public async Task InvokeAsync(HttpContext context)\n    {\n")
	mw.WriteString("        try { await _next(context); }\n")
	mw.WriteString("        catch (ApiException ex)\n        {\n")
	mw.WriteString("            _logger.LogWarning(ex, \"API error: {Code}\", ex.Code);\n")
	mw.WriteString("            context.Response.StatusCode = ex.Status;\n")
	mw.WriteString("            context.Response.ContentType = \"application/json\";\n")
	mw.WriteString("            await context.Response.WriteAsync(JsonSerializer.Serialize(ex.ToErrorResponse()));\n")
	mw.WriteString("        }\n")
	mw.WriteString("        catch (Exception ex)\n        {\n")
	mw.WriteString("            _logger.LogError(ex, \"Unhandled exception\");\n")
	mw.WriteString("            context.Response.StatusCode = (int)HttpStatusCode.InternalServerError;\n")
	mw.WriteString("            context.Response.ContentType = \"application/json\";\n")
	mw.WriteString("            await context.Response.WriteAsync(JsonSerializer.Serialize(ex.ToErrorResponse()));\n")
	mw.WriteString("        }\n    }\n}\n")
	return os.WriteFile(filepath.Join(mwDir, "ErrorHandlingMiddleware.cs"), []byte(mw.String()), 0644)
}

// Legacy no-ops
func (e *CSharpEmitter) emitErrorHandling(_ string) error              { return nil }
func (e *CSharpEmitter) emitModuleErrors(_ ast.Module, _ string) error { return nil }
