package csharp

// types.go — emits Models/*.cs: one C# record per Veld model, one C# enum
// per Veld enum. Nullable reference types are enabled via the .csproj setting.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
)

const csNamespace = "VeldGenerated"

func (e *CSharpEmitter) emitModels(a ast.AST, outDir string) error {
	dir := filepath.Join(outDir, "Models")
	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	for _, en := range a.Enums {
		if err := e.emitEnum(en, dir); err != nil {
			return fmt.Errorf("enum %s: %w", en.Name, err)
		}
	}
	for _, m := range a.Models {
		if err := e.emitRecord(m, enumNames, dir); err != nil {
			return fmt.Errorf("model %s: %w", m.Name, err)
		}
	}
	return nil
}

// emitRecord writes a C# class with properties, constructors, and nullable support.
func (e *CSharpEmitter) emitRecord(m ast.Model, enumNames map[string]bool, dir string) error {
	needsList := false
	needsDict := false
	for _, f := range m.Fields {
		if f.IsArray {
			needsList = true
		}
		if f.IsMap {
			needsDict = true
		}
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	if needsList || needsDict {
		sb.WriteString("using System.Collections.Generic;\n")
	}
	sb.WriteString("using System.Text.Json.Serialization;\n")
	sb.WriteString(fmt.Sprintf("\nnamespace %s.Models;\n\n", csNamespace))

	if m.Description != "" {
		sb.WriteString(fmt.Sprintf("/// <summary>%s</summary>\n", m.Description))
	}

	extendsClause := ""
	if m.Extends != "" {
		extendsClause = " : " + m.Extends
	}
	sb.WriteString(fmt.Sprintf("public class %s%s\n{\n", m.Name, extendsClause))

	// Properties with { get; set; }
	for _, f := range m.Fields {
		csType := csFieldType(f, enumNames)
		propName := csPascalName(f.Name)
		if f.Optional {
			csType = csType + "?"
		}
		sb.WriteString(fmt.Sprintf("    [JsonPropertyName(%q)]\n", f.Name))
		sb.WriteString(fmt.Sprintf("    public %s %s { get; set; }\n\n", csType, propName))
	}

	// Default constructor
	sb.WriteString(fmt.Sprintf("    public %s() { }\n\n", m.Name))

	// Parameterized constructor
	if len(m.Fields) > 0 {
		var ctorParams []string
		for _, f := range m.Fields {
			csType := csFieldType(f, enumNames)
			if f.Optional {
				csType = csType + "?"
			}
			paramName := csCamelCase(f.Name)
			ctorParams = append(ctorParams, fmt.Sprintf("%s %s", csType, paramName))
		}
		sb.WriteString(fmt.Sprintf("    public %s(%s)\n    {\n", m.Name, strings.Join(ctorParams, ", ")))
		for _, f := range m.Fields {
			sb.WriteString(fmt.Sprintf("        %s = %s;\n", csPascalName(f.Name), csCamelCase(f.Name)))
		}
		sb.WriteString("    }\n")
	}

	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, m.Name+".cs"), []byte(sb.String()), 0644)
}

func csCamelCase(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

// emitEnum writes a C# enum with [JsonConverter(typeof(JsonStringEnumConverter))].
func (e *CSharpEmitter) emitEnum(en ast.Enum, dir string) error {
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString("using System.Text.Json.Serialization;\n")
	sb.WriteString(fmt.Sprintf("\nnamespace %s.Models;\n\n", csNamespace))

	if en.Description != "" {
		sb.WriteString(fmt.Sprintf("/// <summary>%s</summary>\n", en.Description))
	}
	sb.WriteString("[JsonConverter(typeof(JsonStringEnumConverter))]\n")
	sb.WriteString(fmt.Sprintf("public enum %s\n{\n", en.Name))
	for _, v := range en.Values {
		// C# enum members are PascalCase; use [EnumMember(Value = ...)] for the raw string.
		memberName := csPascalName(v)
		sb.WriteString(fmt.Sprintf("    [System.Runtime.Serialization.EnumMember(Value = \"%s\")]\n", v))
		sb.WriteString(fmt.Sprintf("    %s,\n", memberName))
	}
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, en.Name+".cs"), []byte(sb.String()), 0644)
}

// csFieldType maps an AST field to a C# type string (without nullability).
func csFieldType(f ast.Field, enumNames map[string]bool) string {
	if f.IsMap {
		return "Dictionary<string, " + veldScalarToCS(f.MapValueType, enumNames) + ">"
	}
	base := veldScalarToCS(f.Type, enumNames)
	if f.IsArray {
		return "List<" + base + ">"
	}
	return base
}

// veldScalarToCS maps a Veld scalar to a C# type.
func veldScalarToCS(t string, _ map[string]bool) string {
	switch t {
	case "string":
		return "string"
	case "uuid":
		return "Guid"
	case "date", "datetime":
		return "DateTime"
	case "int":
		return "long"
	case "float":
		return "double"
	case "decimal":
		return "decimal"
	case "bool":
		return "bool"
	case "any", "json":
		return "object"
	default:
		return t // custom model/enum name
	}
}

// csPascalName converts a name to PascalCase for C# identifiers.
func csPascalName(name string) string {
	parts := strings.FieldsFunc(toSnakeCaseCS(name), func(r rune) bool { return r == '_' })
	var sb strings.Builder
	for _, p := range parts {
		if len(p) > 0 {
			sb.WriteString(strings.ToUpper(p[:1]) + p[1:])
		}
	}
	return sb.String()
}

func toSnakeCaseCS(s string) string {
	var result strings.Builder
	var prev rune
	for i, r := range s {
		isUpper := r >= 'A' && r <= 'Z'
		if isUpper && i > 0 && prev != '_' {
			prevUpper := prev >= 'A' && prev <= 'Z'
			if !prevUpper {
				result.WriteRune('_')
			}
		}
		result.WriteRune(r | 32)
		prev = r
	}
	out := result.String()
	for strings.Contains(out, "__") {
		out = strings.ReplaceAll(out, "__", "_")
	}
	return strings.Trim(out, "_")
}
