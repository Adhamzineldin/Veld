package java

// interfaces.go — emits services/I{Module}Service.java with typed method signatures.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	jstrategy "github.com/Adhamzineldin/Veld/internal/emitter/backend/java/strategy"
)

const javaPackageServices = "maayn.veld.generated.services"

func (e *JavaEmitter) emitInterface(_ jstrategy.FrameworkStrategy, a ast.AST, mod ast.Module, outDir string) error {
	dir := filepath.Join(outDir, "services")

	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", javaPackageServices))
	sb.WriteString(fmt.Sprintf("import %s.*;\n", javaPackageModels))
	sb.WriteString("import java.util.List;\n")
	for _, act := range mod.Actions {
		if len(act.Errors) > 0 {
			sb.WriteString(fmt.Sprintf("import %s.%sException;\n", javaPackageModels, capitalize(act.Name)))
		}
	}
	sb.WriteString("\n")

	modClass := capitalize(mod.Name)

	if mod.Description != "" {
		sb.WriteString(fmt.Sprintf("/** %s */\n", mod.Description))
	}
	sb.WriteString(fmt.Sprintf("public interface I%sService {\n", modClass))

	for _, act := range mod.Actions {
		routePath := act.Path
		if mod.Prefix != "" {
			routePath = mod.Prefix + act.Path
		}
		pathParams := emitter.ExtractPathParams(routePath)

		if act.Method == "WS" {
			actionName := capitalize(act.Name)
			// onConnect
			var connectParams []string
			connectParams = append(connectParams, "javax.websocket.Session session")
			for _, p := range pathParams {
				connectParams = append(connectParams, "String "+javaCamelField(p))
			}
			if act.Description != "" {
				sb.WriteString(fmt.Sprintf("    /** Called when a client opens the WS connection. %s */\n", act.Description))
			} else {
				sb.WriteString(fmt.Sprintf("    /** Called when a client opens WS %s. */\n", routePath))
			}
			sb.WriteString(fmt.Sprintf("    void on%sConnect(%s);\n\n", actionName, strings.Join(connectParams, ", ")))

			// onMessage — only when emit type is set
			if act.Emit != "" {
				sb.WriteString(fmt.Sprintf("    /** Called when a client sends a %s message. */\n", act.Emit))
				sb.WriteString(fmt.Sprintf("    void on%sMessage(javax.websocket.Session session, %s msg);\n\n", actionName, act.Emit))
			}

			// onClose — always included
			sb.WriteString(fmt.Sprintf("    /** Called when the WS %s connection is closed. */\n", routePath))
			sb.WriteString(fmt.Sprintf("    void on%sClose(javax.websocket.Session session);\n\n", actionName))
			continue
		}

		returnType := javaReturnType(act, enumNames)

		var params []string
		for _, p := range pathParams {
			params = append(params, "String "+javaCamelField(p))
		}
		if act.Input != "" {
			params = append(params, act.Input+" input")
		}
		if act.Query != "" {
			params = append(params, act.Query+" query")
		}

		hasDoc := act.Description != "" || len(act.Errors) > 0
		if hasDoc {
			sb.WriteString("    /**\n")
			if act.Description != "" {
				sb.WriteString(fmt.Sprintf("     * %s\n", act.Description))
			}
			for _, errName := range act.Errors {
				code := emitter.ErrorCode(act.Name, errName)
				sb.WriteString(fmt.Sprintf("     * @throws ApiException %s \u2014 %s\n", code, errName))
			}
			sb.WriteString("     */\n")
		}
		throwsClause := ""
		if len(act.Errors) > 0 {
			throwsClause = " throws " + capitalize(act.Name) + "Exception"
		}
		sb.WriteString(fmt.Sprintf("    %s %s(%s)%s;\n\n", returnType, javaCamelField(act.Name), strings.Join(params, ", "), throwsClause))
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, fmt.Sprintf("I%sService.java", modClass)), []byte(sb.String()), 0644)
}

// javaReturnType returns the Java return type for a service method.
func javaReturnType(act ast.Action, _ map[string]bool) string {
	if act.Output == "" {
		return "void"
	}
	base, _, _ := javaLang.MapType(act.Output)
	if act.OutputArray {
		return "List<" + base + ">"
	}
	return base
}
