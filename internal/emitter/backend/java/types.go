package java

// types.go — emits models/{module}/*.java: one Java class per Veld model,
// one Java enum per Veld enum, organised into per-module subpackages.
//
// Import pattern:
//
//	import maayn.veld.generated.models.auth.*;   // all types owned by Auth module
//	import maayn.veld.generated.models.shared.*; // unreferenced/shared types
//
// Java does not support barrel re-exports from parent packages; use the
// per-module wildcard or a specific class import.

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/lang"
)

const javaPackageModels = "maayn.veld.generated.models"

// javaLang is the shared language adapter for pure Java type and naming conventions.
var javaLang = &lang.JavaAdapter{}

func (e *JavaEmitter) emitModels(a ast.AST, outDir string) error {
	modelGroups, enumGroups, modelOwner, enumOwner := emitter.AssignModelsToModules(a)
	keys := emitter.SortedGroupKeys(modelGroups, enumGroups)

	for _, group := range keys {
		subPkg := javaPackageModels + "." + group
		subDir := pkgToDir(outDir, subPkg)
		if err := os.MkdirAll(subDir, 0755); err != nil {
			return err
		}
		for _, en := range enumGroups[group] {
			if err := e.emitEnum(en, subDir, subPkg); err != nil {
				return fmt.Errorf("enum %s: %w", en.Name, err)
			}
		}
		for _, m := range modelGroups[group] {
			if err := e.emitRecord(m, a.Enums, subDir, subPkg, group, modelOwner, enumOwner); err != nil {
				return fmt.Errorf("model %s: %w", m.Name, err)
			}
		}
	}
	return nil
}

// javaModuleModelImports returns the sorted list of per-module import statements
// needed for a module's routes or interfaces, replacing the old flat wildcard import.
func javaModuleModelImports(a ast.AST, mod ast.Module) []string {
	_, _, modelOwner, enumOwner := emitter.AssignModelsToModules(a)

	// Build lookup sets of actual model/enum names so we never import a group
	// just because a primitive type like "bool" slipped through.
	modelNames := make(map[string]bool, len(a.Models))
	for _, m := range a.Models {
		modelNames[m.Name] = true
	}
	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	groups := make(map[string]bool)
	for name := range emitter.CollectTransitiveModels(a, mod) {
		if !modelNames[name] {
			continue
		}
		if g := modelOwner[name]; g != "" {
			groups[g] = true
		}
	}
	for name := range emitter.CollectUsedEnums(a, mod) {
		if !enumNames[name] {
			continue
		}
		if g := enumOwner[name]; g != "" {
			groups[g] = true
		}
	}

	var imports []string
	for g := range groups {
		imports = append(imports, fmt.Sprintf("import %s.%s.*;", javaPackageModels, g))
	}
	sort.Strings(imports)
	return imports
}

// emitRecord writes a Java class for a Veld model with constructor + getters/setters.
func (e *JavaEmitter) emitRecord(m ast.Model, enums []ast.Enum, dir, pkg, group string, modelOwner, enumOwner map[string]string) error {
	enumNames := make(map[string]bool, len(enums))
	for _, en := range enums {
		enumNames[en.Name] = true
	}

	needsList := false
	needsMap := false
	importSet := make(map[string]bool)
	for _, f := range m.Fields {
		if f.IsArray {
			needsList = true
		}
		if f.IsMap {
			needsMap = true
		}
		_, imports, _ := javaLang.MapType(f.Type)
		for _, imp := range imports {
			importSet[imp] = true
		}
		if f.IsMap {
			_, valImports, _ := javaLang.MapType(f.MapValueType)
			for _, imp := range valImports {
				importSet[imp] = true
			}
		}
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", pkg))

	if needsList {
		sb.WriteString("import java.util.List;\n")
	}
	if needsMap {
		sb.WriteString("import java.util.Map;\n")
	}
	for imp := range importSet {
		sb.WriteString(fmt.Sprintf("import %s;\n", imp))
	}
	sb.WriteString("import com.fasterxml.jackson.annotation.JsonIgnoreProperties;\n")

	// Cross-group imports: types owned by another module need a fully-qualified import.
	crossGroupImports := make(map[string]bool)
	addCrossImport := func(typeName string) {
		if typeName == "" || emitter.IsPrimitive(typeName) {
			return
		}
		owner := modelOwner[typeName]
		if owner == "" {
			owner = enumOwner[typeName]
		}
		if owner != "" && owner != group {
			crossGroupImports[fmt.Sprintf("%s.%s.%s", javaPackageModels, owner, typeName)] = true
		}
	}
	if m.Extends != "" {
		addCrossImport(m.Extends)
	}
	for _, f := range m.Fields {
		addCrossImport(f.Type)
		if f.IsMap {
			addCrossImport(f.MapValueType)
		}
	}
	for impPath := range crossGroupImports {
		sb.WriteString(fmt.Sprintf("import %s;\n", impPath))
	}
	sb.WriteString("\n")

	if m.Description != "" {
		sb.WriteString(fmt.Sprintf("/** %s */\n", m.Description))
	}

	sb.WriteString("@JsonIgnoreProperties(ignoreUnknown = true)\n")
	extendsClause := ""
	if m.Extends != "" {
		extendsClause = " extends " + m.Extends
	}
	sb.WriteString(fmt.Sprintf("public class %s%s {\n", m.Name, extendsClause))

	// Private fields
	for _, f := range m.Fields {
		javaType := javaFieldType(f)
		fieldName := javaCamelField(f.Name)
		if f.Default != "" {
			sb.WriteString(fmt.Sprintf("    private %s %s = %s;\n", javaType, fieldName, javaDefaultLiteral(f.Default, f.Type)))
		} else {
			sb.WriteString(fmt.Sprintf("    private %s %s;\n", javaType, fieldName))
		}
	}
	sb.WriteString("\n")

	// No-arg constructor (public so callers can use setters/getters freely)
	sb.WriteString(fmt.Sprintf("    public %s() {}\n\n", m.Name))

	// All-args constructor
	if len(m.Fields) > 0 {
		var ctorParams []string
		for _, f := range m.Fields {
			ctorParams = append(ctorParams, fmt.Sprintf("%s %s", javaFieldType(f), javaCamelField(f.Name)))
		}
		sb.WriteString(fmt.Sprintf("    public %s(%s) {\n", m.Name, strings.Join(ctorParams, ", ")))
		for _, f := range m.Fields {
			fieldName := javaCamelField(f.Name)
			sb.WriteString(fmt.Sprintf("        this.%s = %s;\n", fieldName, fieldName))
		}
		sb.WriteString("    }\n\n")
	}

	// Getters and setters
	for _, f := range m.Fields {
		javaType := javaFieldType(f)
		fieldName := javaCamelField(f.Name)
		getter := "get" + capitalize(f.Name)
		setter := "set" + capitalize(f.Name)
		sb.WriteString(fmt.Sprintf("    public %s %s() { return this.%s; }\n", javaType, getter, fieldName))
		sb.WriteString(fmt.Sprintf("    public void %s(%s %s) { this.%s = %s; }\n", setter, javaType, fieldName, fieldName, fieldName))
	}

	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, m.Name+".java"), []byte(sb.String()), 0644)
}

// emitEnum writes a Java enum with @JsonValue/@JsonCreator for proper JSON serialisation.
func (e *JavaEmitter) emitEnum(en ast.Enum, dir, pkg string) error {
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", pkg))
	sb.WriteString("import com.fasterxml.jackson.annotation.JsonCreator;\n")
	sb.WriteString("import com.fasterxml.jackson.annotation.JsonValue;\n\n")

	if en.Description != "" {
		sb.WriteString(fmt.Sprintf("/** %s */\n", en.Description))
	}
	sb.WriteString(fmt.Sprintf("public enum %s {\n", en.Name))

	for i, v := range en.Values {
		constName := strings.ToUpper(strings.ReplaceAll(v, "-", "_"))
		comma := ","
		if i == len(en.Values)-1 {
			comma = ";"
		}
		sb.WriteString(fmt.Sprintf("    %s(\"%s\")%s\n", constName, v, comma))
	}

	sb.WriteString("\n    private final String value;\n\n")
	sb.WriteString(fmt.Sprintf("    %s(String value) { this.value = value; }\n\n", en.Name))
	sb.WriteString("    @JsonValue\n")
	sb.WriteString("    public String getValue() { return value; }\n\n")
	sb.WriteString("    @JsonCreator\n")
	sb.WriteString(fmt.Sprintf("    public static %s fromValue(String value) {\n", en.Name))
	sb.WriteString("        for (" + en.Name + " e : values()) {\n")
	sb.WriteString("            if (e.value.equals(value)) return e;\n")
	sb.WriteString("        }\n")
	sb.WriteString(fmt.Sprintf("        throw new IllegalArgumentException(\"Unknown %s: \" + value);\n", en.Name))
	sb.WriteString("    }\n")
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, en.Name+".java"), []byte(sb.String()), 0644)
}

// javaFieldType maps an AST field to its Java type string via the language adapter.
func javaFieldType(f ast.Field) string {
	if f.IsMap {
		valType, _, _ := javaLang.MapType(f.MapValueType)
		return "Map<String, " + valType + ">"
	}
	base, _, _ := javaLang.MapType(f.Type)
	if f.IsArray {
		return "List<" + base + ">"
	}
	return base
}

// javaCamelField converts a field or parameter name to Java camelCase via the language adapter.
func javaCamelField(name string) string {
	return javaLang.NamingConvention(name, lang.NamingContextPrivate)
}

// javaDefaultLiteral converts a Veld default value to a Java literal.
func javaDefaultLiteral(val, veldType string) string {
	// Already a quoted string → Java string literal
	if strings.HasPrefix(val, "\"") {
		return val
	}
	// Booleans
	if val == "true" || val == "false" {
		return val
	}
	// Numeric — int/float pass through as-is
	if veldType == "int" || veldType == "long" || veldType == "float" {
		return val
	}
	if veldType == "decimal" {
		return fmt.Sprintf("new java.math.BigDecimal(\"%s\")", val)
	}
	// Enum value or identifier — emit as string
	return "\"" + val + "\""
}
