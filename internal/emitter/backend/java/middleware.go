package java

// middleware.go — emits per-module middleware interfaces for Spring Boot.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitModuleMiddleware writes services/I{Module}Middleware.java with a typed interface
// per middleware name so the user knows exactly what to implement.
func (e *JavaEmitter) emitModuleMiddleware(mod ast.Module, outDir string) error {
	allMiddleware := emitter.CollectModuleMiddleware(mod)
	if len(allMiddleware) == 0 {
		return nil
	}

	dir := filepath.Join(outDir, "services")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	interfaceName := "I" + mod.Name + "Middleware"
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s.services;\n\n", javaPackageModels))
	sb.WriteString("import jakarta.servlet.http.HttpServletRequest;\n")
	sb.WriteString("import jakarta.servlet.http.HttpServletResponse;\n\n")
	sb.WriteString(fmt.Sprintf("/**\n * Middleware interface for the %s module.\n", mod.Name))
	sb.WriteString(" * Implement this interface and register it with the controller.\n */\n")
	sb.WriteString(fmt.Sprintf("public interface %s {\n\n", interfaceName))

	for _, mw := range allMiddleware {
		methodName := emitter.ToCamelCase(mw)
		sb.WriteString(fmt.Sprintf("    /** %s middleware — return true to proceed, false to reject. */\n", mw))
		sb.WriteString(fmt.Sprintf("    boolean %s(HttpServletRequest request, HttpServletResponse response);\n\n", methodName))
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, interfaceName+".java"), []byte(sb.String()), 0644)
}
