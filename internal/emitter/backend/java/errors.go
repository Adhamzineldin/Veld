package java

// errors.go — emits a GlobalExceptionHandler.java with @ControllerAdvice error handling.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

// emitErrorHandler writes controllers/GlobalExceptionHandler.java + models/ApiErrorResponse.java.
func (e *JavaEmitter) emitErrorHandler(outDir string) error {
	// ApiErrorResponse model
	modelsDir := filepath.Join(outDir, "models")
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", javaPackageModels))
	sb.WriteString("/** Standard API error response body. */\n")
	sb.WriteString("public record ApiErrorResponse(\n")
	sb.WriteString("    int code,\n")
	sb.WriteString("    String message,\n")
	sb.WriteString("    String details\n")
	sb.WriteString(") {}\n")
	if err := os.WriteFile(filepath.Join(modelsDir, "ApiErrorResponse.java"), []byte(sb.String()), 0644); err != nil {
		return err
	}

	// GlobalExceptionHandler
	ctrlDir := filepath.Join(outDir, "controllers")
	var eh strings.Builder
	eh.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	eh.WriteString(fmt.Sprintf("package %s;\n\n", javaPackageControllers))
	eh.WriteString(fmt.Sprintf("import %s.ApiErrorResponse;\n", javaPackageModels))
	eh.WriteString("import org.springframework.http.HttpStatus;\n")
	eh.WriteString("import org.springframework.http.ResponseEntity;\n")
	eh.WriteString("import org.springframework.web.bind.annotation.ControllerAdvice;\n")
	eh.WriteString("import org.springframework.web.bind.annotation.ExceptionHandler;\n")
	eh.WriteString("import org.springframework.web.bind.MethodArgumentNotValidException;\n")
	eh.WriteString("import java.util.stream.Collectors;\n\n")

	eh.WriteString("@ControllerAdvice\n")
	eh.WriteString("public class GlobalExceptionHandler {\n\n")

	eh.WriteString("    @ExceptionHandler(IllegalArgumentException.class)\n")
	eh.WriteString("    public ResponseEntity<ApiErrorResponse> handleBadRequest(IllegalArgumentException ex) {\n")
	eh.WriteString("        return ResponseEntity.status(HttpStatus.BAD_REQUEST)\n")
	eh.WriteString("            .body(new ApiErrorResponse(400, ex.getMessage(), null));\n")
	eh.WriteString("    }\n\n")

	eh.WriteString("    @ExceptionHandler(MethodArgumentNotValidException.class)\n")
	eh.WriteString("    public ResponseEntity<ApiErrorResponse> handleValidation(MethodArgumentNotValidException ex) {\n")
	eh.WriteString("        String details = ex.getBindingResult().getFieldErrors().stream()\n")
	eh.WriteString("            .map(e -> e.getField() + \": \" + e.getDefaultMessage())\n")
	eh.WriteString("            .collect(Collectors.joining(\"; \"));\n")
	eh.WriteString("        return ResponseEntity.status(HttpStatus.BAD_REQUEST)\n")
	eh.WriteString("            .body(new ApiErrorResponse(400, \"Validation failed\", details));\n")
	eh.WriteString("    }\n\n")

	eh.WriteString("    @ExceptionHandler(Exception.class)\n")
	eh.WriteString("    public ResponseEntity<ApiErrorResponse> handleAll(Exception ex) {\n")
	eh.WriteString("        return ResponseEntity.status(HttpStatus.INTERNAL_SERVER_ERROR)\n")
	eh.WriteString("            .body(new ApiErrorResponse(500, \"Internal server error\", ex.getMessage()));\n")
	eh.WriteString("    }\n")
	eh.WriteString("}\n")

	return os.WriteFile(filepath.Join(ctrlDir, "GlobalExceptionHandler.java"), []byte(eh.String()), 0644)
}
