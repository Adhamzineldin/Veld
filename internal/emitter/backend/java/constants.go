package java

// constants.go — emits typed constant classes into constants/ directory.
// Each ConstantGroup becomes a final class with static final fields.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
)

const javaPackageConstants = "maayn.veld.generated.constants"

func (e *JavaEmitter) emitConstants(a ast.AST, outDir string) error {
	if len(a.Constants) == 0 {
		return nil
	}
	dir := pkgToDir(outDir, javaPackageConstants)
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	for _, cg := range a.Constants {
		var sb strings.Builder
		sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
		sb.WriteString(fmt.Sprintf("package %s;\n\n", javaPackageConstants))

		// Collect needed imports
		imports := make(map[string]bool)
		for _, f := range cg.Fields {
			for _, imp := range javaConstImports(f.Type) {
				imports[imp] = true
			}
		}
		for imp := range imports {
			sb.WriteString(fmt.Sprintf("import %s;\n", imp))
		}
		if len(imports) > 0 {
			sb.WriteString("\n")
		}

		if cg.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", cg.Description))
		}
		sb.WriteString(fmt.Sprintf("public final class %s {\n", cg.Name))
		sb.WriteString(fmt.Sprintf("    private %s() {} // Prevent instantiation\n\n", cg.Name))

		for _, f := range cg.Fields {
			jType := javaConstType(f.Type)
			jVal := javaConstValue(f)
			sb.WriteString(fmt.Sprintf("    public static final %s %s = %s;\n", jType, f.Name, jVal))
		}

		sb.WriteString("}\n")
		if err := os.WriteFile(filepath.Join(dir, cg.Name+".java"), []byte(sb.String()), 0644); err != nil {
			return err
		}
	}
	return nil
}

func javaConstType(veldType string) string {
	switch veldType {
	case "string", "uuid", "date", "datetime":
		return "String"
	case "int":
		return "long"
	case "float":
		return "double"
	case "decimal":
		return "java.math.BigDecimal"
	case "bool":
		return "boolean"
	default:
		return "String"
	}
}

func javaConstImports(veldType string) []string {
	switch veldType {
	case "decimal":
		return []string{"java.math.BigDecimal"}
	default:
		return nil
	}
}

func javaConstValue(f ast.ConstantField) string {
	switch f.Type {
	case "string", "date", "datetime":
		return fmt.Sprintf("%q", f.Value)
	case "uuid":
		return fmt.Sprintf("%q", f.Value)
	case "int":
		return f.Value + "L"
	case "float":
		return f.Value
	case "decimal":
		return fmt.Sprintf("new java.math.BigDecimal(%q)", f.Value)
	case "bool":
		return f.Value
	default:
		return fmt.Sprintf("%q", f.Value)
	}
}
