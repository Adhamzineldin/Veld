func minimalAST() ast.AST {
	return ast.AST{
		Models: []ast.Model{
			{
				Name: "LoginInput",
				Fields: []ast.Field{
					{Name: "email", Type: "string"},
					{Name: "password", Type: "string"},
				},
			},
			{
				Name: "User",
				Fields: []ast.Field{
					{Name: "id", Type: "uuid"},
					{Name: "email", Type: "string"},
					{Name: "role", Type: "Role"},
					{Name: "tags", Type: "string", IsArray: true},
					{Name: "bio", Type: "string", Optional: true},
				},
			},
		},
		Enums: []ast.Enum{
			{Name: "Role", Values: []string{"admin", "user", "guest"}},
		},
		Modules: []ast.Module{
			{
				Name:   "Auth",
				Prefix: "/api",
				Actions: []ast.Action{
					{
						Name:   "Login",
						Method: "POST",
						Path:   "/auth/login",
						Input:  "LoginInput",
						Output: "User",
					},
					{
						Name:   "Me",
						Method: "GET",
						Path:   "/auth/me",
						Output: "User",
					},
					{
						Name:   "Logout",
						Method: "DELETE",
						Path:   "/auth/logout",
					},
				},
			},
		},
	}
}

// javaPath returns the expected filesystem path for a generated Java file,
// matching Maven standard layout: src/main/java/maayn/veld/generated/<sub>/<file>
func javaPath(outDir, sub, file string) string {
	return filepath.Join(outDir, "src", "main", "java", "maayn", "veld", "generated", sub, file)
}

func TestJavaEmitterEmit(t *testing.T) {
	e := java.New()
	outDir := t.TempDir()

	if err := e.Emit(minimalAST(), outDir, emitter.EmitOptions{BackendFramework: "spring"}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	// Models are now per-module: models/{group}/{Type}.java
	// Auth module uses LoginInput, User, and Role (via User.role field) → models/auth/
	expectedFiles := []string{
		filepath.Join(outDir, "src", "main", "java", "maayn", "veld", "generated", "models", "auth", "LoginInput.java"),
		filepath.Join(outDir, "src", "main", "java", "maayn", "veld", "generated", "models", "auth", "User.java"),
		filepath.Join(outDir, "src", "main", "java", "maayn", "veld", "generated", "models", "auth", "Role.java"),
		javaPath(outDir, "services", "IAuthService.java"),
		javaPath(outDir, "controllers", "AuthController.java"),
	}
	for _, f := range expectedFiles {
		if _, err := os.Stat(f); os.IsNotExist(err) {
			t.Errorf("expected file %s to exist", f)
		}
	}
	// Spring no longer emits pom.xml — the user project owns it.
	if _, err := os.Stat(filepath.Join(outDir, "pom.xml")); !os.IsNotExist(err) {
		t.Error("Spring emitter must not generate pom.xml in outDir")
	}
}

