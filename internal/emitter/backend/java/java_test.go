func TestJavaEmitterPathParams(t *testing.T) {
	e := java.New()
	outDir := t.TempDir()

	a := ast.AST{
		Models: []ast.Model{
			{Name: "Item", Fields: []ast.Field{{Name: "id", Type: "string"}}},
		},
		Modules: []ast.Module{
			{
				Name: "Items",
				Actions: []ast.Action{
					{Name: "GetById", Method: "GET", Path: "/items/:id", Output: "Item"},
				},
			},
		},
	}

	if err := e.Emit(a, outDir, emitter.EmitOptions{BackendFramework: "spring"}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, _ := os.ReadFile(javaPath(outDir, "controllers", "ItemsController.java"))
	content := string(data)

	if !strings.Contains(content, `@PathVariable("id") String id`) {
		t.Errorf("controller should extract path param: got:\n%s", content)
	}
}

func TestJavaEmitterSummary(t *testing.T) {
	e := java.New()
	lines := e.Summary([]string{"Auth", "Users"})

	if len(lines) == 0 {
		t.Fatal("Summary() returned no lines")
	}
	dirs := make(map[string]bool)
	for _, l := range lines {
		dirs[l.Dir] = true
	}
	const srcRoot = "src/main/java/maayn/veld/generated/"
	for _, want := range []string{srcRoot + "models/", srcRoot + "services/", srcRoot + "controllers/", "./"} {
		if !dirs[want] {
			t.Errorf("Summary() missing dir %q", want)
		}
	}
}

