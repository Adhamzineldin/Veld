package java

// validation.go — emits a shared ValidationHelper.java utility class.
// Validation framework imports are delegated to the FrameworkStrategy.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	jstrategy "github.com/Adhamzineldin/Veld/internal/emitter/backend/java/strategy"
)

// emitValidationHelper writes models/ValidationHelper.java.
func (e *JavaEmitter) emitValidationHelper(strat jstrategy.FrameworkStrategy, outDir string) error {
	dir := filepath.Join(outDir, "models")

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", javaPackageModels))
	for _, imp := range strat.ValidationImports() {
		sb.WriteString(fmt.Sprintf("import %s;\n", imp))
	}
	sb.WriteString("\n")

	sb.WriteString("/**\n * Shared validation utility for all Veld-generated models.\n */\n")
	sb.WriteString("public final class ValidationHelper {\n\n")
	sb.WriteString("    private static final ValidatorFactory factory = Validation.buildDefaultValidatorFactory();\n")
	sb.WriteString("    private static final Validator validator = factory.getValidator();\n\n")
	sb.WriteString("    private ValidationHelper() {}\n\n")
	sb.WriteString("    public record ValidationError(String field, String message) {}\n\n")
	sb.WriteString("    public static <T> List<ValidationError> validate(T object) {\n")
	sb.WriteString("        Set<ConstraintViolation<T>> violations = validator.validate(object);\n")
	sb.WriteString("        return violations.stream()\n")
	sb.WriteString("            .map(v -> new ValidationError(\n")
	sb.WriteString("                v.getPropertyPath().toString(),\n")
	sb.WriteString("                v.getMessage()))\n")
	sb.WriteString("            .collect(Collectors.toList());\n")
	sb.WriteString("    }\n\n")
	sb.WriteString("    public static <T> void validateOrThrow(T object) {\n")
	sb.WriteString("        List<ValidationError> errors = validate(object);\n")
	sb.WriteString("        if (!errors.isEmpty()) {\n")
	sb.WriteString("            String msg = errors.stream()\n")
	sb.WriteString("                .map(err -> err.field() + \": \" + err.message())\n")
	sb.WriteString("                .collect(Collectors.joining(\"; \"));\n")
	sb.WriteString("            throw new IllegalArgumentException(\"Validation failed: \" + msg);\n")
	sb.WriteString("        }\n")
	sb.WriteString("    }\n")
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, "ValidationHelper.java"), []byte(sb.String()), 0644)
}

// javaValidationAnnotations returns Jakarta Bean Validation annotations for a field.
func javaValidationAnnotations(f ast.Field) []string {
	var annotations []string
	if !f.Optional {
		annotations = append(annotations, "    @jakarta.validation.constraints.NotNull")
	}
	if f.Type == "string" && !f.IsArray && !f.IsMap && !f.Optional {
		annotations = append(annotations, "    @jakarta.validation.constraints.NotBlank")
	}
	if strings.Contains(strings.ToLower(f.Name), "email") {
		annotations = append(annotations, "    @jakarta.validation.constraints.Email")
	}
	return annotations
}
