package java

// sdk.go — service SDK generation for Java backend.
// Generates typed HTTP clients using java.net.http.HttpClient (Java 11+).

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/sdkhelpers"
)

func (e *JavaEmitter) EmitServiceSdk(consumed []emitter.ConsumedServiceInfo, outDir string, opts emitter.EmitOptions) error {
	if opts.DryRun || len(consumed) == 0 {
		return nil
	}
	for _, c := range consumed {
		if err := emitJavaSdk(c, outDir); err != nil {
			return fmt.Errorf("service sdk %s: %w", c.Name, err)
		}
	}
	return nil
}

func emitJavaSdk(consumed emitter.ConsumedServiceInfo, outDir string) error {
	name := sdkhelpers.ServiceFileName(consumed.Name)
	dir := filepath.Join(outDir, "src", "main", "java", "maayn", "veld", "generated", "sdk", name)
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}
	if err := emitJavaSdkTypes(consumed, dir, name); err != nil {
		return err
	}
	return emitJavaSdkClient(consumed, dir, name)
}

func emitJavaSdkTypes(consumed emitter.ConsumedServiceInfo, dir, pkg string) error {
	a := consumed.AST
	usedModels := make(map[string]bool)
	usedEnums := make(map[string]bool)
	for _, mod := range a.Modules {
		for k, v := range emitter.CollectTransitiveModels(a, mod) {
			if v {
				usedModels[k] = true
			}
		}
		for k, v := range emitter.CollectUsedEnums(a, mod) {
			if v {
				usedEnums[k] = true
			}
		}
	}

	for _, en := range a.Enums {
		if !usedEnums[en.Name] {
			continue
		}
		var sb strings.Builder
		sb.WriteString("// Code generated by Veld — DO NOT EDIT.\n")
		sb.WriteString(fmt.Sprintf("package maayn.veld.generated.sdk.%s;\n\n", pkg))
		sb.WriteString(fmt.Sprintf("public enum %s {\n", en.Name))
		for i, v := range en.Values {
			comma := ","
			if i == len(en.Values)-1 {
				comma = ";"
			}
			sb.WriteString(fmt.Sprintf("    %s(%q)%s\n", strings.ToUpper(v), v, comma))
		}
		sb.WriteString("\n    private final String value;\n")
		sb.WriteString(fmt.Sprintf("    %s(String value) { this.value = value; }\n", en.Name))
		sb.WriteString("    public String getValue() { return value; }\n}\n")
		if err := os.WriteFile(filepath.Join(dir, en.Name+".java"), []byte(sb.String()), 0644); err != nil {
			return err
		}
	}

	for _, m := range a.Models {
		if !usedModels[m.Name] {
			continue
		}
		var sb strings.Builder
		sb.WriteString("// Code generated by Veld — DO NOT EDIT.\n")
		sb.WriteString(fmt.Sprintf("package maayn.veld.generated.sdk.%s;\n\n", pkg))
		sb.WriteString(fmt.Sprintf("public class %s {\n", m.Name))
		for _, f := range m.Fields {
			jType := javaSdkType(f)
			sb.WriteString(fmt.Sprintf("    private %s %s;\n", jType, f.Name))
		}
		sb.WriteString("\n    public " + m.Name + "() {}\n\n")
		for _, f := range m.Fields {
			jType := javaSdkType(f)
			getter := "get" + capitalize(f.Name)
			setter := "set" + capitalize(f.Name)
			sb.WriteString(fmt.Sprintf("    public %s %s() { return this.%s; }\n", jType, getter, f.Name))
			sb.WriteString(fmt.Sprintf("    public void %s(%s %s) { this.%s = %s; }\n", setter, jType, f.Name, f.Name, f.Name))
		}
		sb.WriteString("}\n")
		if err := os.WriteFile(filepath.Join(dir, m.Name+".java"), []byte(sb.String()), 0644); err != nil {
			return err
		}
	}
	return nil
}

func emitJavaSdkClient(consumed emitter.ConsumedServiceInfo, dir, pkg string) error {
	var sb strings.Builder
	a := consumed.AST
	envVar := sdkhelpers.EnvVarName(consumed.Name)
	className := sdkhelpers.ServiceClassName(consumed.Name)

	sb.WriteString("// Code generated by Veld — DO NOT EDIT.\n")
	sb.WriteString(fmt.Sprintf("package maayn.veld.generated.sdk.%s;\n\n", pkg))
	sb.WriteString("import java.net.URI;\nimport java.net.http.HttpClient;\nimport java.net.http.HttpRequest;\nimport java.net.http.HttpResponse;\n")
	sb.WriteString("import java.util.Map;\nimport java.util.HashMap;\n\n")

	sb.WriteString(fmt.Sprintf("public class %s {\n", className))
	sb.WriteString("    private final String base;\n    private final HttpClient client;\n    private final Map<String, String> headers;\n\n")

	sb.WriteString(fmt.Sprintf("    public %s() { this(null, null); }\n\n", className))
	sb.WriteString(fmt.Sprintf("    public %s(String baseUrl, Map<String, String> headers) {\n", className))
	if consumed.BaseUrl != "" {
		sb.WriteString(fmt.Sprintf("        this.base = baseUrl != null ? baseUrl : System.getenv(\"%s\") != null ? System.getenv(\"%s\") : \"%s\";\n", envVar, envVar, consumed.BaseUrl))
	} else {
		sb.WriteString(fmt.Sprintf("        String resolved = baseUrl != null ? baseUrl : System.getenv(\"%s\");\n", envVar))
		sb.WriteString(fmt.Sprintf("        if (resolved == null) throw new IllegalArgumentException(\"No base URL for %s — pass baseUrl or set %s\");\n", consumed.Name, envVar))
		sb.WriteString("        this.base = resolved;\n")
	}
	sb.WriteString("        this.client = HttpClient.newHttpClient();\n        this.headers = headers != null ? headers : new HashMap<>();\n    }\n\n")

	sb.WriteString("    private String request(String method, String path, String body) throws Exception {\n")
	sb.WriteString("        HttpRequest.Builder builder = HttpRequest.newBuilder(URI.create(this.base + path))\n")
	sb.WriteString("            .header(\"Content-Type\", \"application/json\");\n")
	sb.WriteString("        this.headers.forEach(builder::header);\n")
	sb.WriteString("        if (body != null) { builder.method(method, HttpRequest.BodyPublishers.ofString(body)); }\n")
	sb.WriteString("        else { builder.method(method, HttpRequest.BodyPublishers.noBody()); }\n")
	sb.WriteString("        HttpResponse<String> res = client.send(builder.build(), HttpResponse.BodyHandlers.ofString());\n")
	sb.WriteString("        if (res.statusCode() >= 400) throw new RuntimeException(\"HTTP \" + res.statusCode() + \": \" + res.body());\n")
	sb.WriteString("        return res.body();\n    }\n")

	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			if strings.ToUpper(act.Method) == "WS" {
				continue
			}
			sb.WriteString("\n")
			writeJavaSdkMethod(&sb, mod, act)
		}
	}
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, className+".java"), []byte(sb.String()), 0644)
}

func writeJavaSdkMethod(sb *strings.Builder, mod ast.Module, act ast.Action) {
	routePath := mod.Prefix + act.Path
	method := strings.ToUpper(act.Method)
	methodName := emitter.ToCamelCase(act.Name)
	pathParams := emitter.ExtractPathParams(routePath)

	retType := "void"
	if act.Output != "" {
		retType = "String"
	} // returns raw JSON; users deserialize

	var params []string
	for _, p := range pathParams {
		params = append(params, "String "+p)
	}
	if act.Input != "" {
		params = append(params, "String body")
	}

	sb.WriteString(fmt.Sprintf("    /** %s %s */\n", method, routePath))
	sb.WriteString(fmt.Sprintf("    public %s %s(%s) throws Exception {\n", retType, methodName, strings.Join(params, ", ")))

	urlExpr := javaSdkBuildUrl(routePath, pathParams)
	bodyArg := "null"
	if act.Input != "" {
		bodyArg = "body"
	}

	if act.Output != "" {
		sb.WriteString(fmt.Sprintf("        return request(%q, %s, %s);\n    }\n", method, urlExpr, bodyArg))
	} else {
		sb.WriteString(fmt.Sprintf("        request(%q, %s, %s);\n    }\n", method, urlExpr, bodyArg))
	}
}

func javaSdkBuildUrl(routePath string, pathParams []string) string {
	if len(pathParams) == 0 {
		return fmt.Sprintf("%q", routePath)
	}
	expr := "\"" + routePath + "\""
	for _, p := range pathParams {
		expr = strings.Replace(expr, ":"+p, "\" + "+p+" + \"", 1)
	}
	return strings.ReplaceAll(expr, " + \"\"", "")
}

func javaSdkType(f ast.Field) string {
	base := javaSdkScalar(f.Type)
	if f.IsArray {
		return "java.util.List<" + base + ">"
	}
	if f.IsMap {
		return "java.util.Map<String, " + javaSdkScalar(f.MapValueType) + ">"
	}
	return base
}

func javaSdkScalar(t string) string {
	switch t {
	case "string", "uuid", "date", "datetime":
		return "String"
	case "int":
		return "Long"
	case "float":
		return "Double"
	case "decimal":
		return "java.math.BigDecimal"
	case "bool":
		return "Boolean"
	case "any", "json":
		return "Object"
	default:
		return t
	}
}
