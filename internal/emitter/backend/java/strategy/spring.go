// rePathParam matches Veld-style :param segments and converts them to {param} for Spring.
var rePathParam = regexp.MustCompile(`:([a-zA-Z_][a-zA-Z0-9_]*)`)

func toSpringPath(path string) string {
	return rePathParam.ReplaceAllString(path, "{$1}")
}

// SpringStrategy implements FrameworkStrategy for Spring Boot 3.x / 4.x (Java 17+, Jakarta EE 10+).
type SpringStrategy struct{}

func (s *SpringStrategy) ControllerAnnotations(mod ast.Module) []string {
	annotations := []string{"@RestController"}
	if mod.Prefix != "" {
		annotations = append(annotations, fmt.Sprintf("@RequestMapping(%q)", mod.Prefix))
	}
	return annotations
}

func (s *SpringStrategy) RouteAnnotation(method, path string) string {
	springPath := toSpringPath(path) // convert :id → {id}
	// Spring Boot 3+ disabled trailing-slash matching. A path of "/" on a method
	// whose controller already has @RequestMapping("/prefix") would resolve to
	// "/prefix/" — which no longer matches "/prefix". Emit with no path argument
	// so the method maps to the prefix itself.
	bare := springPath == "" || springPath == "/"
	ann := func(name, p string) string {
		if bare {
			return "@" + name
		}
		return fmt.Sprintf("@%s(%q)", name, p)
	}
	switch strings.ToUpper(method) {
	case "GET":
		return ann("GetMapping", springPath)
	case "POST":
		return ann("PostMapping", springPath)
	case "PUT":
		return ann("PutMapping", springPath)
	case "DELETE":
		return ann("DeleteMapping", springPath)
	case "PATCH":
		return ann("PatchMapping", springPath)
	default:
		if bare {
			return fmt.Sprintf("@RequestMapping(method = RequestMethod.%s)", strings.ToUpper(method))
		}
		return fmt.Sprintf("@RequestMapping(method = RequestMethod.%s, value = %q)", strings.ToUpper(method), springPath)
	}
}

func (s *SpringStrategy) InputParamAnnotation() string { return "@RequestBody" }
func (s *SpringStrategy) QueryParamAnnotation() string { return "@ModelAttribute" }
func (s *SpringStrategy) QueryParamType() string       { return "" } // unused: type comes from act.Query
func (s *SpringStrategy) ResponseWrapper() string      { return "ResponseEntity<?>" }

func (s *SpringStrategy) PathParamAnnotation(name string) string {
	return fmt.Sprintf("@PathVariable(%q)", name)
}

func (s *SpringStrategy) OkResponse(expr string) string {
	return fmt.Sprintf("return ResponseEntity.ok(%s);", expr)
}

func (s *SpringStrategy) CreatedResponse(expr string) string {
	return fmt.Sprintf("return ResponseEntity.status(HttpStatus.CREATED).body(%s);", expr)
}

func (s *SpringStrategy) NoContentResponse() string {
	return "return ResponseEntity.noContent().build();"
}

func (s *SpringStrategy) ApiExceptionCatch(errVar string) string {
	return fmt.Sprintf(
		"return ResponseEntity.status(%s.getStatus()).body(%s.toErrorResponse());",
		errVar, errVar,
	)
}

func (s *SpringStrategy) ErrorResponse(errVar string) string {
	return fmt.Sprintf(
		"return ResponseEntity.status(HttpStatus.INTERNAL_SERVER_ERROR).body(ApiException.toErrorResponse(%s));",
		errVar,
	)
}

