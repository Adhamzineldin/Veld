func (s *SpringStrategy) GlobalExceptionHandlerSource(ctrlPkg, errorsPkg string) string {
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", ctrlPkg))
	sb.WriteString(fmt.Sprintf("import %s.ApiErrorResponse;\n", errorsPkg))
	sb.WriteString(fmt.Sprintf("import %s.ApiException;\n", errorsPkg))
	sb.WriteString("import org.springframework.http.HttpStatus;\n")
	sb.WriteString("import org.springframework.http.ResponseEntity;\n")
	sb.WriteString("import org.springframework.web.bind.annotation.ControllerAdvice;\n")
	sb.WriteString("import org.springframework.web.bind.annotation.ExceptionHandler;\n\n")
	sb.WriteString("@ControllerAdvice\n")
	sb.WriteString("public class GlobalExceptionHandler {\n\n")
	sb.WriteString("    @ExceptionHandler(ApiException.class)\n")
	sb.WriteString("    public ResponseEntity<ApiErrorResponse> handleApiException(ApiException ex) {\n")
	sb.WriteString("        return ResponseEntity.status(ex.getStatus())\n")
	sb.WriteString("            .body(ex.toErrorResponse());\n")
	sb.WriteString("    }\n\n")
	sb.WriteString("    @ExceptionHandler(Exception.class)\n")
	sb.WriteString("    public ResponseEntity<ApiErrorResponse> handleAll(Exception ex) {\n")
	sb.WriteString("        return ResponseEntity.status(HttpStatus.INTERNAL_SERVER_ERROR)\n")
	sb.WriteString("            .body(ApiException.toErrorResponse(ex));\n")
	sb.WriteString("    }\n")
	sb.WriteString("}\n")
	return sb.String()
}

func (s *SpringStrategy) InterceptorSource(label, middlewarePkg string) string {
	interceptorName := label + "Interceptor"
	ifaceName := "I" + label + "Middleware"
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", middlewarePkg))
	sb.WriteString("import jakarta.servlet.http.HttpServletRequest;\n")
	sb.WriteString("import jakarta.servlet.http.HttpServletResponse;\n")
	sb.WriteString("import org.springframework.stereotype.Component;\n")
	sb.WriteString("import org.springframework.web.servlet.HandlerInterceptor;\n\n")
	sb.WriteString(fmt.Sprintf("/** Spring HandlerInterceptor delegating to {@link %s}.\n", ifaceName))
	sb.WriteString(" * Register a @Bean implementing " + ifaceName + " and this interceptor activates. */\n")
	sb.WriteString("@Component\n")
	sb.WriteString(fmt.Sprintf("public class %s implements HandlerInterceptor {\n\n", interceptorName))
	sb.WriteString(fmt.Sprintf("    private final %s middleware;\n\n", ifaceName))
	sb.WriteString(fmt.Sprintf("    public %s(%s middleware) {\n", interceptorName, ifaceName))
	sb.WriteString("        this.middleware = middleware;\n    }\n\n")
	sb.WriteString("    @Override\n")
	sb.WriteString("    public boolean preHandle(HttpServletRequest request, HttpServletResponse response, Object handler) throws Exception {\n")
	sb.WriteString("        return middleware.handle(request, response);\n    }\n}\n")
	return sb.String()
}

func (s *SpringStrategy) WebMvcConfigurerSource(pathMap map[string][]string, controllerPkg, middlewarePkg string) string {
	if len(pathMap) == 0 {
		return ""
	}
	labels := make([]string, 0, len(pathMap))
	for l := range pathMap {
		labels = append(labels, l)
	}
	sort.Strings(labels)

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", controllerPkg))
	for _, label := range labels {
		sb.WriteString(fmt.Sprintf("import %s.%sInterceptor;\n", middlewarePkg, label))
	}
	sb.WriteString("import org.springframework.beans.factory.annotation.Autowired;\n")
	sb.WriteString("import org.springframework.context.annotation.Configuration;\n")
	sb.WriteString("import org.springframework.web.servlet.config.annotation.InterceptorRegistry;\n")
	sb.WriteString("import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;\n\n")
	sb.WriteString("@Configuration\npublic class VeldWebMvcConfigurer implements WebMvcConfigurer {\n\n")
	for _, label := range labels {
		field := strings.ToLower(label[:1]) + label[1:] + "Interceptor"
		sb.WriteString(fmt.Sprintf("    @Autowired\n    private %sInterceptor %s;\n\n", label, field))
	}
	sb.WriteString("    @Override\n    public void addInterceptors(InterceptorRegistry registry) {\n")
	for _, label := range labels {
		field := strings.ToLower(label[:1]) + label[1:] + "Interceptor"
		paths := pathMap[label]
		quoted := make([]string, len(paths))
		for i, p := range paths {
			quoted[i] = fmt.Sprintf("%q", p)
		}
		sb.WriteString(fmt.Sprintf("        registry.addInterceptor(%s)\n            .addPathPatterns(%s);\n", field, strings.Join(quoted, ", ")))
	}
	sb.WriteString("    }\n}\n")
	return sb.String()
}

func (s *SpringStrategy) AutoConfigSource(basePkg string) string {
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("package %s;\n\n", basePkg))
	sb.WriteString("import org.springframework.context.annotation.ComponentScan;\n")
	sb.WriteString("import org.springframework.context.annotation.Configuration;\n\n")
	sb.WriteString("/**\n * Veld auto-configuration — scans all generated controllers and interceptors.\n")
	sb.WriteString(" * Activated via src/main/resources/META-INF/spring/\n")
	sb.WriteString(" *   org.springframework.boot.autoconfigure.AutoConfiguration.imports\n")
	sb.WriteString(" * If controllers are still not found, add to your main class:\n")
	sb.WriteString(" *   @Import(maayn.veld.generated.VeldAutoConfiguration.class)\n */\n")
	sb.WriteString("@Configuration\n")
	sb.WriteString(fmt.Sprintf("@ComponentScan(%q)\n", basePkg))
	sb.WriteString("public class VeldAutoConfiguration {}\n")
	return sb.String()
}

// BuildFile returns ("", "") — Spring projects own their pom.xml/build.gradle.
// Use `veld setup` to add build-helper-maven-plugin or a Gradle sourceSet pointing
// at generated/src/main/java instead.
func (s *SpringStrategy) BuildFile() (string, string) {
	return "", ""
}

func (s *SpringStrategy) WSControllerMethod(actionName, routePath, emitType, streamType string) string {
	if emitType == "" {
		emitType = "String"
	}
	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("\n    /** WebSocket handler for %s */\n", routePath))
	sb.WriteString(fmt.Sprintf("    @MessageMapping(%q)\n", routePath))
	if streamType != "" {
		sb.WriteString(fmt.Sprintf("    @SendTo(%q)\n", routePath+"/stream"))
	}
	sb.WriteString(fmt.Sprintf("    public void handle%s(@Payload %s msg, SimpMessageHeaderAccessor headerAccessor) {\n", actionName, emitType))
	sb.WriteString(fmt.Sprintf("        // implement: service.on%sMessage(headerAccessor.getSessionId(), msg);\n", actionName))
	sb.WriteString("    }\n")
	return sb.String()
}

