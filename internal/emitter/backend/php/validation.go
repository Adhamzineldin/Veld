package php

// validation.go — emits a FormRequest base class with Laravel validation rules.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
)

// emitValidationRules writes app/Http/Requests/VeldFormRequest.php and
// per-model request classes with validation rules.
func (e *PhpEmitter) emitValidationRules(a ast.AST, outDir string) error {
	dir := filepath.Join(outDir, "app", "Http", "Requests")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	// Base form request
	var base strings.Builder
	base.WriteString("<?php\n\n")
	base.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	base.WriteString("declare(strict_types=1);\n\n")
	base.WriteString("namespace App\\Http\\Requests;\n\n")
	base.WriteString("use Illuminate\\Foundation\\Http\\FormRequest;\n\n")
	base.WriteString("abstract class VeldFormRequest extends FormRequest\n{\n")
	base.WriteString("    public function authorize(): bool\n")
	base.WriteString("    {\n")
	base.WriteString("        return true; // Override in subclass for authorization logic\n")
	base.WriteString("    }\n")
	base.WriteString("}\n")
	if err := os.WriteFile(filepath.Join(dir, "VeldFormRequest.php"), []byte(base.String()), 0644); err != nil {
		return err
	}

	// Per-model request classes (only for models used as action inputs)
	inputModels := make(map[string]bool)
	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			if act.Input != "" {
				inputModels[act.Input] = true
			}
		}
	}

	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	for _, m := range a.Models {
		if !inputModels[m.Name] {
			continue
		}
		if err := e.emitFormRequest(m, enumNames, dir); err != nil {
			return fmt.Errorf("form request %s: %w", m.Name, err)
		}
	}

	return nil
}

func (e *PhpEmitter) emitFormRequest(m ast.Model, enumNames map[string]bool, dir string) error {
	className := m.Name + "Request"

	var sb strings.Builder
	sb.WriteString("<?php\n\n")
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	sb.WriteString("declare(strict_types=1);\n\n")
	sb.WriteString("namespace App\\Http\\Requests;\n\n")

	sb.WriteString(fmt.Sprintf("class %s extends VeldFormRequest\n{\n", className))
	sb.WriteString("    /**\n     * @return array<string, mixed>\n     */\n")
	sb.WriteString("    public function rules(): array\n")
	sb.WriteString("    {\n")
	sb.WriteString("        return [\n")

	for _, f := range m.Fields {
		rules := phpValidationRules(f, enumNames)
		sb.WriteString(fmt.Sprintf("            '%s' => '%s',\n", phpSnakeName(f.Name), strings.Join(rules, "|")))
	}

	sb.WriteString("        ];\n")
	sb.WriteString("    }\n")
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, className+".php"), []byte(sb.String()), 0644)
}

// phpValidationRules returns Laravel validation rules for a field.
func phpValidationRules(f ast.Field, enumNames map[string]bool) []string {
	var rules []string

	if f.Optional {
		rules = append(rules, "nullable")
	} else {
		rules = append(rules, "required")
	}

	switch f.Type {
	case "string":
		rules = append(rules, "string")
		if !f.Optional {
			rules = append(rules, "min:1")
		}
	case "int":
		rules = append(rules, "integer")
	case "float":
		rules = append(rules, "numeric")
	case "bool":
		rules = append(rules, "boolean")
	case "date":
		rules = append(rules, "date")
	case "datetime":
		rules = append(rules, "date")
	case "uuid":
		rules = append(rules, "uuid")
	default:
		if enumNames[f.Type] {
			rules = append(rules, "string")
		}
	}

	if f.IsArray {
		rules = append(rules, "array")
	}

	if strings.Contains(strings.ToLower(f.Name), "email") {
		rules = append(rules, "email")
	}
	if strings.HasSuffix(strings.ToLower(f.Name), "url") {
		rules = append(rules, "url")
	}

	return rules
}
