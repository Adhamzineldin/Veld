package php

// middleware.go — emits per-module middleware interfaces for Laravel.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitModuleMiddleware writes app/Middleware/I{Module}Middleware.php with a typed interface.
func (e *PhpEmitter) emitModuleMiddleware(mod ast.Module, outDir string) error {
	allMiddleware := emitter.CollectModuleMiddleware(mod)
	if len(allMiddleware) == 0 {
		return nil
	}

	dir := filepath.Join(outDir, "app", "Middleware")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	interfaceName := "I" + mod.Name + "Middleware"
	var sb strings.Builder
	sb.WriteString("<?php\n\n// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	sb.WriteString("declare(strict_types=1);\n\nnamespace App\\Middleware;\n\n")
	sb.WriteString("use Closure;\n")
	sb.WriteString("use Illuminate\\Http\\Request;\n\n")
	sb.WriteString(fmt.Sprintf("/**\n * Middleware interface for the %s module.\n", mod.Name))
	sb.WriteString(" * Implement this interface and register it with the route registrar.\n */\n")
	sb.WriteString(fmt.Sprintf("interface %s\n{\n", interfaceName))

	for _, mw := range allMiddleware {
		camelMw := emitter.ToCamelCase(mw)
		sb.WriteString(fmt.Sprintf("    /** %s middleware handler. */\n", mw))
		sb.WriteString(fmt.Sprintf("    public function %s(Request $request, Closure $next);\n\n", camelMw))
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, interfaceName+".php"), []byte(sb.String()), 0644)
}
