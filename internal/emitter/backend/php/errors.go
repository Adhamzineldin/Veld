package php

// errors.go — emits ApiException, VeldExceptionHandler, 7 convenience
// exception subclasses, ErrorHelpers, and per-module typed error factories.
// Matches the comprehensive approach used by the TypeScript/Node emitter.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitErrorHandler writes app/Exceptions/ApiException.php, VeldExceptionHandler.php,
// 7 convenience subclasses, and ErrorHelpers.php.
func (e *PhpEmitter) emitErrorHandler(outDir string) error {
	dir := filepath.Join(outDir, "app", "Exceptions")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	// ApiException — base class with helper methods
	var ae strings.Builder
	ae.WriteString("<?php\n\n// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	ae.WriteString("declare(strict_types=1);\n\nnamespace App\\Exceptions;\n\n")
	ae.WriteString("use RuntimeException;\n")
	ae.WriteString("use DateTimeImmutable;\n")
	ae.WriteString("use DateTimeInterface;\n\n")
	ae.WriteString("/**\n")
	ae.WriteString(" * Typed API exception with a machine-readable code and HTTP status.\n")
	ae.WriteString(" * Every Veld-generated error factory returns ApiException.\n")
	ae.WriteString(" *\n")
	ae.WriteString(" * @example throw new ApiException('GET_USER_NOT_FOUND', 404, 'user not found');\n")
	ae.WriteString(" */\n")
	ae.WriteString("class ApiException extends RuntimeException\n{\n")
	ae.WriteString("    public readonly string $timestamp;\n\n")
	ae.WriteString("    public function __construct(\n")
	ae.WriteString("        public readonly string $code,\n")
	ae.WriteString("        public readonly int $status,\n")
	ae.WriteString("        string $message = '',\n")
	ae.WriteString("    ) {\n")
	ae.WriteString("        parent::__construct($message ?: $code);\n")
	ae.WriteString("        $this->timestamp = (new DateTimeImmutable())->format(DateTimeInterface::ATOM);\n")
	ae.WriteString("    }\n\n")
	ae.WriteString("    /** Serialise to a JSON-safe array. */\n")
	ae.WriteString("    public function toArray(): array\n")
	ae.WriteString("    {\n")
	ae.WriteString("        return ['error' => $this->getMessage(), 'code' => $this->code, 'status' => $this->status];\n")
	ae.WriteString("    }\n\n")

	// Helper methods
	ae.WriteString("    /** Check if this exception matches a specific error code. */\n")
	ae.WriteString("    public function isErrorCode(string $errorCode): bool\n")
	ae.WriteString("    {\n")
	ae.WriteString("        return $this->code === $errorCode;\n")
	ae.WriteString("    }\n\n")
	ae.WriteString("    /** Check if this exception has a specific HTTP status. */\n")
	ae.WriteString("    public function isHttpStatus(int $httpStatus): bool\n")
	ae.WriteString("    {\n")
	ae.WriteString("        return $this->status === $httpStatus;\n")
	ae.WriteString("    }\n")
	ae.WriteString("}\n")
	if err := os.WriteFile(filepath.Join(dir, "ApiException.php"), []byte(ae.String()), 0644); err != nil {
		return err
	}

	// ── 7 convenience exception subclasses ───────────────────────────────
	type errDef struct {
		name      string
		status    int
		msg       string
		codeConst string
	}
	defs := []errDef{
		{"NotFoundException", 404, "Not Found", "NOT_FOUND"},
		{"BadRequestException", 400, "Bad Request", "BAD_REQUEST"},
		{"UnauthorizedException", 401, "Unauthorized", "UNAUTHORIZED"},
		{"ForbiddenException", 403, "Forbidden", "FORBIDDEN"},
		{"ConflictException", 409, "Conflict", "CONFLICT"},
		{"ValidationException", 422, "Validation Failed", "VALIDATION_FAILED"},
		{"InternalException", 500, "Internal Server Error", "INTERNAL_SERVER_ERROR"},
	}

	for _, d := range defs {
		var cs strings.Builder
		cs.WriteString("<?php\n\n// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
		cs.WriteString("declare(strict_types=1);\n\nnamespace App\\Exceptions;\n\n")
		cs.WriteString(fmt.Sprintf("/** %d %s — throw from service implementations for quick status responses. */\n", d.status, d.msg))
		cs.WriteString(fmt.Sprintf("class %s extends ApiException\n{\n", d.name))
		cs.WriteString(fmt.Sprintf("    public function __construct(?string $message = null, ?string $code = null)\n"))
		cs.WriteString("    {\n")
		cs.WriteString(fmt.Sprintf("        parent::__construct($code ?? '%s', %d, $message ?? '%s');\n", d.codeConst, d.status, d.msg))
		cs.WriteString("    }\n")
		cs.WriteString("}\n")
		if err := os.WriteFile(filepath.Join(dir, d.name+".php"), []byte(cs.String()), 0644); err != nil {
			return err
		}
	}

	// ErrorHelpers — free-standing helper functions
	var eh strings.Builder
	eh.WriteString("<?php\n\n// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	eh.WriteString("declare(strict_types=1);\n\nnamespace App\\Exceptions;\n\n")
	eh.WriteString("use Throwable;\n\n")
	eh.WriteString("/** Check if a throwable is an ApiException. */\n")
	eh.WriteString("function is_api_exception(Throwable $e): bool\n{\n")
	eh.WriteString("    return $e instanceof ApiException;\n")
	eh.WriteString("}\n\n")
	eh.WriteString("/** Check if a throwable is an ApiException with the given code. */\n")
	eh.WriteString("function is_error_code(Throwable $e, string $code): bool\n{\n")
	eh.WriteString("    return $e instanceof ApiException && $e->code === $code;\n")
	eh.WriteString("}\n\n")
	eh.WriteString("/** Check if a throwable is an ApiException with the given HTTP status. */\n")
	eh.WriteString("function is_http_status(Throwable $e, int $status): bool\n{\n")
	eh.WriteString("    return $e instanceof ApiException && $e->status === $status;\n")
	eh.WriteString("}\n\n")
	eh.WriteString("/**\n")
	eh.WriteString(" * Convert any throwable into a consistent error response array.\n")
	eh.WriteString(" * ApiException instances preserve their code and status;\n")
	eh.WriteString(" * other throwables become 500 Internal Server Error.\n")
	eh.WriteString(" *\n")
	eh.WriteString(" * @return array{error: string, code: string, status: int}\n")
	eh.WriteString(" */\n")
	eh.WriteString("function to_error_response(Throwable $e): array\n{\n")
	eh.WriteString("    if ($e instanceof ApiException) {\n")
	eh.WriteString("        return $e->toArray();\n")
	eh.WriteString("    }\n")
	eh.WriteString("    return ['error' => $e->getMessage(), 'code' => 'INTERNAL_ERROR', 'status' => 500];\n")
	eh.WriteString("}\n\n")
	eh.WriteString("/**\n")
	eh.WriteString(" * Framework-agnostic error handler.\n")
	eh.WriteString(" * Returns a [body_array, status_code] tuple.\n")
	eh.WriteString(" *\n")
	eh.WriteString(" * @return array{0: array{error: string, code: string}, 1: int}\n")
	eh.WriteString(" */\n")
	eh.WriteString("function error_handler(Throwable $e): array\n{\n")
	eh.WriteString("    $resp = to_error_response($e);\n")
	eh.WriteString("    return [['error' => $resp['error'], 'code' => $resp['code']], $resp['status']];\n")
	eh.WriteString("}\n")
	if err := os.WriteFile(filepath.Join(dir, "ErrorHelpers.php"), []byte(eh.String()), 0644); err != nil {
		return err
	}

	// VeldExceptionHandler — Laravel handler using helpers
	var sb strings.Builder
	sb.WriteString("<?php\n\n// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	sb.WriteString("declare(strict_types=1);\n\nnamespace App\\Exceptions;\n\n")
	sb.WriteString("use Illuminate\\Foundation\\Exceptions\\Handler as ExceptionHandler;\n")
	sb.WriteString("use Illuminate\\Http\\JsonResponse;\n")
	sb.WriteString("use Throwable;\n\n")
	sb.WriteString("class VeldExceptionHandler extends ExceptionHandler\n{\n")
	sb.WriteString("    public function render($request, Throwable $e): JsonResponse\n    {\n")
	sb.WriteString("        if ($e instanceof ApiException) {\n")
	sb.WriteString("            return response()->json(\n")
	sb.WriteString("                ['error' => $e->getMessage(), 'code' => $e->code],\n")
	sb.WriteString("                $e->status\n")
	sb.WriteString("            );\n")
	sb.WriteString("        }\n\n")
	sb.WriteString("        return response()->json([\n")
	sb.WriteString("            'error' => 'Internal server error',\n")
	sb.WriteString("            'code' => 'INTERNAL_ERROR',\n")
	sb.WriteString("        ], 500);\n")
	sb.WriteString("    }\n}\n")

	return os.WriteFile(filepath.Join(dir, "VeldExceptionHandler.php"), []byte(sb.String()), 0644)
}

// emitModuleErrors writes app/Errors/{Module}Errors.php with typed error factory methods.
func (e *PhpEmitter) emitModuleErrors(mod ast.Module, outDir string) error {
	if !emitter.HasErrors(mod) {
		return nil
	}

	dir := filepath.Join(outDir, "app", "Errors")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	className := mod.Name + "Errors"
	var sb strings.Builder
	sb.WriteString("<?php\n\n// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	sb.WriteString("declare(strict_types=1);\n\nnamespace App\\Errors;\n\n")
	sb.WriteString("use App\\Exceptions\\ApiException;\n\n")
	sb.WriteString(fmt.Sprintf("final class %s\n{\n", className))

	for _, act := range mod.Actions {
		if len(act.Errors) == 0 {
			continue
		}
		for _, errName := range act.Errors {
			code := emitter.ErrorCode(act.Name, errName)
			status := emitter.ErrorHTTPStatus(errName)
			methodName := emitter.ToCamelCase(act.Name) + errName
			sb.WriteString(fmt.Sprintf("    public static function %s(?string $message = null): ApiException\n", methodName))
			sb.WriteString("    {\n")
			sb.WriteString(fmt.Sprintf("        return new ApiException('%s', %d, $message ?? '%s');\n", code, status, errName))
			sb.WriteString("    }\n\n")
		}
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, className+".php"), []byte(sb.String()), 0644)
}
