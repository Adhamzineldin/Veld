package php

// errors.go — emits a Laravel exception handler for consistent JSON error responses.

import (
	"os"
	"path/filepath"
	"strings"
)

// emitErrorHandler writes app/Exceptions/VeldExceptionHandler.php.
func (e *PhpEmitter) emitErrorHandler(outDir string) error {
	dir := filepath.Join(outDir, "app", "Exceptions")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	var sb strings.Builder
	sb.WriteString("<?php\n\n")
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	sb.WriteString("declare(strict_types=1);\n\n")
	sb.WriteString("namespace App\\Exceptions;\n\n")
	sb.WriteString("use Illuminate\\Foundation\\Exceptions\\Handler as ExceptionHandler;\n")
	sb.WriteString("use Illuminate\\Http\\JsonResponse;\n")
	sb.WriteString("use Illuminate\\Validation\\ValidationException;\n")
	sb.WriteString("use Symfony\\Component\\HttpKernel\\Exception\\HttpException;\n")
	sb.WriteString("use Throwable;\n\n")

	sb.WriteString("class VeldExceptionHandler extends ExceptionHandler\n{\n")

	sb.WriteString("    /**\n     * Render an exception into an HTTP response.\n     */\n")
	sb.WriteString("    public function render($request, Throwable $e): JsonResponse\n")
	sb.WriteString("    {\n")
	sb.WriteString("        if ($e instanceof ValidationException) {\n")
	sb.WriteString("            return response()->json([\n")
	sb.WriteString("                'code' => 422,\n")
	sb.WriteString("                'message' => 'Validation failed',\n")
	sb.WriteString("                'details' => $e->errors(),\n")
	sb.WriteString("            ], 422);\n")
	sb.WriteString("        }\n\n")

	sb.WriteString("        if ($e instanceof HttpException) {\n")
	sb.WriteString("            return response()->json([\n")
	sb.WriteString("                'code' => $e->getStatusCode(),\n")
	sb.WriteString("                'message' => $e->getMessage() ?: 'HTTP error',\n")
	sb.WriteString("                'details' => null,\n")
	sb.WriteString("            ], $e->getStatusCode());\n")
	sb.WriteString("        }\n\n")

	sb.WriteString("        return response()->json([\n")
	sb.WriteString("            'code' => 500,\n")
	sb.WriteString("            'message' => 'Internal server error',\n")
	sb.WriteString("            'details' => config('app.debug') ? $e->getMessage() : null,\n")
	sb.WriteString("        ], 500);\n")
	sb.WriteString("    }\n")
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, "VeldExceptionHandler.php"), []byte(sb.String()), 0644)
}
