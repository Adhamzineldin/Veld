func TestPhpEmitterPathParams(t *testing.T) {
	e := php.New()
	outDir := t.TempDir()

	a := ast.AST{
		Models: []ast.Model{
			{Name: "Item", Fields: []ast.Field{{Name: "id", Type: "string"}}},
		},
		Modules: []ast.Module{
			{
				Name: "Items",
				Actions: []ast.Action{
					{Name: "GetById", Method: "GET", Path: "/items/:id", Output: "Item"},
				},
			},
		},
	}

	if err := e.Emit(a, outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, _ := os.ReadFile(filepath.Join(outDir, "routes", "api.php"))
	routesContent := string(data)
	// Laravel uses {id} syntax.
	if !strings.Contains(routesContent, "'/items/{id}'") {
		t.Errorf("api.php should use {id} Laravel syntax:\n%s", routesContent)
	}

	data2, _ := os.ReadFile(filepath.Join(outDir, "app", "Http", "Controllers", "ItemsController.php"))
	ctrlContent := string(data2)
	if !strings.Contains(ctrlContent, "string $id") {
		t.Errorf("controller should have string $id param:\n%s", ctrlContent)
	}
}

func TestPhpEmitterSummary(t *testing.T) {
	e := php.New()
	lines := e.Summary([]string{"Auth", "Users"})

	if len(lines) == 0 {
		t.Fatal("Summary() returned no lines")
	}
	dirs := make(map[string]bool)
	for _, l := range lines {
		dirs[l.Dir] = true
	}
	for _, want := range []string{"app/Models/", "app/Services/", "app/Http/Controllers/", "routes/", "./"} {
		if !dirs[want] {
			t.Errorf("Summary() missing dir %q", want)
		}
	}
}

