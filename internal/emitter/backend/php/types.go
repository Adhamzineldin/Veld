package php

// types.go — emits app/Models/*.php: one PHP 8.1 readonly class per Veld model,
// one PHP 8.1 backed enum per Veld enum.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
)

func (e *PhpEmitter) emitModels(a ast.AST, outDir string) error {
	dir := filepath.Join(outDir, "app", "Models")
	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	for _, en := range a.Enums {
		if err := e.emitEnum(en, dir); err != nil {
			return fmt.Errorf("enum %s: %w", en.Name, err)
		}
	}
	for _, m := range a.Models {
		if err := e.emitClass(m, enumNames, dir); err != nil {
			return fmt.Errorf("model %s: %w", m.Name, err)
		}
	}
	return nil
}

// emitClass writes a PHP 8.1+ readonly class as a DTO.
func (e *PhpEmitter) emitClass(m ast.Model, enumNames map[string]bool, dir string) error {
	var sb strings.Builder
	sb.WriteString("<?php\n\n")
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n\n")
	sb.WriteString("declare(strict_types=1);\n\n")
	sb.WriteString("namespace App\\Models;\n\n")

	if m.Description != "" {
		sb.WriteString(fmt.Sprintf("/** %s */\n", m.Description))
	}

	parent := ""
	if m.Extends != "" {
		parent = " extends " + m.Extends
	}
	sb.WriteString(fmt.Sprintf("class %s%s\n{\n", m.Name, parent))
	sb.WriteString("    public function __construct(\n")

	for i, f := range m.Fields {
		phpType := phpFieldType(f, enumNames)
		propName := "$" + phpSnakeName(f.Name)

		// Optional fields get a default of null.
		defaultVal := ""
		if f.Optional {
			phpType = "?" + phpType
			defaultVal = " = null"
		}

		comma := ","
		if i == len(m.Fields)-1 {
			comma = ","
		}
		sb.WriteString(fmt.Sprintf("        public readonly %s %s%s%s\n", phpType, propName, defaultVal, comma))
	}

	sb.WriteString("    ) {}\n}\n")
	return os.WriteFile(filepath.Join(dir, m.Name+".php"), []byte(sb.String()), 0644)
}

// emitEnum writes a PHP 8.1+ backed enum (string).
func (e *PhpEmitter) emitEnum(en ast.Enum, dir string) error {
	var sb strings.Builder
	sb.WriteString("<?php\n\n")
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n\n")
	sb.WriteString("declare(strict_types=1);\n\n")
	sb.WriteString("namespace App\\Models;\n\n")

	if en.Description != "" {
		sb.WriteString(fmt.Sprintf("/** %s */\n", en.Description))
	}
	sb.WriteString(fmt.Sprintf("enum %s: string\n{\n", en.Name))
	for _, v := range en.Values {
		caseName := phpPascalName(v)
		sb.WriteString(fmt.Sprintf("    case %s = '%s';\n", caseName, v))
	}
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, en.Name+".php"), []byte(sb.String()), 0644)
}

// phpFieldType maps an AST field to a PHP type hint.
func phpFieldType(f ast.Field, enumNames map[string]bool) string {
	if f.IsMap || f.IsArray {
		return "array"
	}
	return veldScalarToPhp(f.Type, enumNames)
}

// veldScalarToPhp maps a Veld scalar to a PHP type hint.
func veldScalarToPhp(t string, _ map[string]bool) string {
	switch t {
	case "string", "date", "datetime", "uuid":
		return "string"
	case "int":
		return "int"
	case "float":
		return "float"
	case "bool":
		return "bool"
	case "any", "json":
		return "mixed"
	default:
		return t // custom model/enum name
	}
}

// phpSnakeName converts a name to snake_case for PHP property names.
func phpSnakeName(name string) string {
	var result strings.Builder
	var prev rune
	for i, r := range name {
		isUpper := r >= 'A' && r <= 'Z'
		if isUpper && i > 0 && prev != '_' {
			prevUpper := prev >= 'A' && prev <= 'Z'
			if !prevUpper {
				result.WriteRune('_')
			}
		}
		result.WriteRune(r | 32)
		prev = r
	}
	out := result.String()
	for strings.Contains(out, "__") {
		out = strings.ReplaceAll(out, "__", "_")
	}
	return strings.Trim(out, "_")
}

// phpPascalName converts a name to PascalCase for PHP class/enum names.
func phpPascalName(name string) string {
	parts := strings.FieldsFunc(phpSnakeName(name), func(r rune) bool { return r == '_' })
	var sb strings.Builder
	for _, p := range parts {
		if len(p) > 0 {
			sb.WriteString(strings.ToUpper(p[:1]) + p[1:])
		}
	}
	return sb.String()
}
