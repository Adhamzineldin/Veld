package php

// routes.go — emits app/Http/Controllers/{Module}Controller.php and
// routes/api.php with all route registrations. Status codes: 201 for POST,
// 204 for DELETE with no output, 200 for everything else.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/veld-dev/veld/internal/ast"
	"github.com/veld-dev/veld/internal/emitter"
)

func (e *PhpEmitter) emitController(a ast.AST, mod ast.Module, outDir string) error {
	dir := filepath.Join(outDir, "app", "Http", "Controllers")

	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	modClass := capitalize(mod.Name)

	seen := make(map[string]bool)
	var modelUses []string
	for _, act := range mod.Actions {
		if act.Input != "" && !seen[act.Input] {
			seen[act.Input] = true
			modelUses = append(modelUses, act.Input)
		}
	}

	var sb strings.Builder
	sb.WriteString("<?php\n\n")
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n\n")
	sb.WriteString("declare(strict_types=1);\n\n")
	sb.WriteString("namespace App\\Http\\Controllers;\n\n")
	sb.WriteString("use Illuminate\\Http\\Request;\n")
	sb.WriteString("use Illuminate\\Http\\JsonResponse;\n")
	sb.WriteString(fmt.Sprintf("use App\\Services\\I%sService;\n", modClass))
	for _, m := range modelUses {
		sb.WriteString(fmt.Sprintf("use App\\Models\\%s;\n", m))
	}
	sb.WriteString("\n")

	if mod.Description != "" {
		sb.WriteString(fmt.Sprintf("/** %s */\n", mod.Description))
	}
	sb.WriteString(fmt.Sprintf("class %sController extends Controller\n{\n", modClass))
	sb.WriteString("    public function __construct(\n")
	sb.WriteString(fmt.Sprintf("        private readonly I%sService $service,\n", modClass))
	sb.WriteString("    ) {}\n")

	for _, act := range mod.Actions {
		writePhpAction(&sb, mod, act, enumNames)
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, modClass+"Controller.php"), []byte(sb.String()), 0644)
}

// writePhpAction appends a single Laravel controller action to sb.
func writePhpAction(sb *strings.Builder, mod ast.Module, act ast.Action, enumNames map[string]bool) {
	routePath := act.Path
	if mod.Prefix != "" {
		routePath = mod.Prefix + act.Path
	}

	pathParams := emitter.ExtractPathParams(routePath)
	returnType := phpReturnType(act, enumNames)
	methodName := phpCamelName(act.Name)

	sb.WriteString("\n")
	if act.Description != "" {
		sb.WriteString(fmt.Sprintf("    /** %s */\n", act.Description))
	}

	// Method signature: Request first, then path params.
	var sigParams []string
	sigParams = append(sigParams, "Request $request")
	for _, p := range pathParams {
		sigParams = append(sigParams, "string $"+phpSnakeName(p))
	}

	responseHint := "JsonResponse"
	if act.Method == "DELETE" && returnType == "void" {
		responseHint = "\\Illuminate\\Http\\Response|JsonResponse"
	}

	sb.WriteString(fmt.Sprintf("    public function %s(%s): %s\n    {\n",
		methodName, strings.Join(sigParams, ", "), responseHint))
	sb.WriteString("        try {\n")

	// Build service call arguments.
	var callArgs []string
	for _, p := range pathParams {
		callArgs = append(callArgs, "$"+phpSnakeName(p))
	}
	if act.Input != "" {
		// Instantiate the input DTO from the validated request data.
		sb.WriteString(fmt.Sprintf("            $input = new %s(...$request->validate($request->all()));\n", act.Input))
		callArgs = append(callArgs, "$input")
	}
	if act.Query != "" {
		callArgs = append(callArgs, "$request->query()")
	}

	serviceCall := fmt.Sprintf("$this->service->%s(%s)", methodName, strings.Join(callArgs, ", "))

	switch {
	case act.Method == "DELETE" && returnType == "void":
		sb.WriteString(fmt.Sprintf("            %s;\n", serviceCall))
		sb.WriteString("            return response()->noContent();\n")
	case act.Method == "POST":
		sb.WriteString(fmt.Sprintf("            $result = %s;\n", serviceCall))
		sb.WriteString("            return response()->json($result, 201);\n")
	default:
		sb.WriteString(fmt.Sprintf("            $result = %s;\n", serviceCall))
		sb.WriteString("            return response()->json($result);\n")
	}

	sb.WriteString("        } catch (\\Exception $e) {\n")
	sb.WriteString("            return response()->json(['error' => $e->getMessage()], 500);\n")
	sb.WriteString("        }\n    }\n")
}

// emitRoutes writes routes/api.php with all module route registrations.
func (e *PhpEmitter) emitRoutes(a ast.AST, outDir string) error {
	dir := filepath.Join(outDir, "routes")

	var sb strings.Builder
	sb.WriteString("<?php\n\n")
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n\n")
	sb.WriteString("use Illuminate\\Support\\Facades\\Route;\n")

	for _, mod := range a.Modules {
		sb.WriteString(fmt.Sprintf("use App\\Http\\Controllers\\%sController;\n", capitalize(mod.Name)))
	}
	sb.WriteString("\n")

	for _, mod := range a.Modules {
		modClass := capitalize(mod.Name)
		if mod.Description != "" {
			sb.WriteString(fmt.Sprintf("// %s\n", mod.Description))
		}
		for _, act := range mod.Actions {
			routePath := act.Path
			if mod.Prefix != "" {
				routePath = mod.Prefix + act.Path
			}
			// Laravel uses {param} route syntax (same as ASP.NET Core / Chi).
			laravelPath := emitter.ToChiPath(routePath)
			method := strings.ToLower(act.Method)
			methodName := phpCamelName(act.Name)
			sb.WriteString(fmt.Sprintf("Route::%s('%s', [%sController::class, '%s']);\n",
				method, laravelPath, modClass, methodName))
		}
		sb.WriteString("\n")
	}

	return os.WriteFile(filepath.Join(dir, "api.php"), []byte(sb.String()), 0644)
}
