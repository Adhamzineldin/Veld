package php

// sdk.go — service SDK generation for PHP backend.
// Generates typed HTTP clients using cURL.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/sdkhelpers"
)

func (e *PhpEmitter) EmitServiceSdk(consumed []emitter.ConsumedServiceInfo, outDir string, opts emitter.EmitOptions) error {
	if opts.DryRun || len(consumed) == 0 {
		return nil
	}
	for _, c := range consumed {
		if err := emitPhpSdk(c, outDir); err != nil {
			return fmt.Errorf("service sdk %s: %w", c.Name, err)
		}
	}
	return nil
}

func emitPhpSdk(consumed emitter.ConsumedServiceInfo, outDir string) error {
	dir := filepath.Join(outDir, "app", "Sdk", capitalize(consumed.Name))
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}
	if err := emitPhpSdkTypes(consumed, dir); err != nil {
		return err
	}
	return emitPhpSdkClient(consumed, dir)
}

func emitPhpSdkTypes(consumed emitter.ConsumedServiceInfo, dir string) error {
	var sb strings.Builder
	a := consumed.AST
	ns := "App\\Sdk\\" + capitalize(consumed.Name)

	sb.WriteString("<?php\n\n// Code generated by Veld — DO NOT EDIT.\n\n")
	sb.WriteString(fmt.Sprintf("namespace %s;\n\n", ns))

	usedModels := make(map[string]bool)
	for _, mod := range a.Modules {
		for k, v := range emitter.CollectTransitiveModels(a, mod) {
			if v {
				usedModels[k] = true
			}
		}
	}

	for _, m := range a.Models {
		if !usedModels[m.Name] {
			continue
		}
		sb.WriteString(fmt.Sprintf("class %s\n{\n", m.Name))
		sb.WriteString("    public function __construct(\n")
		for i, f := range m.Fields {
			phpType := phpSdkType(f)
			comma := ","
			if i == len(m.Fields)-1 {
				comma = ""
			}
			sb.WriteString(fmt.Sprintf("        public %s $%s%s\n", phpType, f.Name, comma))
		}
		sb.WriteString("    ) {}\n\n")
		sb.WriteString("    public static function fromArray(array $data): self\n    {\n        return new self(\n")
		for i, f := range m.Fields {
			comma := ","
			if i == len(m.Fields)-1 {
				comma = ""
			}
			sb.WriteString(fmt.Sprintf("            $data['%s'] ?? null%s\n", f.Name, comma))
		}
		sb.WriteString("        );\n    }\n}\n\n")
	}

	return os.WriteFile(filepath.Join(dir, "Types.php"), []byte(sb.String()), 0644)
}

func emitPhpSdkClient(consumed emitter.ConsumedServiceInfo, dir string) error {
	var sb strings.Builder
	a := consumed.AST
	envVar := sdkhelpers.EnvVarName(consumed.Name)
	className := sdkhelpers.ServiceClassName(consumed.Name)
	ns := "App\\Sdk\\" + capitalize(consumed.Name)

	sb.WriteString("<?php\n\n// Code generated by Veld — DO NOT EDIT.\n\n")
	sb.WriteString(fmt.Sprintf("namespace %s;\n\n", ns))
	sb.WriteString("class VeldApiError extends \\RuntimeException\n{\n")
	sb.WriteString("    public function __construct(public readonly int $status, public readonly string $body)\n")
	sb.WriteString("    {\n        parent::__construct(\"HTTP {$status}: {$body}\");\n    }\n}\n\n")

	sb.WriteString(fmt.Sprintf("class %s\n{\n", className))
	sb.WriteString("    private string $base;\n    private array $headers;\n\n")

	sb.WriteString(fmt.Sprintf("    public function __construct(?string $baseUrl = null, array $headers = [])\n    {\n"))
	if consumed.BaseUrl != "" {
		sb.WriteString(fmt.Sprintf("        $this->base = $baseUrl ?? getenv('%s') ?: '%s';\n", envVar, consumed.BaseUrl))
	} else {
		sb.WriteString(fmt.Sprintf("        $resolved = $baseUrl ?? getenv('%s') ?: null;\n", envVar))
		sb.WriteString(fmt.Sprintf("        if (!$resolved) throw new \\InvalidArgumentException('No base URL for %s — pass $baseUrl or set %s');\n", consumed.Name, envVar))
		sb.WriteString("        $this->base = $resolved;\n")
	}
	sb.WriteString("        $this->headers = $headers;\n    }\n\n")

	sb.WriteString("    private function request(string $method, string $path, ?array $body = null): ?array\n    {\n")
	sb.WriteString("        $ch = curl_init($this->base . $path);\n")
	sb.WriteString("        $hdrs = array_merge(['Content-Type: application/json'], array_map(fn($k, $v) => \"$k: $v\", array_keys($this->headers), $this->headers));\n")
	sb.WriteString("        curl_setopt_array($ch, [\n")
	sb.WriteString("            CURLOPT_CUSTOMREQUEST => $method,\n            CURLOPT_RETURNTRANSFER => true,\n            CURLOPT_HTTPHEADER => $hdrs,\n")
	sb.WriteString("        ]);\n")
	sb.WriteString("        if ($body !== null) curl_setopt($ch, CURLOPT_POSTFIELDS, json_encode($body));\n")
	sb.WriteString("        $response = curl_exec($ch);\n        $status = curl_getinfo($ch, CURLINFO_HTTP_CODE);\n        curl_close($ch);\n")
	sb.WriteString("        if ($status >= 400) throw new VeldApiError($status, $response ?: '');\n")
	sb.WriteString("        if ($status === 204) return null;\n        return json_decode($response, true);\n    }\n")

	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			if strings.ToUpper(act.Method) == "WS" {
				continue
			}
			sb.WriteString("\n")
			writePhpSdkMethod(&sb, mod, act)
		}
	}
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, className+".php"), []byte(sb.String()), 0644)
}

func writePhpSdkMethod(sb *strings.Builder, mod ast.Module, act ast.Action) {
	routePath := mod.Prefix + act.Path
	method := strings.ToUpper(act.Method)
	fnName := emitter.ToCamelCase(act.Name)
	pathParams := emitter.ExtractPathParams(routePath)

	retType := "void"
	if act.Output != "" {
		retType = "?array"
	}

	var params []string
	for _, p := range pathParams {
		params = append(params, "string $"+p)
	}
	if act.Input != "" {
		params = append(params, "array $body")
	}

	sb.WriteString(fmt.Sprintf("    /** %s %s */\n", method, routePath))
	sb.WriteString(fmt.Sprintf("    public function %s(%s): %s\n    {\n", fnName, strings.Join(params, ", "), retType))

	urlExpr := phpSdkBuildUrl(routePath, pathParams)
	bodyArg := "null"
	if act.Input != "" {
		bodyArg = "$body"
	}

	if act.Output != "" {
		sb.WriteString(fmt.Sprintf("        return $this->request('%s', %s, %s);\n    }\n", method, urlExpr, bodyArg))
	} else {
		sb.WriteString(fmt.Sprintf("        $this->request('%s', %s, %s);\n    }\n", method, urlExpr, bodyArg))
	}
}

func phpSdkBuildUrl(routePath string, pathParams []string) string {
	if len(pathParams) == 0 {
		return fmt.Sprintf("'%s'", routePath)
	}
	expr := routePath
	for _, p := range pathParams {
		expr = strings.Replace(expr, ":"+p, "{$"+p+"}", 1)
	}
	return "\"" + expr + "\""
}

func phpSdkType(f ast.Field) string {
	base := phpSdkScalar(f.Type)
	if f.IsArray || f.IsMap {
		return "array"
	}
	if f.Optional {
		return "?" + base
	}
	return base
}

func phpSdkScalar(t string) string {
	switch t {
	case "string", "uuid", "date", "datetime":
		return "string"
	case "int":
		return "int"
	case "float":
		return "float"
	case "bool":
		return "bool"
	case "any", "json":
		return "mixed"
	default:
		return t
	}
}
