package php

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
)

func (e *PhpEmitter) emitConstants(a ast.AST, outDir string) error {
	if len(a.Constants) == 0 {
		return nil
	}
	dir := filepath.Join(outDir, "app", "Constants")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	for _, cg := range a.Constants {
		var sb strings.Builder
		sb.WriteString("<?php\n// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
		sb.WriteString("namespace App\\Constants;\n\n")
		if cg.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", cg.Description))
		}
		sb.WriteString(fmt.Sprintf("final class %s\n{\n", cg.Name))
		for _, f := range cg.Fields {
			phpVal := phpConstValue(f)
			sb.WriteString(fmt.Sprintf("    public const %s = %s;\n", f.Name, phpVal))
		}
		sb.WriteString("}\n")
		if err := os.WriteFile(filepath.Join(dir, cg.Name+".php"), []byte(sb.String()), 0644); err != nil {
			return err
		}
	}
	return nil
}

func phpConstValue(f ast.ConstantField) string {
	switch f.Type {
	case "string", "uuid", "date", "datetime", "decimal":
		return fmt.Sprintf("'%s'", f.Value)
	case "int":
		return f.Value
	case "float":
		return f.Value
	case "bool":
		return f.Value
	default:
		return fmt.Sprintf("'%s'", f.Value)
	}
}
