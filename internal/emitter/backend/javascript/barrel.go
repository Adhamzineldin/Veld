package javascript

// barrel.go — emits the root index.js barrel export and package.json alias.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
)

func (e *JSEmitter) emitBarrel(a ast.AST, outDir string) error {
	if err := writeIndexJS(a, outDir); err != nil {
		return err
	}
	return writePackageJSON(a, outDir)
}

func writeIndexJS(a ast.AST, outDir string) error {
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")
	sb.WriteString("// Re-export all types (as JSDoc typedefs, consumed via /** @type {import(...)} */).\n")
	sb.WriteString("// This file serves as the barrel entry point.\n")
	return os.WriteFile(filepath.Join(outDir, "index.js"), []byte(sb.String()), 0644)
}

func writePackageJSON(a ast.AST, outDir string) error {
	var sb strings.Builder
	sb.WriteString("{\n")
	sb.WriteString("  \"name\": \"@veld/generated\",\n")
	sb.WriteString("  \"version\": \"0.0.0\",\n")
	sb.WriteString("  \"private\": true,\n")
	sb.WriteString("  \"main\": \"./index.js\",\n")
	sb.WriteString("  \"exports\": {\n")
	sb.WriteString("    \".\": \"./index.js\",\n")
	sb.WriteString("    \"./types\": \"./types/index.js\",\n")
	sb.WriteString("    \"./types/*\": \"./types/*.js\",\n")
	sb.WriteString("    \"./interfaces/*\": \"./interfaces/*.js\",\n")
	sb.WriteString("    \"./errors\": \"./errors/index.js\",\n")
	sb.WriteString("    \"./errors/*\": \"./errors/*.js\",\n")
	sb.WriteString("    \"./client\": \"./client/api.js\",\n")
	sb.WriteString("    \"./client/*\": \"./client/*.js\"")
	for _, mod := range a.Modules {
		moduleLower := strings.ToLower(mod.Name)
		sb.WriteString(fmt.Sprintf(",\n    \"./routes/%s.routes\": \"./routes/%s.routes.js\"", moduleLower, moduleLower))
	}
	sb.WriteString("\n  }\n}\n")
	return os.WriteFile(filepath.Join(outDir, "package.json"), []byte(sb.String()), 0644)
}
