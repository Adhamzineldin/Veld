func TestJSEmitterTypesContent(t *testing.T) {
	e := javascript.New()
	outDir := t.TempDir()
	if err := e.Emit(minimalAST(), outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(outDir, "types", "auth.js"))
	if err != nil {
		t.Fatalf("ReadFile types/auth.js: %v", err)
	}
	content := string(data)

	checks := []struct {
		desc   string
		needle string
	}{
		{"header", "AUTO-GENERATED BY VELD"},
		{"User typedef", "@typedef {Object} User"},
		{"optional field", "[bio]"},
		{"array field", "string[]"},
		{"enum typedef", "@typedef {'admin' | 'user' | 'guest'} Role"},
	}
	for _, c := range checks {
		if !strings.Contains(content, c.needle) {
			t.Errorf("types/auth.js missing %s: want %q\ngot:\n%s", c.desc, c.needle, content)
		}
	}

	// Must NOT contain TypeScript syntax.
	tsChecks := []string{"export interface", "export type Role ="}
	for _, ts := range tsChecks {
		if strings.Contains(content, ts) {
			t.Errorf("types/auth.js should not contain TypeScript syntax %q", ts)
		}
	}
}

