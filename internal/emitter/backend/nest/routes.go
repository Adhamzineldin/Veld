package nest

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/tshelpers"
)

func (e *NestEmitter) emitControllers(a ast.AST, mod ast.Module, outDir string, opts emitter.EmitOptions) error {
	dir := filepath.Join(outDir, "controllers")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	moduleLower := strings.ToLower(mod.Name)
	controllerName := mod.Name + "Controller"
	serviceType := "I" + mod.Name + "Service"
	serviceField := emitter.ToCamelCase(mod.Name) + "Service"

	usedTypes := collectControllerTypes(mod)
	decorators := collectControllerDecorators(mod)

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD - DO NOT EDIT\n")
	sb.WriteString("import { ")
	sb.WriteString(strings.Join(decorators, ", "))
	sb.WriteString(" } from '@nestjs/common';\n")
	sb.WriteString(fmt.Sprintf("import type { %s } from '../interfaces/I%sService';\n", serviceType, mod.Name))
	if len(usedTypes) > 0 {
		sb.WriteString(fmt.Sprintf("import type { %s } from '../types/%s';\n", strings.Join(usedTypes, ", "), moduleLower))
	}
	if opts.Validate {
		if imports := buildValidatorImports(mod); len(imports) > 0 {
			sb.WriteString(fmt.Sprintf("import { %s } from '../_validators';\n", strings.Join(imports, ", ")))
		}
	}
	sb.WriteString("\n")

	basePath := strings.TrimPrefix(strings.TrimSpace(mod.Prefix), "/")
	sb.WriteString(fmt.Sprintf("@Controller('%s')\n", basePath))
	sb.WriteString(fmt.Sprintf("export class %s {\n", controllerName))
	sb.WriteString(fmt.Sprintf("  constructor(private readonly %s: %s) {}\n", serviceField, serviceType))

	for _, act := range mod.Actions {
		writeControllerMethod(&sb, mod, act, serviceField, opts)
	}

	sb.WriteString("}\n")
	return os.WriteFile(filepath.Join(dir, moduleLower+".controller.ts"), []byte(sb.String()), 0644)
}

func writeControllerMethod(sb *strings.Builder, mod ast.Module, act ast.Action, serviceField string, opts emitter.EmitOptions) {
	if act.Description != "" {
		sb.WriteString(fmt.Sprintf("\n  // %s\n", act.Description))
	}

	if act.Method == "WS" {
		sb.WriteString("\n  // WebSocket actions are not emitted as HTTP endpoints by the NestJS emitter.\n")
		sb.WriteString(fmt.Sprintf("  // Action: %s\n", act.Name))
		return
	}

	httpDecorator := nestHTTPDecorator(act.Method)
	localPath := routePathWithinPrefix(mod.Prefix, act.Path)
	if localPath == "" {
		localPath = "/"
	}
	nestPath := strings.TrimPrefix(localPath, "/")

	sb.WriteString(fmt.Sprintf("\n  @%s('%s')\n", httpDecorator, nestPath))
	if act.Method == "POST" {
		sb.WriteString("  @HttpCode(201)\n")
	}
	if act.Method == "DELETE" && act.Output == "" {
		sb.WriteString("  @HttpCode(204)\n")
	}

	methodName := emitter.ToCamelCase(act.Name)
	params, callArgs := buildMethodParams(localPath, act, opts)
	returnType := tshelpers.FormatOutputType(act)
	sb.WriteString(fmt.Sprintf("  async %s(%s): Promise<%s> {\n", methodName, strings.Join(params, ", "), returnType))

	if act.Input != "" && opts.Validate {
		sb.WriteString(fmt.Sprintf("    const input = parse%s(rawBody);\n", act.Input))
	}

	sb.WriteString(fmt.Sprintf("    const result = await this.%s.%s(%s);\n", serviceField, methodName, strings.Join(callArgs, ", ")))

	if opts.Validate && act.Output != "" {
		if act.OutputArray {
			sb.WriteString(fmt.Sprintf("    assert%sArray(result);\n", act.Output))
		} else {
			sb.WriteString(fmt.Sprintf("    assert%s(result);\n", act.Output))
		}
	}

	if act.Method == "DELETE" && act.Output == "" {
		sb.WriteString("    return undefined as void;\n")
	} else {
		sb.WriteString("    return result;\n")
	}
	sb.WriteString("  }\n")
}

func buildMethodParams(localPath string, act ast.Action, opts emitter.EmitOptions) ([]string, []string) {
	pathParams := emitter.ExtractPathParams(localPath)
	params := make([]string, 0, len(pathParams)+2)
	callArgs := make([]string, 0, len(pathParams)+2)

	for _, p := range pathParams {
		params = append(params, fmt.Sprintf("@Param('%s') %s: string", p, p))
		callArgs = append(callArgs, p)
	}

	if act.Input != "" {
		if opts.Validate {
			params = append(params, "@Body() rawBody: unknown")
			callArgs = append(callArgs, "input")
		} else {
			params = append(params, fmt.Sprintf("@Body() input: %s", tshelpers.VeldScalarToTS(act.Input)))
			callArgs = append(callArgs, "input")
		}
	}

	if act.Query != "" {
		params = append(params, fmt.Sprintf("@Query() query: %s", tshelpers.VeldScalarToTS(act.Query)))
		callArgs = append(callArgs, "query")
	}

	return params, callArgs
}

func routePathWithinPrefix(prefix, actionPath string) string {
	p := strings.TrimSpace(prefix)
	a := strings.TrimSpace(actionPath)
	if p == "" {
		return a
	}
	if strings.HasPrefix(a, p) {
		trimmed := strings.TrimPrefix(a, p)
		if trimmed == "" {
			return "/"
		}
		if !strings.HasPrefix(trimmed, "/") {
			return "/" + trimmed
		}
		return trimmed
	}
	return a
}

func collectControllerTypes(mod ast.Module) []string {
	seen := map[string]bool{}
	addType := func(name string) {
		if name == "" {
			return
		}
		ts := tshelpers.VeldScalarToTS(name)
		if ts == "string" || ts == "number" || ts == "boolean" || ts == "any" || strings.HasPrefix(ts, "Record<") {
			return
		}
		seen[ts] = true
	}

	for _, act := range mod.Actions {
		addType(act.Input)
		addType(act.Query)
		if act.Output != "" {
			addType(act.Output)
		}
	}

	out := make([]string, 0, len(seen))
	for t := range seen {
		out = append(out, t)
	}
	sort.Strings(out)
	return out
}

func collectControllerDecorators(mod ast.Module) []string {
	set := map[string]bool{
		"Controller": true,
	}

	for _, act := range mod.Actions {
		if act.Input != "" {
			set["Body"] = true
		}
		if act.Query != "" {
			set["Query"] = true
		}
		if len(emitter.ExtractPathParams(routePathWithinPrefix(mod.Prefix, act.Path))) > 0 {
			set["Param"] = true
		}
		if act.Method == "POST" || (act.Method == "DELETE" && act.Output == "") {
			set["HttpCode"] = true
		}

		switch strings.ToUpper(act.Method) {
		case "GET":
			set["Get"] = true
		case "POST":
			set["Post"] = true
		case "PUT":
			set["Put"] = true
		case "PATCH":
			set["Patch"] = true
		case "DELETE":
			set["Delete"] = true
		}
	}

	decorators := make([]string, 0, len(set))
	for d := range set {
		decorators = append(decorators, d)
	}
	sort.Strings(decorators)
	return decorators
}

func nestHTTPDecorator(method string) string {
	switch strings.ToUpper(method) {
	case "GET":
		return "Get"
	case "POST":
		return "Post"
	case "PUT":
		return "Put"
	case "PATCH":
		return "Patch"
	case "DELETE":
		return "Delete"
	default:
		return "Get"
	}
}
