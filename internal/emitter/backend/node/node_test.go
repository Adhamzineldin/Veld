func TestNodeEmitterPathParams(t *testing.T) {
	e := node.New()
	outDir := t.TempDir()

	a := ast.AST{
		Models: []ast.Model{
			{Name: "Item", Fields: []ast.Field{{Name: "id", Type: "string"}}},
		},
		Modules: []ast.Module{
			{
				Name: "Items",
				Actions: []ast.Action{
					{Name: "GetById", Method: "GET", Path: "/items/:id", Output: "Item"},
					{Name: "Delete", Method: "DELETE", Path: "/items/:id"},
				},
			},
		},
	}

	if err := e.Emit(a, outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, _ := os.ReadFile(filepath.Join(outDir, "routes", "items.routes.ts"))
	content := string(data)

	if !strings.Contains(content, "req.params.id") {
		t.Error("path param :id should be extracted via req.params.id")
	}

	data2, _ := os.ReadFile(filepath.Join(outDir, "interfaces", "IItemsService.ts"))
	iface := string(data2)
	if !strings.Contains(iface, "id: string") {
		t.Error("interface should include path param id: string")
	}
}

