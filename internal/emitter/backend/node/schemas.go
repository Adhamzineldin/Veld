package node

// schemas.go — emits schemas/schemas.ts with Zod validation schemas for all
// models and enums. Supports extends via .extend(), optional fields, and defaults.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/veld-dev/veld/internal/ast"
)

func (e *NodeEmitter) emitZodSchemas(a ast.AST, outDir string) error {
	dir := filepath.Join(outDir, "schemas")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString("import { z } from 'zod';\n\n")

	for _, en := range a.Enums {
		if en.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", en.Description))
		}
		quoted := make([]string, len(en.Values))
		for i, v := range en.Values {
			quoted[i] = fmt.Sprintf("'%s'", v)
		}
		sb.WriteString(fmt.Sprintf("export const %sSchema = z.enum([%s]);\n\n", en.Name, strings.Join(quoted, ", ")))
	}

	for _, m := range a.Models {
		if m.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", m.Description))
		}
		if m.Extends != "" {
			sb.WriteString(fmt.Sprintf("export const %sSchema = %sSchema.extend({\n", m.Name, m.Extends))
		} else {
			sb.WriteString(fmt.Sprintf("export const %sSchema = z.object({\n", m.Name))
		}
		for _, f := range m.Fields {
			zodType := veldFieldToZod(f, enumNames)
			if f.Default != "" {
				zodType += fmt.Sprintf(".default(%s)", normaliseDefault(f.Default))
			}
			if f.Optional && f.Default == "" {
				zodType += ".optional()"
			}
			sb.WriteString(fmt.Sprintf("  %s: %s,\n", f.Name, zodType))
		}
		sb.WriteString("});\n\n")
	}

	return os.WriteFile(filepath.Join(dir, "schemas.ts"), []byte(sb.String()), 0644)
}

// veldFieldToZod returns the Zod expression for a field (before optional/default modifiers).
func veldFieldToZod(f ast.Field, enumNames map[string]bool) string {
	if f.IsMap {
		return fmt.Sprintf("z.record(z.string(), %s)", veldTypeToZod(f.MapValueType, false, enumNames))
	}
	return veldTypeToZod(f.Type, f.IsArray, enumNames)
}

// veldTypeToZod maps a Veld scalar type (or model/enum name) to a Zod expression.
func veldTypeToZod(t string, isArray bool, enumNames map[string]bool) string {
	var base string
	switch t {
	case "string":
		base = "z.string()"
	case "int":
		base = "z.number().int()"
	case "float":
		base = "z.number()"
	case "bool":
		base = "z.boolean()"
	case "date":
		base = "z.string().date()"
	case "datetime":
		base = "z.string().datetime()"
	case "uuid":
		base = "z.string().uuid()"
	default:
		base = fmt.Sprintf("%sSchema", t)
	}
	if isArray {
		return fmt.Sprintf("z.array(%s)", base)
	}
	return base
}

// normaliseDefault converts a Veld default value string to a valid JS literal.
func normaliseDefault(d string) string {
	if strings.HasPrefix(d, "\"") {
		return "'" + strings.Trim(d, "\"") + "'"
	}
	if d == "true" || d == "false" || isNumericDefault(d) {
		return d
	}
	return "'" + d + "'"
}

// isNumericDefault reports whether s looks like an integer or float literal.
func isNumericDefault(s string) bool {
	if len(s) == 0 {
		return false
	}
	start := 0
	if s[0] == '-' {
		start = 1
	}
	if start >= len(s) {
		return false
	}
	for i := start; i < len(s); i++ {
		if (s[i] < '0' || s[i] > '9') && s[i] != '.' {
			return false
		}
	}
	return true
}
