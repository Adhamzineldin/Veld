package node

// routes.go — emits routes/{module}.routes.ts with try/catch and
// correct HTTP status codes (201 for POST, 204 for DELETE with no output).
//
// When EmitOptions.Validate is true, route handlers are augmented with:
//   - parseX(req.body)  — validates input shape before passing to service (→ 400)
//   - assertX(result)   — guards output shape before sending to client   (→ 500)

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	nodestrategy "github.com/Adhamzineldin/Veld/internal/emitter/backend/node/strategy"
)

func (e *NodeEmitter) emitRoutes(a ast.AST, mod ast.Module, outDir string, opts emitter.EmitOptions, strat nodestrategy.NodeFrameworkStrategy) error {
	dir := filepath.Join(outDir, "routes")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	moduleLower := strings.ToLower(mod.Name)
	funcName := moduleLower + "Router"
	allMiddleware := emitter.CollectModuleMiddleware(mod)

	// Detect whether any WS actions exist in this module.
	hasWS := false
	for _, act := range mod.Actions {
		if act.Method == "WS" {
			hasWS = true
			break
		}
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString(fmt.Sprintf("import type { I%sService } from '../interfaces/I%sService';\n", mod.Name, mod.Name))

	// Add WS imports when the module has WebSocket actions.
	if hasWS {
		wsImports := strat.WSImports()
		if wsImports != "" {
			sb.WriteString(wsImports)
		} else {
			// Plain strategy: no typed import needed, but we still reference WebSocket
			// generically; no import line required.
		}
	}

	if emitter.HasErrors(mod) {
		sb.WriteString(fmt.Sprintf("import { ApiError } from '../errors/%s.errors';\n", moduleLower))
	}

	if len(allMiddleware) > 0 {
		// Import from the single shared middleware interface.
		sb.WriteString("import type { IMiddleware } from '../middleware/IMiddleware';\n")
	}

	// When validation is enabled, import the generated zero-dep validators.
	if opts.Validate {
		if imports := buildValidatorImports(mod); len(imports) > 0 {
			sb.WriteString(fmt.Sprintf("import { %s } from '../_validators';\n", strings.Join(imports, ", ")))
		}
	}

	sb.WriteString("\n")
	if len(allMiddleware) > 0 {
		sb.WriteString(fmt.Sprintf("export function %s(router: %s, service: I%sService, middleware: IMiddleware): void {\n", funcName, strat.RouterType(), mod.Name))
	} else {
		sb.WriteString(fmt.Sprintf("export function %s(router: %s, service: I%sService): void {\n", funcName, strat.RouterType(), mod.Name))
	}

	var wsMounts strings.Builder
	for _, act := range mod.Actions {
		if act.Method == "WS" {
			writeWSRouteComment(&sb, mod, act)
			writeWSMountFunction(&wsMounts, mod, act, strat)
		} else {
			if err := writeRouteHandler(&sb, mod, act, opts, strat); err != nil {
				return err
			}
		}
	}

	sb.WriteString("}\n")

	// Append WS mount functions outside the HTTP router function.
	if wsMounts.Len() > 0 {
		sb.WriteString("\n")
		sb.WriteString(wsMounts.String())
	}

	if err := os.WriteFile(filepath.Join(dir, moduleLower+".routes.ts"), []byte(sb.String()), 0644); err != nil {
		return err
	}

	return nil
}

// writeRouteHandler appends a single route registration + async handler to sb.
func writeRouteHandler(sb *strings.Builder, mod ast.Module, act ast.Action, opts emitter.EmitOptions, strat nodestrategy.NodeFrameworkStrategy) error {
	method := strings.ToLower(act.Method)
	routePath := act.Path
	if mod.Prefix != "" {
		routePath = mod.Prefix + act.Path
	}
	camelName := emitter.ToCamelCase(act.Name)

	if act.Description != "" {
		sb.WriteString(fmt.Sprintf("\n  // %s\n", act.Description))
	}

	mwArgs := ""
	for _, mw := range act.Middleware {
		mwArgs += fmt.Sprintf("middleware.%s, ", emitter.ToCamelCase(mw))
	}

	sb.WriteString(fmt.Sprintf("\n  router.%s('%s', %sasync (req: %s, res: %s) => {\n", method, routePath, mwArgs, strat.RequestType(), strat.ResponseType()))
	sb.WriteString("    try {\n")

	// ── Build service call arguments ─────────────────────────────────────────
	pathParams := emitter.ExtractPathParams(routePath)
	callArgs := make([]string, 0, len(pathParams)+2)
	for _, p := range pathParams {
		callArgs = append(callArgs, fmt.Sprintf("req.params.%s", p))
	}

	if act.Input != "" {
		if opts.Validate {
			// Parse and validate the request body; throws VeldValidationError (400) on failure.
			sb.WriteString(fmt.Sprintf("      const body = parse%s(req.body);\n", act.Input))
			callArgs = append(callArgs, "body")
		} else {
			callArgs = append(callArgs, "req.body")
		}
	}

	if act.Query != "" {
		callArgs = append(callArgs, "req.query")
	}

	serviceCall := fmt.Sprintf("service.%s(%s)", camelName, strings.Join(callArgs, ", "))

	// ── Service call + optional output assertion + response ──────────────────
	switch {
	case act.Method == "DELETE" && act.Output == "":
		sb.WriteString(fmt.Sprintf("      await %s;\n", serviceCall))
		sb.WriteString("      res.status(204).end();\n")

	case act.Method == "POST":
		sb.WriteString(fmt.Sprintf("      const result = await %s;\n", serviceCall))
		if opts.Validate && act.Output != "" {
			writeNodeOutputAssertion(sb, act)
		}
		sb.WriteString("      res.status(201).json(result);\n")

	default:
		sb.WriteString(fmt.Sprintf("      const result = await %s;\n", serviceCall))
		if opts.Validate && act.Output != "" {
			writeNodeOutputAssertion(sb, act)
		}
		sb.WriteString("      res.json(result);\n")
	}

	// Catch block: handles VeldValidationError (400), VeldContractError (500), and service errors.
	sb.WriteString("    } catch (err: any) {\n")
	sb.WriteString("      const status = err.status ?? err.statusCode ?? 500;\n")
	sb.WriteString("      const body: Record<string, string> = { error: err.message ?? 'Internal Server Error' };\n")
	sb.WriteString("      if (err.code) body.code = err.code;\n")
	sb.WriteString("      res.status(status).json(body);\n")
	sb.WriteString("    }\n")
	sb.WriteString("  });\n")
	return nil
}

// writeNodeOutputAssertion emits assertX(result) or assertXArray(result).
// These throw VeldContractError (→ 500) if the service returns the wrong shape.
func writeNodeOutputAssertion(sb *strings.Builder, act ast.Action) {
	if act.OutputArray {
		sb.WriteString(fmt.Sprintf("      assert%sArray(result);\n", act.Output))
	} else {
		sb.WriteString(fmt.Sprintf("      assert%s(result);\n", act.Output))
	}
}

// writeWSRouteComment appends a comment inside the HTTP router function body
// pointing developers to the corresponding mount function.
func writeWSRouteComment(sb *strings.Builder, mod ast.Module, act ast.Action) {
	routePath := act.Path
	if mod.Prefix != "" {
		routePath = mod.Prefix + act.Path
	}
	camelName := emitter.ToCamelCase(act.Name)
	if act.Description != "" {
		sb.WriteString(fmt.Sprintf("\n  // %s\n", act.Description))
	}
	sb.WriteString(fmt.Sprintf("\n  // WebSocket: WS %s → use mount%sWS(wss, service)\n", routePath, camelName))
}

// writeWSMountFunction appends a standalone exported mount function (and optional
// broadcast helper) for a WS action to the provided builder.
func writeWSMountFunction(sb *strings.Builder, mod ast.Module, act ast.Action, strat nodestrategy.NodeFrameworkStrategy) {
	routePath := act.Path
	if mod.Prefix != "" {
		routePath = mod.Prefix + act.Path
	}
	camelName := emitter.ToCamelCase(act.Name)
	pathParams := emitter.ExtractPathParams(routePath)

	// Determine message type (client→server).
	emitType := "unknown"
	if act.Emit != "" {
		emitType = act.Emit
	}

	wsRouterParam := strat.WSRouterParam()

	// Build path param extraction from req.url.
	var paramLines strings.Builder
	for _, p := range pathParams {
		paramLines.WriteString(fmt.Sprintf("    const %s = url.pathname.split('/').at(-1) ?? '';\n", p))
	}

	// Build params object literal for onConnect call.
	paramFields := make([]string, 0, len(pathParams))
	for _, p := range pathParams {
		paramFields = append(paramFields, p)
	}
	paramsArg := "{ " + strings.Join(paramFields, ", ") + " }"
	if len(paramFields) == 0 {
		paramsArg = "{}"
	}

	sb.WriteString(fmt.Sprintf("export function mount%sWS(%s, service: I%sService): void {\n", camelName, wsRouterParam, mod.Name))
	sb.WriteString("  wss.on('connection', (socket: any, req: any) => {\n")
	sb.WriteString("    const url = new URL(req.url ?? '', 'http://x');\n")
	sb.WriteString(paramLines.String())
	sb.WriteString("    socket.on('message', (raw: any) => {\n")
	sb.WriteString("      try {\n")
	sb.WriteString(fmt.Sprintf("        const msg = JSON.parse(String(raw)) as %s;\n", emitType))
	sb.WriteString(fmt.Sprintf("        service.on%sMessage?.(socket, msg);\n", camelName))
	sb.WriteString("      } catch {\n")
	sb.WriteString("        socket.send(JSON.stringify({ error: 'invalid message' }));\n")
	sb.WriteString("      }\n")
	sb.WriteString("    });\n")
	sb.WriteString(fmt.Sprintf("    socket.on('close', () => service.on%sClose?.(socket));\n", camelName))
	sb.WriteString(fmt.Sprintf("    socket.on('error', (err: Error) => service.on%sError?.(socket, err));\n", camelName))
	sb.WriteString(fmt.Sprintf("    service.on%sConnect(socket, %s);\n", camelName, paramsArg))
	sb.WriteString("  });\n")
	sb.WriteString("}\n")

	// Optional broadcast helper when stream type is defined.
	if act.Stream != "" {
		sb.WriteString(fmt.Sprintf("\nexport function broadcast%s(socket: any, msg: %s): void {\n", camelName, act.Stream))
		sb.WriteString("  socket.send(JSON.stringify(msg));\n")
		sb.WriteString("}\n")
	}
}

// emitMiddlewareInterface writes middleware/IMiddleware.ts — a single shared
// interface containing all unique middleware methods used across ALL modules.
// This way developers implement one middleware object and pass it to every module's router.
func (e *NodeEmitter) emitMiddlewareInterface(a ast.AST, outDir string, strat nodestrategy.NodeFrameworkStrategy) error {
	allMiddleware := emitter.CollectAllMiddleware(a.Modules)
	if len(allMiddleware) == 0 {
		return nil
	}

	dir := filepath.Join(outDir, "middleware")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n\n")
	sb.WriteString("/**\n * Shared middleware interface for all modules.\n")
	sb.WriteString(" * Implement this interface once and pass it to every module's route registrar.\n */\n")
	sb.WriteString("export interface IMiddleware {\n")
	for _, mw := range allMiddleware {
		camelMw := emitter.ToCamelCase(mw)
		sb.WriteString(fmt.Sprintf("  /** %s middleware handler. */\n", mw))
		sb.WriteString(fmt.Sprintf("  %s(req: %s, res: %s, next: () => void): void;\n", camelMw, strat.RequestType(), strat.ResponseType()))
	}
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, "IMiddleware.ts"), []byte(sb.String()), 0644)
}
