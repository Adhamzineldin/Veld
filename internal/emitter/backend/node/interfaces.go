package node

// interfaces.go — emits interfaces/I{Module}Service.ts with typed method signatures.

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/tshelpers"
)

func (e *NodeEmitter) emitInterface(a ast.AST, mod ast.Module, outDir string) error {
	dir := filepath.Join(outDir, "interfaces")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	usedTypes := emitter.CollectUsedTypes(a, mod)
	moduleLower := strings.ToLower(mod.Name)

	// Collect per-action error type names for import
	var errorTypeImports []string
	for _, act := range mod.Actions {
		if len(act.Errors) > 0 {
			errorTypeImports = append(errorTypeImports, act.Name+"Error")
		}
	}

	// Detect whether this module has any WS actions.
	hasWS := false
	for _, act := range mod.Actions {
		if act.Method == "WS" {
			hasWS = true
			break
		}
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	if hasWS {
		sb.WriteString("import type { WebSocket } from 'ws';\n")
	}
	if len(usedTypes) > 0 {
		sb.WriteString(fmt.Sprintf("import type { %s } from '../types/%s';\n", strings.Join(usedTypes, ", "), moduleLower))
	}
	if len(errorTypeImports) > 0 {
		sb.WriteString(fmt.Sprintf("import type { %s } from '../errors/%s.errors';\n",
			strings.Join(errorTypeImports, ", "), moduleLower))
	}

	if mod.Description != "" {
		sb.WriteString(fmt.Sprintf("\n/** %s */\n", mod.Description))
	}
	sb.WriteString(fmt.Sprintf("\nexport interface I%sService {\n", mod.Name))

	for _, act := range mod.Actions {
		// Build JSDoc with description, @deprecated, and @throws for each error
		hasJSDoc := act.Description != "" || act.Deprecated != "" || len(act.Errors) > 0
		if hasJSDoc {
			sb.WriteString("  /**\n")
			if act.Description != "" {
				sb.WriteString(fmt.Sprintf("   * %s\n", act.Description))
			}
			if act.Deprecated != "" {
				sb.WriteString(fmt.Sprintf("   * @deprecated %s\n", act.Deprecated))
			}
			for _, errName := range act.Errors {
				code := emitter.ErrorCode(act.Name, errName)
				sb.WriteString(fmt.Sprintf("   * @throws {%sError} %s — %s\n", act.Name, code, errName))
			}
			sb.WriteString("   */\n")
		}

		routePath := act.Path
		if mod.Prefix != "" {
			routePath = mod.Prefix + act.Path
		}
		pathParams := emitter.ExtractPathParams(routePath)

		// WS actions get lifecycle method signatures instead of a REST method.
		if act.Method == "WS" {
			camelName := emitter.ToCamelCase(act.Name)

			// Build params type literal: { room: string, ... }
			paramFields := make([]string, 0, len(pathParams))
			for _, p := range pathParams {
				paramFields = append(paramFields, p+": string")
			}
			paramsType := "{ " + strings.Join(paramFields, "; ") + " }"
			if len(paramFields) == 0 {
				paramsType = "Record<string, never>"
			}

			// Determine client→server message type.
			emitType := "unknown"
			if act.Emit != "" {
				emitType = act.Emit
			}
			msgOptional := "?"
			if act.Emit != "" {
				msgOptional = ""
			}

			sb.WriteString(fmt.Sprintf("  on%sConnect(socket: WebSocket, params: %s): void | Promise<void>;\n", camelName, paramsType))
			sb.WriteString(fmt.Sprintf("  on%sMessage%s(socket: WebSocket, msg: %s): void | Promise<void>;\n", camelName, msgOptional, emitType))
			sb.WriteString(fmt.Sprintf("  on%sClose?(socket: WebSocket): void;\n", camelName))
			sb.WriteString(fmt.Sprintf("  on%sError?(socket: WebSocket, err: Error): void;\n", camelName))
			continue
		}

		var params []string
		for _, p := range pathParams {
			params = append(params, p+": string")
		}
		if act.Input != "" {
			params = append(params, "input: "+act.Input)
		}
		if act.Query != "" {
			params = append(params, "query: "+act.Query)
		}
		if act.Headers != "" {
			params = append(params, "headers: "+act.Headers)
		}

		outputType := tshelpers.FormatOutputType(act)

		sb.WriteString(fmt.Sprintf("  %s(%s): Promise<%s>;\n",
			emitter.ToCamelCase(act.Name), strings.Join(params, ", "), outputType))
	}
	sb.WriteString("}\n")

	return os.WriteFile(filepath.Join(dir, fmt.Sprintf("I%sService.ts", mod.Name)), []byte(sb.String()), 0644)
}
