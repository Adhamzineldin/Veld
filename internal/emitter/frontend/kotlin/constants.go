package kotlin

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
)

func (e *KotlinEmitter) emitConstants(a ast.AST, outDir string) error {
	if len(a.Constants) == 0 {
		return nil
	}
	dir := filepath.Join(outDir, "client")

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString("package veld.generated.client\n\n")

	for _, cg := range a.Constants {
		if cg.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", cg.Description))
		}
		sb.WriteString(fmt.Sprintf("object %s {\n", cg.Name))
		for _, f := range cg.Fields {
			kType := kotlinConstType(f.Type)
			kVal := kotlinConstValue(f)
			sb.WriteString(fmt.Sprintf("    const val %s: %s = %s\n", f.Name, kType, kVal))
		}
		sb.WriteString("}\n\n")
	}
	return os.WriteFile(filepath.Join(dir, "Constants.kt"), []byte(sb.String()), 0644)
}

func kotlinConstType(veldType string) string {
	switch veldType {
	case "string", "uuid", "date", "datetime", "decimal":
		return "String"
	case "int":
		return "Long"
	case "float":
		return "Double"
	case "bool":
		return "Boolean"
	default:
		return "String"
	}
}

func kotlinConstValue(f ast.ConstantField) string {
	switch f.Type {
	case "string", "uuid", "date", "datetime", "decimal":
		return fmt.Sprintf("%q", f.Value)
	case "int":
		return f.Value + "L"
	case "float":
		return f.Value
	case "bool":
		return f.Value
	default:
		return fmt.Sprintf("%q", f.Value)
	}
}
