func TestJSFrontendSummary(t *testing.T) {
	e := javascript.New()
	lines := e.Summary([]string{"Auth", "Users"})

	if len(lines) == 0 {
		t.Fatal("Summary() returned no lines")
	}
	if lines[0].Dir != "client/" {
		t.Errorf("Summary() first dir = %q, want client/", lines[0].Dir)
	}
}

