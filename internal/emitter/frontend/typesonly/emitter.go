// Package typesonly emits only TypeScript interfaces and Zod schemas (no API client).
package typesonly

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/tshelpers"
)

func init() {
	emitter.RegisterFrontend("types-only", New())
}

type TypesOnlyEmitter struct{}

func New() *TypesOnlyEmitter          { return &TypesOnlyEmitter{} }
func (*TypesOnlyEmitter) IsFrontend() {}

func (e *TypesOnlyEmitter) Summary(_ []string) []emitter.SummaryLine {
	return []emitter.SummaryLine{
		{Dir: "types/", Files: "index.ts"},
		{Dir: "schemas/", Files: "schemas.ts"},
	}
}

func (e *TypesOnlyEmitter) Emit(a ast.AST, outDir string, opts emitter.EmitOptions) error {
	if opts.DryRun {
		return nil
	}

	typesDir := filepath.Join(outDir, "types")
	schemasDir := filepath.Join(outDir, "schemas")
	for _, d := range []string{typesDir, schemasDir} {
		if err := os.MkdirAll(d, 0755); err != nil {
			return err
		}
	}

	if err := e.emitTypes(a, typesDir); err != nil {
		return fmt.Errorf("types: %w", err)
	}
	return e.emitSchemas(a, schemasDir)
}

func (e *TypesOnlyEmitter) emitTypes(a ast.AST, dir string) error {
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")

	// Enums
	for _, en := range a.Enums {
		if en.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", en.Description))
		}
		vals := make([]string, len(en.Values))
		for i, v := range en.Values {
			vals[i] = fmt.Sprintf("  %s = '%s'", v, v)
		}
		sb.WriteString(fmt.Sprintf("export enum %s {\n%s\n}\n\n", en.Name, strings.Join(vals, ",\n")))
	}

	// Interfaces
	for _, m := range a.Models {
		if m.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", m.Description))
		}
		ext := ""
		if m.Extends != "" {
			ext = " extends " + m.Extends
		}
		sb.WriteString(fmt.Sprintf("export interface %s%s {\n", m.Name, ext))
		for _, f := range m.Fields {
			tsType := tshelpers.VeldFieldToTS(f)
			opt := ""
			if f.Optional {
				opt = "?"
			}
			sb.WriteString(fmt.Sprintf("  %s%s: %s;\n", f.Name, opt, tsType))
		}
		sb.WriteString("}\n\n")
	}

	return os.WriteFile(filepath.Join(dir, "index.ts"), []byte(sb.String()), 0644)
}

func (e *TypesOnlyEmitter) emitSchemas(a ast.AST, dir string) error {
	enumNames := make(map[string]bool, len(a.Enums))
	for _, en := range a.Enums {
		enumNames[en.Name] = true
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n")
	sb.WriteString("import { z } from 'zod';\n\n")

	// Enum schemas
	for _, en := range a.Enums {
		if en.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", en.Description))
		}
		quoted := make([]string, len(en.Values))
		for i, v := range en.Values {
			quoted[i] = fmt.Sprintf("'%s'", v)
		}
		sb.WriteString(fmt.Sprintf("export const %sSchema = z.enum([%s]);\n\n", en.Name, strings.Join(quoted, ", ")))
	}

	// Model schemas
	for _, m := range a.Models {
		if m.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", m.Description))
		}
		if m.Extends != "" {
			sb.WriteString(fmt.Sprintf("export const %sSchema = %sSchema.extend({\n", m.Name, m.Extends))
		} else {
			sb.WriteString(fmt.Sprintf("export const %sSchema = z.object({\n", m.Name))
		}
		for _, f := range m.Fields {
			zodType := veldFieldToZod(f, enumNames)
			if f.Default != "" {
				zodType += fmt.Sprintf(".default(%s)", normaliseDefault(f.Default))
			}
			if f.Optional && f.Default == "" {
				zodType += ".optional()"
			}
			sb.WriteString(fmt.Sprintf("  %s: %s,\n", f.Name, zodType))
		}
		sb.WriteString("});\n\n")
	}

	return os.WriteFile(filepath.Join(dir, "schemas.ts"), []byte(sb.String()), 0644)
}

func veldFieldToZod(f ast.Field, enumNames map[string]bool) string {
	if f.IsMap {
		return fmt.Sprintf("z.record(z.string(), %s)", veldTypeToZod(f.MapValueType, false, enumNames))
	}
	return veldTypeToZod(f.Type, f.IsArray, enumNames)
}

func veldTypeToZod(t string, isArray bool, enumNames map[string]bool) string {
	var base string
	switch t {
	case "string":
		base = "z.string()"
	case "int":
		base = "z.number().int()"
	case "float":
		base = "z.number()"
	case "bool":
		base = "z.boolean()"
	case "date", "datetime":
		base = "z.string().datetime()"
	case "uuid":
		base = "z.string().uuid()"
	default:
		if enumNames[t] {
			base = t + "Schema"
		} else {
			base = t + "Schema"
		}
	}
	if isArray {
		return "z.array(" + base + ")"
	}
	return base
}

func normaliseDefault(d string) string {
	if d == "true" || d == "false" {
		return d
	}
	if d == "now()" {
		return "undefined"
	}
	if strings.Contains(d, ".") {
		return d // float literal
	}
	if _, err := fmt.Sscanf(d, "%d", new(int)); err == nil {
		return d // integer literal
	}
	return fmt.Sprintf("'%s'", d)
}
