// Package typesonly emits only TypeScript interfaces (no API client).
package typesonly

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
	"github.com/Adhamzineldin/Veld/internal/emitter/tshelpers"
)

func init() {
	emitter.RegisterFrontend("types-only", New())
}

type TypesOnlyEmitter struct{}

func New() *TypesOnlyEmitter          { return &TypesOnlyEmitter{} }
func (*TypesOnlyEmitter) IsFrontend() {}

func (e *TypesOnlyEmitter) Summary(_ []string) []emitter.SummaryLine {
	return []emitter.SummaryLine{
		{Dir: "types/", Files: "index.ts"},
	}
}

func (e *TypesOnlyEmitter) Emit(a ast.AST, outDir string, opts emitter.EmitOptions) error {
	if opts.DryRun {
		return nil
	}

	typesDir := filepath.Join(outDir, "types")
	if err := os.MkdirAll(typesDir, 0755); err != nil {
		return err
	}

	if err := e.emitTypes(a, typesDir); err != nil {
		return fmt.Errorf("types: %w", err)
	}
	return nil
}

func (e *TypesOnlyEmitter) emitTypes(a ast.AST, dir string) error {
	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\n\n")

	// Enums
	for _, en := range a.Enums {
		if en.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", en.Description))
		}
		vals := make([]string, len(en.Values))
		for i, v := range en.Values {
			vals[i] = fmt.Sprintf("  %s = '%s'", v, v)
		}
		sb.WriteString(fmt.Sprintf("export enum %s {\n%s\n}\n\n", en.Name, strings.Join(vals, ",\n")))
	}

	// Interfaces
	for _, m := range a.Models {
		if m.Description != "" {
			sb.WriteString(fmt.Sprintf("/** %s */\n", m.Description))
		}
		ext := ""
		if m.Extends != "" {
			ext = " extends " + m.Extends
		}
		sb.WriteString(fmt.Sprintf("export interface %s%s {\n", m.Name, ext))
		for _, f := range m.Fields {
			tsType := tshelpers.VeldFieldToTS(f)
			opt := ""
			if f.Optional {
				opt = "?"
			}
			sb.WriteString(fmt.Sprintf("  %s%s: %s;\n", f.Name, opt, tsType))
		}
		sb.WriteString("}\n\n")
	}

	return os.WriteFile(filepath.Join(dir, "index.ts"), []byte(sb.String()), 0644)
}
