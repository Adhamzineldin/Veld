func TestSwiftEmitterRouteDocComments(t *testing.T) {
	e := swift.New()
	outDir := t.TempDir()
	if err := e.Emit(minimalAST(), outDir, emitter.EmitOptions{}); err != nil {
		t.Fatalf("Emit() error: %v", err)
	}

	data, _ := os.ReadFile(filepath.Join(outDir, "client", "APIClient.swift"))
	content := string(data)

	if !strings.Contains(content, "/// POST /api/auth/login") {
		t.Error("missing route doc comment for POST /api/auth/login")
	}
	if !strings.Contains(content, "/// GET /api/auth/me") {
		t.Error("missing route doc comment for GET /api/auth/me")
	}
}
