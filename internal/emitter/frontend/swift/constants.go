package swift

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
)

func (e *SwiftEmitter) emitConstants(a ast.AST, outDir string) error {
	if len(a.Constants) == 0 {
		return nil
	}
	dir := filepath.Join(outDir, "client")

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD — DO NOT EDIT\nimport Foundation\n\n")

	for _, cg := range a.Constants {
		if cg.Description != "" {
			sb.WriteString(fmt.Sprintf("/// %s\n", cg.Description))
		}
		sb.WriteString(fmt.Sprintf("enum %s {\n", cg.Name))
		for _, f := range cg.Fields {
			sType := swiftConstType(f.Type)
			sVal := swiftConstValue(f)
			sb.WriteString(fmt.Sprintf("    static let %s: %s = %s\n", f.Name, sType, sVal))
		}
		sb.WriteString("}\n\n")
	}
	return os.WriteFile(filepath.Join(dir, "Constants.swift"), []byte(sb.String()), 0644)
}

func swiftConstType(veldType string) string {
	switch veldType {
	case "string", "uuid", "date", "datetime", "decimal":
		return "String"
	case "int":
		return "Int64"
	case "float":
		return "Double"
	case "bool":
		return "Bool"
	default:
		return "String"
	}
}

func swiftConstValue(f ast.ConstantField) string {
	switch f.Type {
	case "string", "uuid", "date", "datetime", "decimal":
		return fmt.Sprintf("%q", f.Value)
	case "int", "float":
		return f.Value
	case "bool":
		return f.Value
	default:
		return fmt.Sprintf("%q", f.Value)
	}
}
