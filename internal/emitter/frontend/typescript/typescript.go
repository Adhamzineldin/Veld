package typescript

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/veld-dev/veld/internal/ast"
	"github.com/veld-dev/veld/internal/emitter"
	"github.com/veld-dev/veld/internal/emitter/tshelpers"
)

func init() {
	emitter.RegisterFrontend("typescript", New())
}

// TypeScriptEmitter generates the frontend fetch SDK (generated/client/api.ts).
type TypeScriptEmitter struct{}

func (*TypeScriptEmitter) IsFrontend() {} // marker for FrontendEmitter

// New returns a TypeScriptEmitter.
func New() *TypeScriptEmitter { return &TypeScriptEmitter{} }

// Summary returns a human-friendly breakdown of generated files.
func (e *TypeScriptEmitter) Summary(modules []string) []emitter.SummaryLine {
	return []emitter.SummaryLine{
		{Dir: "client/", Files: "api.ts"},
	}
}

// Emit writes generated/client/api.ts into outDir.
func (e *TypeScriptEmitter) Emit(a ast.AST, outDir string, opts emitter.EmitOptions) error {
	if opts.DryRun {
		return nil
	}
	dir := filepath.Join(outDir, "client")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	// Collect which HTTP methods are used across all actions.
	methods := make(map[string]bool)
	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			methods[strings.ToUpper(act.Method)] = true
		}
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")

	// Collect all used types across all modules (deduplicated)
	allTypes := make(map[string]bool)
	for _, mod := range a.Modules {
		for _, t := range emitter.CollectUsedTypes(a, mod) {
			allTypes[t] = true
		}
	}
	if len(allTypes) > 0 {
		var sorted []string
		for t := range allTypes {
			sorted = append(sorted, t)
		}
		// Stable order: enums first, then models
		var typeList []string
		for _, en := range a.Enums {
			if allTypes[en.Name] {
				typeList = append(typeList, en.Name)
			}
		}
		for _, m := range a.Models {
			if allTypes[m.Name] {
				typeList = append(typeList, m.Name)
			}
		}
		sb.WriteString(fmt.Sprintf("import type { %s } from '../types/types';\n", strings.Join(typeList, ", ")))
	}

	// VeldApiError class
	sb.WriteString(`
export class VeldApiError extends Error {
  status: number;
  body: string;
  constructor(status: number, body: string) {
    super(` + "`" + `Veld API error ${status}: ${body}` + "`" + `);
    this.name = 'VeldApiError';
    this.status = status;
    this.body = body;
  }
}
`)

	// Base URL
	if opts.BaseUrl != "" {
		sb.WriteString(fmt.Sprintf("\nconst BASE = '%s';\n", opts.BaseUrl))
	} else {
		sb.WriteString("\nconst BASE = (typeof process !== 'undefined' && process.env?.VELD_API_URL) || '';\n")
	}

	// Generate HTTP method helpers for each method actually used.
	if methods["GET"] {
		sb.WriteString(`
async function get<T>(path: string): Promise<T> {
  const res = await fetch(BASE + path);
  if (!res.ok) throw new VeldApiError(res.status, await res.text());
  return res.json();
}
`)
	}

	bodyMethods := []string{"POST", "PUT", "PATCH", "DELETE"}
	for _, m := range bodyMethods {
		if methods[m] {
			fn := strings.ToLower(m)
			if m == "DELETE" {
				fn = "del"
			}
			sb.WriteString(fmt.Sprintf(`
async function %s<T>(path: string, body?: unknown): Promise<T> {
  const res = await fetch(BASE + path, {
    method: '%s',
    headers: { 'Content-Type': 'application/json' },
    body: body !== undefined ? JSON.stringify(body) : undefined,
  });
  if (!res.ok) throw new VeldApiError(res.status, await res.text());
  return res.json();
}
`, fn, m))
		}
	}

	// API object
	sb.WriteString("\nexport const api = {\n")
	for _, mod := range a.Modules {
		sb.WriteString(fmt.Sprintf("  %s: {\n", mod.Name))
		for _, act := range mod.Actions {
			outputType := tshelpers.FormatOutputType(act)
			routePath := act.Path
			if mod.Prefix != "" {
				routePath = mod.Prefix + act.Path
			}

			if act.Description != "" {
				sb.WriteString(fmt.Sprintf("    /** %s */\n", act.Description))
			}

			pathParams := emitter.ExtractPathParams(routePath)
			hasPathParams := len(pathParams) > 0
			method := strings.ToUpper(act.Method)
			fnName := strings.ToLower(method)
			if method == "DELETE" {
				fnName = "del"
			}

			// Build URL expression
			var urlExpr string
			if hasPathParams {
				urlExpr = "`" + emitter.ToTemplateLiteral(routePath) + "`"
			} else {
				urlExpr = "'" + routePath + "'"
			}

			// Build function signature params
			var sigParams []string
			for _, p := range pathParams {
				sigParams = append(sigParams, p+": string")
			}
			if act.Input != "" {
				sigParams = append(sigParams, "input: "+act.Input)
			}
			if act.Query != "" {
				sigParams = append(sigParams, "query?: "+act.Query)
			}

			sig := strings.Join(sigParams, ", ")

			// Build query string suffix
			queryAppend := ""
			if act.Query != "" {
				queryAppend = " + (query ? '?' + new URLSearchParams(query as Record<string, string>).toString() : '')"
			}

			// Build the call expression
			if method == "GET" {
				sb.WriteString(fmt.Sprintf("    %s: (%s): Promise<%s> =>\n", act.Name, sig, outputType))
				sb.WriteString(fmt.Sprintf("      get(%s%s),\n", urlExpr, queryAppend))
			} else {
				bodyArg := "input"
				if act.Input == "" {
					bodyArg = "{}"
				}
				sb.WriteString(fmt.Sprintf("    %s: (%s): Promise<%s> =>\n", act.Name, sig, outputType))
				sb.WriteString(fmt.Sprintf("      %s(%s%s, %s),\n", fnName, urlExpr, queryAppend, bodyArg))
			}
		}
		sb.WriteString("  },\n")
	}
	sb.WriteString("};\n")

	return os.WriteFile(filepath.Join(dir, "api.ts"), []byte(sb.String()), 0644)
}
