package typescript

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/veld-dev/veld/internal/ast"
	"github.com/veld-dev/veld/internal/emitter"
	"github.com/veld-dev/veld/internal/emitter/tshelpers"
)

func init() {
	emitter.RegisterFrontend("typescript", New())
}

// TypeScriptEmitter generates the frontend fetch SDK (generated/client/api.ts).
type TypeScriptEmitter struct{}

func (*TypeScriptEmitter) IsFrontend() {} // marker for FrontendEmitter

// New returns a TypeScriptEmitter.
func New() *TypeScriptEmitter { return &TypeScriptEmitter{} }

// Summary returns a human-friendly breakdown of generated files.
func (e *TypeScriptEmitter) Summary(modules []string) []emitter.SummaryLine {
	return []emitter.SummaryLine{
		{Dir: "client/", Files: "api.ts"},
	}
}

// Emit writes generated/client/api.ts into outDir.
func (e *TypeScriptEmitter) Emit(a ast.AST, outDir string) error {
	dir := filepath.Join(outDir, "client")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	needsPost := false
	needsGet := false
	for _, mod := range a.Modules {
		for _, act := range mod.Actions {
			if act.Method == "GET" {
				needsGet = true
			} else {
				needsPost = true
			}
		}
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")

	for _, mod := range a.Modules {
		types := emitter.CollectUsedTypes(a, mod)
		if len(types) > 0 {
			sb.WriteString(fmt.Sprintf("import type { %s } from '../types/%s';\n",
				strings.Join(types, ", "), strings.ToLower(mod.Name)))
		}
	}

	if needsPost {
		sb.WriteString(`
async function post<T>(path: string, body: unknown): Promise<T> {
  const base = process.env.VELD_API_URL ?? '';
  const res = await fetch(base + path, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  });
  if (!res.ok) throw new Error(await res.text());
  return res.json();
}
`)
	}
	if needsGet {
		sb.WriteString(`
async function get<T>(path: string): Promise<T> {
  const base = process.env.VELD_API_URL ?? '';
  const res = await fetch(base + path);
  if (!res.ok) throw new Error(await res.text());
  return res.json();
}
`)
	}

	sb.WriteString("\nexport const api = {\n")
	for _, mod := range a.Modules {
		sb.WriteString(fmt.Sprintf("  %s: {\n", mod.Name))
		for _, act := range mod.Actions {
			outputType := tshelpers.FormatOutputType(act)
			routePath := act.Path
			if mod.Prefix != "" {
				routePath = mod.Prefix + act.Path
			}

			if act.Description != "" {
				sb.WriteString(fmt.Sprintf("    /** %s */\n", act.Description))
			}

			if act.Query != "" && act.Input != "" {
				sb.WriteString(fmt.Sprintf("    %s: (input: %s, query?: %s): Promise<%s> =>\n", act.Name, act.Input, act.Query, outputType))
				sb.WriteString(fmt.Sprintf("      post('%s' + (query ? '?' + new URLSearchParams(query as Record<string, string>).toString() : ''), input),\n", routePath))
			} else if act.Query != "" {
				sb.WriteString(fmt.Sprintf("    %s: (query?: %s): Promise<%s> =>\n", act.Name, act.Query, outputType))
				sb.WriteString(fmt.Sprintf("      get('%s' + (query ? '?' + new URLSearchParams(query as Record<string, string>).toString() : '')),\n", routePath))
			} else if act.Input != "" {
				sb.WriteString(fmt.Sprintf("    %s: (input: %s): Promise<%s> => post('%s', input),\n",
					act.Name, act.Input, outputType, routePath))
			} else if act.Method == "GET" {
				sb.WriteString(fmt.Sprintf("    %s: (): Promise<%s> => get('%s'),\n",
					act.Name, outputType, routePath))
			} else {
				sb.WriteString(fmt.Sprintf("    %s: (): Promise<%s> => post('%s', {}),\n",
					act.Name, outputType, routePath))
			}
		}
		sb.WriteString("  },\n")
	}
	sb.WriteString("};\n")

	return os.WriteFile(filepath.Join(dir, "api.ts"), []byte(sb.String()), 0644)
}
