package typescript

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Adhamzineldin/Veld/internal/ast"
	"github.com/Adhamzineldin/Veld/internal/emitter"
)

// emitServerClient generates generated/server-client/api.ts — a server-to-server
// typed client where the base URL is required (no env-var fallback).
func emitServerClient(a ast.AST, outDir string, opts emitter.EmitOptions) error {
	dir := filepath.Join(outDir, "server-client")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return err
	}

	var sb strings.Builder
	sb.WriteString("// AUTO-GENERATED BY VELD \u2014 DO NOT EDIT\n")
	sb.WriteString("// Server-to-server client \u2014 for use in backend services\n")
	sb.WriteString("\n")

	// VeldApiError (self-contained, no import needed).
	emitErrorClass(&sb)
	sb.WriteString("\n")

	// buildQueryString utility.
	emitBuildQueryString(&sb)
	sb.WriteString("\n")

	// VeldWebSocket (included for completeness — may be unused server-side).
	sb.WriteString(veldWebSocketClass())
	sb.WriteString("\n")

	// Type imports — re-export from ../types if it exists, otherwise inline nothing.
	allUsedTypes := collectAllUsedTypes(a)
	if len(allUsedTypes) > 0 {
		sb.WriteString(fmt.Sprintf("import type { %s } from '../types';\n\n", strings.Join(allUsedTypes, ", ")))
	}

	// Per-module classes (server SDK variant: required baseUrl).
	for _, mod := range a.Modules {
		defaultBase := opts.BaseUrlForModule(mod.Name)
		if mod.BaseUrl != "" && defaultBase == "" {
			defaultBase = mod.BaseUrl
		}
		writeModuleClass(&sb, a, mod, defaultBase, true /* serverSdk */)
		sb.WriteString("\n")
	}

	// Root composer class (server SDK variant).
	writeVeldApiClient(&sb, a.Modules, true /* serverSdk */)

	return os.WriteFile(filepath.Join(dir, "api.ts"), []byte(sb.String()), 0644)
}

// collectAllUsedTypes returns the union of all used types across all modules.
func collectAllUsedTypes(a ast.AST) []string {
	seen := make(map[string]bool)
	var result []string
	for _, mod := range a.Modules {
		for _, t := range emitter.CollectUsedTypes(a, mod) {
			if !seen[t] {
				seen[t] = true
				result = append(result, t)
			}
		}
	}
	return result
}
